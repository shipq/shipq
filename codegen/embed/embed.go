package embed

import (
	"bytes"
	"fmt"
	"io/fs"
	"os"
	"path/filepath"
	"strings"

	shipqsrc "github.com/shipq/shipq"
)

// shipqModulePath is the canonical import path for the shipq module.
// Embedded source files contain imports using this prefix; they are
// rewritten to the user's module path at embed time.
const shipqModulePath = "github.com/shipq/shipq/"

type embeddedPackage struct {
	fs      fs.FS
	srcDir  string
	destDir string
}

// EmbedAllPackages writes embedded shipq library source files into
// <shipqRoot>/shipq/lib so generated projects are self-contained.
// Import paths within the embedded files that reference
// "github.com/shipq/shipq/..." are rewritten to use the user's
// modulePath + "/shipq/lib/..." so the generated project never
// depends on the published shipq module.
// EmbedOptions controls which optional packages are embedded.
type EmbedOptions struct {
	FilesEnabled   bool
	WorkersEnabled bool
}

func EmbedAllPackages(shipqRoot, modulePath string, opts EmbedOptions) error {
	packages := []embeddedPackage{
		{fs: shipqsrc.HandlerFS, srcDir: "handler", destDir: filepath.Join("shipq", "lib", "handler")},
		{fs: shipqsrc.HttperrorFS, srcDir: "httperror", destDir: filepath.Join("shipq", "lib", "httperror")},
		{fs: shipqsrc.HttpserverFS, srcDir: "httpserver", destDir: filepath.Join("shipq", "lib", "httpserver")},
		{fs: shipqsrc.LoggingFS, srcDir: "logging", destDir: filepath.Join("shipq", "lib", "logging")},
		{fs: shipqsrc.CryptoFS, srcDir: "crypto", destDir: filepath.Join("shipq", "lib", "crypto")},
		{fs: shipqsrc.NanoidFS, srcDir: "nanoid", destDir: filepath.Join("shipq", "lib", "nanoid")},
		{fs: shipqsrc.HttputilFS, srcDir: "httputil", destDir: filepath.Join("shipq", "lib", "httputil")},
		{fs: shipqsrc.QueryFS, srcDir: filepath.Join("db", "portsql", "query"), destDir: filepath.Join("shipq", "lib", "db", "portsql", "query")},
		{fs: shipqsrc.QueryCompileFS, srcDir: filepath.Join("db", "portsql", "query", "compile"), destDir: filepath.Join("shipq", "lib", "db", "portsql", "query", "compile")},
		{fs: shipqsrc.MigrateFS, srcDir: filepath.Join("db", "portsql", "migrate"), destDir: filepath.Join("shipq", "lib", "db", "portsql", "migrate")},
		{fs: shipqsrc.DdlFS, srcDir: filepath.Join("db", "portsql", "ddl"), destDir: filepath.Join("shipq", "lib", "db", "portsql", "ddl")},
		{fs: shipqsrc.RefFS, srcDir: filepath.Join("db", "portsql", "ref"), destDir: filepath.Join("shipq", "lib", "db", "portsql", "ref")},
		{fs: shipqsrc.ProptestFS, srcDir: "proptest", destDir: filepath.Join("shipq", "lib", "proptest")},
	}

	if opts.FilesEnabled {
		packages = append(packages, embeddedPackage{
			fs: shipqsrc.FilestorageFS, srcDir: "filestorage",
			destDir: filepath.Join("shipq", "lib", "filestorage"),
		})
	}

	if opts.WorkersEnabled {
		packages = append(packages, embeddedPackage{
			fs: shipqsrc.ChannelFS, srcDir: "channel",
			destDir: filepath.Join("shipq", "lib", "channel"),
		})
	}

	for _, pkg := range packages {
		if err := copyEmbeddedPackage(pkg, shipqRoot, modulePath); err != nil {
			return err
		}
	}

	// Copy static assets (JS, CSS) for development tooling.
	// These are non-Go files and do not need import rewriting.
	assetsDestDir := filepath.Join(shipqRoot, "shipq", "assets")
	if err := copyEmbeddedAssets(shipqsrc.AssetsFS, "assets", assetsDestDir, shipqRoot); err != nil {
		return err
	}

	// Generate the embed.go file for the assets package so the user's
	// generated server code can import the assets at compile time.
	if err := generateAssetsEmbed(assetsDestDir); err != nil {
		return err
	}

	return nil
}

// copyEmbeddedAssets copies non-Go asset files (JS, CSS, etc.) from the
// embedded filesystem to the user's project. No import rewriting is done.
func copyEmbeddedAssets(fsys fs.FS, srcDir, destDir, shipqRoot string) error {
	entries, err := fs.ReadDir(fsys, srcDir)
	if err != nil {
		return fmt.Errorf("read embedded assets dir %q: %w", srcDir, err)
	}

	if err := os.MkdirAll(destDir, 0o755); err != nil {
		return fmt.Errorf("create assets dir %q: %w", destDir, err)
	}

	for _, entry := range entries {
		if entry.IsDir() {
			continue
		}

		srcPath := filepath.Join(srcDir, entry.Name())
		content, err := fs.ReadFile(fsys, srcPath)
		if err != nil {
			return fmt.Errorf("read embedded asset %q: %w", srcPath, err)
		}

		destPath := filepath.Join(destDir, entry.Name())
		if err := os.WriteFile(destPath, content, 0o644); err != nil {
			return fmt.Errorf("write embedded asset %q: %w", destPath, err)
		}
	}

	return nil
}

// generateAssetsEmbed writes a shipq/assets/embed.go file that uses //go:embed
// to provide the JS/CSS files as exported byte slices.
func generateAssetsEmbed(destDir string) error {
	content := []byte(`// Code generated by shipq.
package assets

import _ "embed"

//go:embed web-components.min.js
var WebComponentsJS []byte

//go:embed styles.min.css
var StylesCSS []byte

//go:embed admin.min.js
var AdminJS []byte
`)
	return os.WriteFile(filepath.Join(destDir, "embed.go"), content, 0o644)
}

func copyEmbeddedPackage(pkg embeddedPackage, shipqRoot, modulePath string) error {
	entries, err := fs.ReadDir(pkg.fs, pkg.srcDir)
	if err != nil {
		return fmt.Errorf("read embedded dir %q: %w", pkg.srcDir, err)
	}

	destDir := filepath.Join(shipqRoot, pkg.destDir)
	if err := os.MkdirAll(destDir, 0o755); err != nil {
		return fmt.Errorf("create destination dir %q: %w", destDir, err)
	}

	// Prepare import rewrite: github.com/shipq/shipq/ -> <modulePath>/shipq/lib/
	oldImport := []byte(shipqModulePath)
	newImport := []byte(modulePath + "/shipq/lib/")

	for _, entry := range entries {
		if entry.IsDir() || filepath.Ext(entry.Name()) != ".go" {
			continue
		}
		if strings.HasSuffix(entry.Name(), "_test.go") {
			continue
		}

		srcPath := filepath.Join(pkg.srcDir, entry.Name())
		content, err := fs.ReadFile(pkg.fs, srcPath)
		if err != nil {
			return fmt.Errorf("read embedded file %q: %w", srcPath, err)
		}

		// Rewrite imports from the canonical shipq module to the
		// user's embedded lib path so the generated project is
		// fully self-contained.
		content = bytes.ReplaceAll(content, oldImport, newImport)

		destPath := filepath.Join(destDir, entry.Name())
		if err := os.WriteFile(destPath, content, 0o644); err != nil {
			return fmt.Errorf("write embedded file %q: %w", destPath, err)
		}
	}

	return nil
}
