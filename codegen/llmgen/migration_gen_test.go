func TestGenerateLLMMigration_SetCurrentMigrationBeforeMessages(t *testing.T) {
	src := GenerateLLMMigration("20260615120000", "myapp", false, false)
	code := string(src)

	// The generated code must call SetCurrentMigration before the second AddTable
	// to prevent the migration plan from falling back to a runtime-generated timestamp.
	expectedCall := `plan.SetCurrentMigration("20260615120001_llm_messages")`
	if !strings.Contains(code, expectedCall) {
		t.Errorf("expected SetCurrentMigration call before llm_messages AddTable:\n  wanted: %s\n  code:\n%s", expectedCall, code)
	}

	// Verify ordering: SetCurrentMigration must appear between the two AddTable calls.
	convIdx := strings.Index(code, `plan.AddTable("llm_conversations"`)
	setIdx := strings.Index(code, `plan.SetCurrentMigration(`)
	msgIdx := strings.Index(code, `plan.AddTable("llm_messages"`)

	if convIdx < 0 || setIdx < 0 || msgIdx < 0 {
		t.Fatal("all three calls (AddTable conversations, SetCurrentMigration, AddTable messages) must be present")
	}
	if !(convIdx < setIdx && setIdx < msgIdx) {
		t.Error("expected SetCurrentMigration to appear after llm_conversations AddTable and before llm_messages AddTable")
	}
}

func TestIncrementTimestamp(t *testing.T) {
	tests := []struct {
		input    string
		expected string
	}{
		{"20260615120000", "20260615120001"},
		{"20260615120059", "20260615120100"},
		{"20260615125959", "20260615130000"},
		{"20260615235959", "20260616000000"},
		{"20261231235959", "20270101000000"},
	}

	for _, tt := range tests {
		t.Run(tt.input, func(t *testing.T) {
			got := incrementTimestamp(tt.input)
			if got != tt.expected {
				t.Errorf("incrementTimestamp(%q) = %q, want %q", tt.input, got, tt.expected)
			}
		})
	}
}

