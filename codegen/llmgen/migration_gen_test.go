func TestGenerateLLMMigration_ErrorHandlingBetweenTables(t *testing.T) {
	src := GenerateLLMMigration("20260615120000", "myapp", false, false)
	code := string(src)

	// There should be an error check between the two AddTable calls.
	convIdx := strings.Index(code, `plan.AddTable("llm_conversations"`)
	msgIdx := strings.Index(code, `plan.AddTable("llm_messages"`)

	if convIdx < 0 || msgIdx < 0 {
		t.Fatal("both table creation calls must be present")
	}

	between := code[convIdx:msgIdx]
	if !strings.Contains(between, "if err != nil") {
		t.Error("expected error check between llm_conversations and llm_messages creation")
	}
}

