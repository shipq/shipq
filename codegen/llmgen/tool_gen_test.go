func TestGenerateToolRegistry_SchemaEmbeddedAsVar(t *testing.T) {
	schema := `{"type":"object","properties":{"city":{"type":"string"},"country":{"type":"string"}},"required":["city","country"],"additionalProperties":false}`
	pkg := llmcompile.SerializedToolPackage{
		PackagePath: "myapp/tools/weather",
		PackageName: "weather",
		Tools: []llmcompile.SerializedToolInfo{
			{
				Name:        "get_weather",
				Description: "Get weather",
				FuncName:    "GetWeather",
				PackagePath: "myapp/tools/weather",
				PackageName: "weather",
				InputSchema: json.RawMessage(schema),
				InputType:   "WeatherInput",
				OutputType:  "WeatherOutput",
			},
		},
	}

	src, err := GenerateToolRegistry(pkg, "myapp")
	if err != nil {
		t.Fatalf("GenerateToolRegistry failed: %v", err)
	}

	code := string(src)

	// The schema should be embedded as a json.RawMessage variable.
	if !strings.Contains(code, "json.RawMessage(") || !strings.Contains(code, "getWeatherSchema") {
		t.Error("expected schema to be embedded as a json.RawMessage variable")
	}

	// The schema content should appear in the source.
	if !strings.Contains(code, `"city"`) {
		t.Error("expected schema to contain city property")
	}
	if !strings.Contains(code, `"country"`) {
		t.Error("expected schema to contain country property")
	}
}

