package llmgen

import (
	"fmt"
	"time"
)

// GenerateLLMMigration generates a migration file that creates the
// llm_conversations and llm_messages tables. The generated migration follows
// the same pattern as GenerateJobResultsMigration in channelgen.
//
// Parameters:
//   - timestamp: the migration timestamp (e.g., "20260615120000")
//   - modulePath: the user's Go module path (e.g., "myapp")
//   - hasTenancy: if true, adds an organization_id column to llm_conversations
//
// Returns the generated Go source code for the migration file.
func GenerateLLMMigration(timestamp, modulePath string, hasTenancy bool) []byte {
	orgColumn := ""
	if hasTenancy {
		orgColumn = `		tb.Bigint("organization_id").Nullable()
`
	}

	// Compute a second timestamp for the llm_messages table migration entry.
	// This ensures the second AddTable call gets a deterministic, properly
	// ordered migration name instead of falling back to a runtime-generated
	// timestamp (which can conflict with auth migration timestamps).
	messagesTimestamp := incrementTimestamp(timestamp)

	return []byte(fmt.Sprintf(`// Code generated by shipq. DO NOT EDIT.
package migrations

import (
	"%s/shipq/lib/db/portsql/ddl"
	"%s/shipq/lib/db/portsql/migrate"
)

func Migrate_%s_llm_tables(plan *migrate.MigrationPlan) error {
	_, err := plan.AddTable("llm_conversations", func(tb *ddl.TableBuilder) error {
		tb.String("job_id")
		tb.String("channel_name")
		tb.Bigint("account_id").Nullable()
%s		tb.String("provider")
		tb.String("model")
		tb.Text("system_prompt").Nullable()
		tb.Integer("total_input_tokens").Default(0)
		tb.Integer("total_output_tokens").Default(0)
		tb.Integer("tool_call_count").Default(0)
		tb.String("status").Default("running")
		tb.Text("error_message").Nullable()
		tb.Datetime("started_at")
		tb.Datetime("completed_at").Nullable()
		return nil
	})
	if err != nil {
		return err
	}

	plan.SetCurrentMigration("%s_llm_messages")
	_, err = plan.AddTable("llm_messages", func(tb *ddl.TableBuilder) error {
		tb.Bigint("conversation_id")
		tb.Integer("sequence")
		tb.String("role")
		tb.Text("content").Nullable()
		tb.String("tool_name").Nullable()
		tb.String("tool_call_id").Nullable()
		tb.JSON("tool_input").Nullable()
		tb.JSON("tool_output").Nullable()
		tb.Text("tool_error").Nullable()
		tb.Integer("tool_duration_ms").Nullable()
		tb.Integer("input_tokens").Default(0)
		tb.Integer("output_tokens").Default(0)
		return nil
	})
	return err
}
`, modulePath, modulePath, timestamp, orgColumn, messagesTimestamp))
}

// incrementTimestamp parses a 14-digit migration timestamp (YYYYMMDDHHMMSS)
// and returns the timestamp incremented by one second. This is used to give
// the second table in a multi-table migration a deterministic, properly
// ordered migration name.
func incrementTimestamp(timestamp string) string {
	t, err := time.Parse("20060102150405", timestamp)
	if err != nil {
		// Fallback: just append "1" — this shouldn't happen with valid timestamps.
		return timestamp + "1"
	}
	return t.Add(time.Second).Format("20060102150405")
}
