func TestGenerateLLMQuerydefs_QueryTypeBreakdown(t *testing.T) {
	src := GenerateLLMQuerydefs("myapp", false)
	code := string(src)

	// Verify the query type for each query matches the spec.
	tests := []struct {
		queryName string
		queryType string
	}{
		{"InsertLLMConversation", "MustDefineOne"},
		{"UpdateLLMConversation", "MustDefineExec"},
		{"InsertLLMMessage", "MustDefineExec"},
		{"GetLLMConversation", "MustDefineOne"},
		{"ListLLMMessagesByConversation", "MustDefineMany"},
		{"ListLLMConversationsByJob", "MustDefineMany"},
	}

	for _, tt := range tests {
		idx := strings.Index(code, `"`+tt.queryName+`"`)
		if idx < 0 {
			t.Errorf("query %s not found", tt.queryName)
			continue
		}

		// Look backwards to find which MustDefine* was used.
		prefix := code[:idx]
		lastQueryDot := strings.LastIndex(prefix, "query.")
		if lastQueryDot < 0 {
			t.Errorf("query %s: could not find query. prefix", tt.queryName)
			continue
		}

		snippet := prefix[lastQueryDot:]
		if !strings.Contains(snippet, tt.queryType) {
			t.Errorf("query %s: expected %s, got snippet: %q", tt.queryName, tt.queryType, snippet)
		}
	}
}

