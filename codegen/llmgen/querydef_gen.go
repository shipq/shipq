package llmgen

import (
	"bytes"
	"fmt"
	"go/format"
)

// GenerateLLMQuerydefs generates querydefs/llm/queries.go with typed query
// definitions for the llm_conversations and llm_messages tables. It follows
// the same pattern as GenerateJobResultsQuerydefs in channelgen.
//
// Parameters:
//   - modulePath: the user's Go module path (e.g., "myapp")
//   - hasTenancy: if true, includes organization_id in relevant queries
//   - hasAuth: if true, includes author_account_id (nullable *int64) in
//     InsertLLMConversation for author tracking
//
// Returns the generated Go source code for the querydefs file.
func GenerateLLMQuerydefs(modulePath string, hasTenancy bool, hasAuth bool) []byte {
	var buf bytes.Buffer

	schemaPkg := modulePath + "/shipq/db/schema"
	queryPkg := modulePath + "/shipq/lib/db/portsql/query"

	buf.WriteString("// Code generated by shipq. DO NOT EDIT.\n")
	buf.WriteString("package llm\n\n")
	buf.WriteString("import (\n")
	fmt.Fprintf(&buf, "\t%q\n", schemaPkg)
	fmt.Fprintf(&buf, "\t%q\n", queryPkg)
	buf.WriteString(")\n\n")

	buf.WriteString("func init() {\n")

	// ── InsertLLMConversation ────────────────────────────────────────────
	buf.WriteString("\t// InsertLLMConversation creates a new conversation row and returns its id + public_id.\n")
	buf.WriteString("\tquery.MustDefineOne(\"InsertLLMConversation\",\n")
	buf.WriteString("\t\tquery.InsertInto(schema.LlmConversations).\n")
	buf.WriteString("\t\t\tColumns(\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.PublicId(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.JobId(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.ChannelName(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.AccountId(),\n")
	if hasAuth {
		buf.WriteString("\t\t\t\tschema.LlmConversations.AuthorAccountId(),\n")
	}
	if hasTenancy {
		buf.WriteString("\t\t\t\tschema.LlmConversations.OrganizationId(),\n")
	}
	buf.WriteString("\t\t\t\tschema.LlmConversations.Provider(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.Model(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.Status(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.StartedAt(),\n")
	buf.WriteString("\t\t\t).\n")
	buf.WriteString("\t\t\tValues(\n")
	buf.WriteString("\t\t\t\tquery.Param[string](\"publicId\"),\n")
	buf.WriteString("\t\t\t\tquery.Param[string](\"jobId\"),\n")
	buf.WriteString("\t\t\t\tquery.Param[string](\"channelName\"),\n")
	buf.WriteString("\t\t\t\tquery.Param[int64](\"accountId\"),\n")
	if hasAuth {
		buf.WriteString("\t\t\t\tquery.Param[*int64](\"authorAccountId\"),\n")
	}
	if hasTenancy {
		buf.WriteString("\t\t\t\tquery.Param[int64](\"organizationId\"),\n")
	}
	buf.WriteString("\t\t\t\tquery.Param[string](\"provider\"),\n")
	buf.WriteString("\t\t\t\tquery.Param[string](\"model\"),\n")
	buf.WriteString("\t\t\t\tquery.Param[string](\"status\"),\n")
	buf.WriteString("\t\t\t\tquery.Param[string](\"startedAt\"),\n")
	buf.WriteString("\t\t\t).\n")
	buf.WriteString("\t\t\tReturning(\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.Id(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.PublicId(),\n")
	buf.WriteString("\t\t\t).\n")
	buf.WriteString("\t\t\tBuild())\n\n")

	// ── UpdateLLMConversation ────────────────────────────────────────────
	buf.WriteString("\t// UpdateLLMConversation updates status, tokens, tool_call_count, error_message, and completed_at.\n")
	buf.WriteString("\tquery.MustDefineExec(\"UpdateLLMConversation\",\n")
	buf.WriteString("\t\tquery.Update(schema.LlmConversations).\n")
	buf.WriteString("\t\t\tSet(schema.LlmConversations.Status(), query.Param[string](\"status\")).\n")
	buf.WriteString("\t\t\tSet(schema.LlmConversations.TotalInputTokens(), query.Param[int](\"totalInputTokens\")).\n")
	buf.WriteString("\t\t\tSet(schema.LlmConversations.TotalOutputTokens(), query.Param[int](\"totalOutputTokens\")).\n")
	buf.WriteString("\t\t\tSet(schema.LlmConversations.ToolCallCount(), query.Param[int](\"toolCallCount\")).\n")
	buf.WriteString("\t\t\tSet(schema.LlmConversations.ErrorMessage(), query.Param[*string](\"errorMessage\")).\n")
	buf.WriteString("\t\t\tSet(schema.LlmConversations.CompletedAt(), query.Param[*string](\"completedAt\")).\n")
	buf.WriteString("\t\t\tWhere(schema.LlmConversations.Id().Eq(query.Param[int64](\"id\"))).\n")
	buf.WriteString("\t\t\tBuild())\n\n")

	// ── InsertLLMMessage ─────────────────────────────────────────────────
	buf.WriteString("\t// InsertLLMMessage inserts a single message row.\n")
	buf.WriteString("\tquery.MustDefineExec(\"InsertLLMMessage\",\n")
	buf.WriteString("\t\tquery.InsertInto(schema.LlmMessages).\n")
	buf.WriteString("\t\t\tColumns(\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.ConversationId(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.Role(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.Content(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.ToolName(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.ToolCallId(),\n")
	buf.WriteString("\t\t\t).\n")
	buf.WriteString("\t\t\tValues(\n")
	buf.WriteString("\t\t\t\tquery.Param[int64](\"conversationId\"),\n")
	buf.WriteString("\t\t\t\tquery.Param[string](\"role\"),\n")
	buf.WriteString("\t\t\t\tquery.Param[*string](\"content\"),\n")
	buf.WriteString("\t\t\t\tquery.Param[*string](\"toolName\"),\n")
	buf.WriteString("\t\t\t\tquery.Param[*string](\"toolCallId\"),\n")
	buf.WriteString("\t\t\t).\n")
	buf.WriteString("\t\t\tBuild())\n\n")

	// ── GetLLMConversation ───────────────────────────────────────────────
	buf.WriteString("\t// GetLLMConversation retrieves a conversation by public_id.\n")
	buf.WriteString("\tquery.MustDefineOne(\"GetLLMConversation\",\n")
	buf.WriteString("\t\tquery.From(schema.LlmConversations).\n")
	buf.WriteString("\t\t\tSelect(\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.Id(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.PublicId(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.JobId(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.ChannelName(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.AccountId(),\n")
	if hasTenancy {
		buf.WriteString("\t\t\t\tschema.LlmConversations.OrganizationId(),\n")
	}
	buf.WriteString("\t\t\t\tschema.LlmConversations.Provider(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.Model(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.SystemPrompt(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.TotalInputTokens(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.TotalOutputTokens(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.ToolCallCount(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.Status(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.ErrorMessage(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.StartedAt(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.CompletedAt(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.CreatedAt(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.UpdatedAt(),\n")
	buf.WriteString("\t\t\t).\n")
	buf.WriteString("\t\t\tWhere(schema.LlmConversations.PublicId().Eq(query.Param[string](\"publicId\"))).\n")
	buf.WriteString("\t\t\tBuild())\n\n")

	// ── ListLLMMessagesByConversation ─────────────────────────────────────
	buf.WriteString("\t// ListLLMMessagesByConversation lists all messages for a conversation, ordered by sequence.\n")
	buf.WriteString("\tquery.MustDefineMany(\"ListLLMMessagesByConversation\",\n")
	buf.WriteString("\t\tquery.From(schema.LlmMessages).\n")
	buf.WriteString("\t\t\tSelect(\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.Id(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.ConversationId(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.Sequence(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.Role(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.Content(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.ToolName(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.ToolCallId(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.ToolInput(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.ToolOutput(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.ToolError(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.ToolDurationMs(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.InputTokens(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.OutputTokens(),\n")
	buf.WriteString("\t\t\t\tschema.LlmMessages.CreatedAt(),\n")
	buf.WriteString("\t\t\t).\n")
	buf.WriteString("\t\t\tWhere(schema.LlmMessages.ConversationId().Eq(query.Param[int64](\"conversationId\"))).\n")
	buf.WriteString("\t\t\tOrderBy(schema.LlmMessages.Sequence().Asc()).\n")
	buf.WriteString("\t\t\tBuild())\n\n")

	// ── ListLLMConversationsByJob ────────────────────────────────────────
	buf.WriteString("\t// ListLLMConversationsByJob lists conversations for a given job_id.\n")
	buf.WriteString("\tquery.MustDefineMany(\"ListLLMConversationsByJob\",\n")
	buf.WriteString("\t\tquery.From(schema.LlmConversations).\n")
	buf.WriteString("\t\t\tSelect(\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.Id(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.PublicId(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.JobId(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.ChannelName(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.AccountId(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.Status(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.StartedAt(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.CompletedAt(),\n")
	buf.WriteString("\t\t\t\tschema.LlmConversations.CreatedAt(),\n")
	buf.WriteString("\t\t\t).\n")
	buf.WriteString("\t\t\tWhere(schema.LlmConversations.JobId().Eq(query.Param[string](\"jobId\"))).\n")
	buf.WriteString("\t\t\tOrderBy(schema.LlmConversations.StartedAt().Asc()).\n")
	buf.WriteString("\t\t\tBuild())\n\n")

	buf.WriteString("}\n")

	return formatQuerydefSourceLLM(buf.Bytes())
}

// formatQuerydefSourceLLM attempts to gofmt the source. If formatting fails,
// the raw source is returned so the caller can debug the output.
func formatQuerydefSourceLLM(src []byte) []byte {
	formatted, err := format.Source(src)
	if err != nil {
		return src
	}
	return formatted
}
