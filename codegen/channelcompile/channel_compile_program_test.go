func TestGenerateChannelCompileProgram(t *testing.T) {
	tests := []struct {
		name     string
		cfg      ChannelCompileProgramConfig
		contains []string
	}{
		{
			name: "basic config with one package",
			cfg: ChannelCompileProgramConfig{
				ModulePath:  "myapp",
				ChannelPkgs: []string{"myapp/channels/email"},
			},
			contains: []string{
				"package main",
				`"myapp/shipq/lib/channel"`,
				`ch0 "myapp/channels/email"`,
				"ch0.Register(app)",
				"app := channel.NewApp()",
				"json.MarshalIndent",
			},
		},
		{
			name: "multiple packages",
			cfg: ChannelCompileProgramConfig{
				ModulePath: "github.com/example/myapp",
				ChannelPkgs: []string{
					"github.com/example/myapp/channels/email",
					"github.com/example/myapp/channels/chatbot",
					"github.com/example/myapp/channels/notifications",
				},
			},
			contains: []string{
				`ch0 "github.com/example/myapp/channels/chatbot"`,       // sorted alphabetically
				`ch1 "github.com/example/myapp/channels/email"`,         // sorted alphabetically
				`ch2 "github.com/example/myapp/channels/notifications"`, // sorted alphabetically
				"ch0.Register(app)",
				"ch1.Register(app)",
				"ch2.Register(app)",
			},
		},
		{
			name: "empty packages",
			cfg: ChannelCompileProgramConfig{
				ModulePath:  "myapp",
				ChannelPkgs: []string{},
			},
			contains: []string{
				"package main",
				"app := channel.NewApp()",
			},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			result, err := GenerateChannelCompileProgram(tt.cfg)
			if err != nil {
				t.Fatalf("unexpected error: %v", err)
			}

			code := string(result)

			for _, expected := range tt.contains {
				if !strings.Contains(code, expected) {
					t.Errorf("expected code to contain %q\nGot:\n%s", expected, code)
				}
			}
		})
	}
}

func TestGenerateChannelCompileProgram_ValidGo(t *testing.T) {
	cfg := ChannelCompileProgramConfig{
		ModulePath: "myapp",
		ChannelPkgs: []string{
			"myapp/channels/email",
			"myapp/channels/chatbot",
		},
	}

	result, err := GenerateChannelCompileProgram(cfg)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	code := string(result)

	// Check for proper structure
	if !strings.HasPrefix(code, "// Code generated by shipq") {
		t.Error("expected generated code header")
	}

	// Check that it has the main function
	if !strings.Contains(code, "func main()") {
		t.Error("expected main function")
	}

	// Check for serialization types
	expectedTypes := []string{
		"type SerializedChannelInfo struct",
		"type SerializedRateLimitConfig struct",
		"type SerializedMessageInfo struct",
		"type SerializedFieldInfo struct",
		"type SerializedStructInfo struct",
	}

	for _, typ := range expectedTypes {
		if !strings.Contains(code, typ) {
			t.Errorf("expected code to contain %q", typ)
		}
	}

	// Check for helper functions
	expectedFuncs := []string{
		"func visibilityToString",
		"func convertRateLimit",
		"func convertMessages",
		"func convertChannelFields",
		"func convertChannelStructInfo",
	}

	for _, fn := range expectedFuncs {
		if !strings.Contains(code, fn) {
			t.Errorf("expected code to contain %q", fn)
		}
	}
}

