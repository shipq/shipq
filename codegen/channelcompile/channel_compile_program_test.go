func TestGenerateChannelCompileProgram_PackagePathAssignment(t *testing.T) {
	cfg := ChannelCompileProgramConfig{
		ModulePath: "myapp",
		ChannelPkgs: []string{
			"myapp/channels/email",
			"myapp/channels/chatbot",
		},
	}

	result, err := GenerateChannelCompileProgram(cfg)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	code := string(result)

	// Verify the generated code populates PackagePath and PackageName
	if !strings.Contains(code, "PackagePath") {
		t.Error("expected code to set PackagePath")
	}
	if !strings.Contains(code, "PackageName") {
		t.Error("expected code to set PackageName")
	}

	// Verify the channel import strings are embedded for package path assignment
	if !strings.Contains(code, `"myapp/channels/email"`) {
		t.Error("expected channel import path to be embedded in generated code")
	}
	if !strings.Contains(code, `"myapp/channels/chatbot"`) {
		t.Error("expected channel import path to be embedded in generated code")
	}
}
