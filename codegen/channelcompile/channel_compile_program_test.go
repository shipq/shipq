func TestGenerateChannelCompileProgram_PackagesSorted(t *testing.T) {
	cfg := ChannelCompileProgramConfig{
		ModulePath: "myapp",
		ChannelPkgs: []string{
			"myapp/channels/zebra",
			"myapp/channels/alpha",
			"myapp/channels/middle",
		},
	}

	result, err := GenerateChannelCompileProgram(cfg)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	code := string(result)

	// Find positions of imports
	alphaPos := strings.Index(code, `"myapp/channels/alpha"`)
	middlePos := strings.Index(code, `"myapp/channels/middle"`)
	zebraPos := strings.Index(code, `"myapp/channels/zebra"`)

	if alphaPos == -1 || middlePos == -1 || zebraPos == -1 {
		t.Fatal("expected all packages to be in output")
	}

	if !(alphaPos < middlePos && middlePos < zebraPos) {
		t.Error("expected packages to be sorted alphabetically")
	}
}

