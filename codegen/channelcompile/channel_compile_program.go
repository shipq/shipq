package channelcompile

import (
	"bytes"
	"fmt"
	"go/format"
	"sort"
)

// ChannelCompileProgramConfig holds configuration for generating the channel
// compile program.
type ChannelCompileProgramConfig struct {
	ModulePath  string   // e.g., "myapp"
	ChannelPkgs []string // import paths to channel packages with Register functions
}

// GenerateChannelCompileProgram generates the temporary program that extracts
// channel registrations. The generated program imports all channel packages,
// calls their Register functions, then serializes the registry for processing.
func GenerateChannelCompileProgram(cfg ChannelCompileProgramConfig) ([]byte, error) {
	var buf bytes.Buffer

	// Sort for deterministic output
	pkgs := make([]string, len(cfg.ChannelPkgs))
	copy(pkgs, cfg.ChannelPkgs)
	sort.Strings(pkgs)

	channelPkg := cfg.ModulePath + "/shipq/lib/channel"

	buf.WriteString(`// Code generated by shipq. This file can be run manually for debugging.
//
// To run manually:
//   cd <project_root>
//   go run .shipq/channel_compile/main.go
//
// Output: JSON array of channel definitions to stdout
// Errors: Written to stderr
//
package main

import (
	"encoding/json"
	"fmt"
	"os"
	"strings"

`)
	fmt.Fprintf(&buf, "\t%q\n", channelPkg)

	// Write imports for channel packages
	if len(pkgs) > 0 {
		buf.WriteString("\n\t// Channel packages - importing to call their Register functions\n")
		for i, pkg := range pkgs {
			fmt.Fprintf(&buf, "\tch%d %q\n", i, pkg)
		}
	}

	buf.WriteString(`)

// SerializedChannelInfo is a JSON-serializable version of channel.ChannelInfo.
type SerializedChannelInfo struct {
	Name           string                     ` + "`json:\"name\"`" + `
	Visibility     string                     ` + "`json:\"visibility\"`" + `
	IsPublic       bool                       ` + "`json:\"is_public\"`" + `
	RateLimit      *SerializedRateLimitConfig ` + "`json:\"rate_limit,omitempty\"`" + `
	Messages       []SerializedMessageInfo    ` + "`json:\"messages\"`" + `
	MaxRetries     int                        ` + "`json:\"max_retries\"`" + `
	BackoffSeconds int                        ` + "`json:\"backoff_seconds\"`" + `
	TimeoutSeconds int                        ` + "`json:\"timeout_seconds\"`" + `
	RequiredRole   string                     ` + "`json:\"required_role\"`" + `
	PackagePath    string                     ` + "`json:\"package_path\"`" + `
	PackageName    string                     ` + "`json:\"package_name\"`" + `
}

// SerializedRateLimitConfig is a JSON-serializable version of channel.RateLimitConfig.
type SerializedRateLimitConfig struct {
	RequestsPerMinute int ` + "`json:\"requests_per_minute\"`" + `
	BurstSize         int ` + "`json:\"burst_size\"`" + `
}

// SerializedMessageInfo is a JSON-serializable version of channel.MessageInfo.
type SerializedMessageInfo struct {
	Direction   string                ` + "`json:\"direction\"`" + `
	TypeName    string                ` + "`json:\"type_name\"`" + `
	Fields      []SerializedFieldInfo ` + "`json:\"fields\"`" + `
	IsDispatch  bool                  ` + "`json:\"is_dispatch\"`" + `
	HandlerName string                ` + "`json:\"handler_name\"`" + `
}

// SerializedFieldInfo is a JSON-serializable version of channel.FieldInfo.
type SerializedFieldInfo struct {
	Name         string                ` + "`json:\"name\"`" + `
	Type         string                ` + "`json:\"type\"`" + `
	JSONName     string                ` + "`json:\"json_name\"`" + `
	JSONOmit     bool                  ` + "`json:\"json_omit\"`" + `
	Required     bool                  ` + "`json:\"required\"`" + `
	Tags         map[string]string     ` + "`json:\"tags\"`" + `
	StructFields *SerializedStructInfo ` + "`json:\"struct_fields,omitempty\"`" + `
}

// SerializedStructInfo holds nested struct metadata.
type SerializedStructInfo struct {
	Name    string                ` + "`json:\"name\"`" + `
	Package string                ` + "`json:\"package\"`" + `
	Fields  []SerializedFieldInfo ` + "`json:\"fields\"`" + `
}

func main() {
	app := channel.NewApp()

	// Call all Register functions
`)

	for i := range pkgs {
		fmt.Fprintf(&buf, "\tch%d.Register(app)\n", i)
	}

	buf.WriteString(`
	// Convert to serializable format
	serialized := make([]SerializedChannelInfo, len(app.Channels))
	for i, ch := range app.Channels {
		serialized[i] = SerializedChannelInfo{
			Name:           ch.Name,
			Visibility:     visibilityToString(ch.Visibility),
			IsPublic:       ch.IsPublic,
			RateLimit:      convertRateLimit(ch.RateLimit),
			Messages:       convertMessages(ch.Messages),
			MaxRetries:     ch.MaxRetries,
			BackoffSeconds: ch.BackoffSeconds,
			TimeoutSeconds: ch.TimeoutSeconds,
			RequiredRole:   ch.RequiredRole,
		}
	}

	// Fill in PackagePath and PackageName from the message types.
	// Each channel's messages were registered from a specific package;
	// we use the first message's reflect.Type package path to determine
	// the source package. However, since we already know the import order
	// matches the channel order, we can assign them directly.
	channelImports := []string{
`)
	for _, pkg := range pkgs {
		fmt.Fprintf(&buf, "\t\t%q,\n", pkg)
	}
	buf.WriteString(`	}
	for i := range serialized {
		if i < len(channelImports) {
			serialized[i].PackagePath = channelImports[i]
			// Extract package name from import path (last segment)
			parts := strings.Split(channelImports[i], "/")
			serialized[i].PackageName = parts[len(parts)-1]
		}
	}

	// Output as JSON
	data, err := json.MarshalIndent(serialized, "", "  ")
	if err != nil {
		fmt.Fprintf(os.Stderr, "error serializing channels: %v\n", err)
		os.Exit(1)
	}

	os.Stdout.Write(data)
}

func visibilityToString(v channel.Visibility) string {
	switch v {
	case channel.Frontend:
		return "frontend"
	case channel.Backend:
		return "backend"
	default:
		return "unknown"
	}
}

func convertRateLimit(rl *channel.RateLimitConfig) *SerializedRateLimitConfig {
	if rl == nil {
		return nil
	}
	return &SerializedRateLimitConfig{
		RequestsPerMinute: rl.RequestsPerMinute,
		BurstSize:         rl.BurstSize,
	}
}

func convertMessages(msgs []channel.MessageInfo) []SerializedMessageInfo {
	if msgs == nil {
		return []SerializedMessageInfo{}
	}
	result := make([]SerializedMessageInfo, len(msgs))
	for i, m := range msgs {
		dir := "server_to_client"
		if m.Direction == channel.ClientToServer {
			dir = "client_to_server"
		}
		result[i] = SerializedMessageInfo{
			Direction:   dir,
			TypeName:    m.TypeName,
			Fields:      convertChannelFields(m.Fields),
			IsDispatch:  m.IsDispatch,
			HandlerName: m.HandlerName,
		}
	}
	return result
}

func convertChannelFields(fields []channel.FieldInfo) []SerializedFieldInfo {
	if fields == nil {
		return []SerializedFieldInfo{}
	}
	result := make([]SerializedFieldInfo, len(fields))
	for i, f := range fields {
		result[i] = SerializedFieldInfo{
			Name:         f.Name,
			Type:         f.Type,
			JSONName:     f.JSONName,
			JSONOmit:     f.JSONOmit,
			Required:     f.Required,
			Tags:         f.Tags,
			StructFields: convertChannelStructInfo(f.StructFields),
		}
	}
	return result
}

func convertChannelStructInfo(info *channel.MessageStructInfo) *SerializedStructInfo {
	if info == nil {
		return nil
	}
	return &SerializedStructInfo{
		Name:    info.Name,
		Package: info.Package,
		Fields:  convertChannelFields(info.Fields),
	}
}


`)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return unformatted source for debugging
		return buf.Bytes(), fmt.Errorf("failed to format channel compile program: %w", err)
	}

	return formatted, nil
}
