func TestWriteHooks_ReactContentHasCorrectImportsAndHooks(t *testing.T) {
	tmpDir := t.TempDir()
	channels := makeFrontendChannels()
	tsDir := "ts-output"

	if err := WriteReactChannelHooks(channels, tmpDir, tsDir, nil); err != nil {
		t.Fatalf("WriteReactChannelHooks failed: %v", err)
	}

	content := assertFileExists(t, filepath.Join(tmpDir, tsDir, "react", "shipq-channels.ts"))

	// React-specific markers
	mustContain := []string{
		"from \"react\"",
		"useEffect,",
		"useRef,",
		"useCallback,",
		"useState",
		"from \"../shipq-channels\"",
		"useEmailNotification",
		"useChatbot",
		"type EmailNotificationChannel,",
		"type ChatbotChannel,",
		"dispatchEmailNotification,",
		"dispatchChatbot,",
	}
	for _, s := range mustContain {
		if !strings.Contains(content, s) {
			t.Errorf("react channel hooks should contain %q", s)
		}
	}

	// Should NOT contain Svelte-specific patterns
	if strings.Contains(content, "writable") {
		t.Error("react hooks should not contain Svelte's writable")
	}
	if strings.Contains(content, "onDestroy") {
		t.Error("react hooks should not contain Svelte's onDestroy")
	}
}

