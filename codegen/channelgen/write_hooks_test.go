func TestWriteHooks_MixedChannels_BackendSkipped(t *testing.T) {
	tmpDir := t.TempDir()
	channels := []codegen.SerializedChannelInfo{
		makeUnidirectionalEmailChannel(),
		makeBackendBillingChannel(),
	}
	tsDir := "ts-output"

	if err := WriteReactChannelHooks(channels, tmpDir, tsDir, nil); err != nil {
		t.Fatalf("WriteReactChannelHooks failed: %v", err)
	}
	if err := WriteSvelteChannelHooks(channels, tmpDir, tsDir, nil); err != nil {
		t.Fatalf("WriteSvelteChannelHooks failed: %v", err)
	}

	reactContent := assertFileExists(t, filepath.Join(tmpDir, tsDir, "react", "shipq-channels.ts"))
	svelteContent := assertFileExists(t, filepath.Join(tmpDir, tsDir, "svelte", "shipq-channels.ts"))

	// Frontend channel should be present
	if !strings.Contains(reactContent, "useEmailNotification") {
		t.Error("react hooks should contain useEmailNotification for frontend channel")
	}
	if !strings.Contains(svelteContent, "createEmailNotificationChannel") {
		t.Error("svelte hooks should contain createEmailNotificationChannel for frontend channel")
	}

	// Backend channel should NOT be present
	if strings.Contains(reactContent, "BillingSync") || strings.Contains(reactContent, "billing_sync") {
		t.Error("react hooks should not contain backend channel code")
	}
	if strings.Contains(svelteContent, "BillingSync") || strings.Contains(svelteContent, "billing_sync") {
		t.Error("svelte hooks should not contain backend channel code")
	}
}
