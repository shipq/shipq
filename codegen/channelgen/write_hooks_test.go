func TestWriteReactChannelHooks_WritesReactSubdirectory(t *testing.T) {
	tmpDir := t.TempDir()
	channels := makeFrontendChannels()

	err := WriteReactChannelHooks(channels, tmpDir, "ts-output", nil)
	if err != nil {
		t.Fatalf("WriteReactChannelHooks failed: %v", err)
	}

	content := assertFileExists(t, filepath.Join(tmpDir, "ts-output", "react", "shipq-channels.ts"))

	if !strings.Contains(content, "Code generated by shipq") {
		t.Error("react channel hooks should contain generated header")
	}
	if !strings.Contains(content, "useEmailNotification") {
		t.Error("react channel hooks should contain useEmailNotification hook")
	}
	if !strings.Contains(content, "useChatbot") {
		t.Error("react channel hooks should contain useChatbot hook")
	}
}

