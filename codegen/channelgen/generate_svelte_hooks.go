package channelgen

import (
	"bytes"
	"fmt"

	"github.com/shipq/shipq/codegen"
	"github.com/shipq/shipq/codegen/tsutil"
)

// GenerateSvelteChannelHooks generates the Svelte integration layer for channels
// (svelte/shipq-channels.ts). It produces a Svelte store factory per frontend channel
// that wraps the base dispatch function with writable stores and onDestroy cleanup.
func GenerateSvelteChannelHooks(channels []codegen.SerializedChannelInfo) ([]byte, error) {
	// Filter to frontend-only channels
	var frontendChannels []codegen.SerializedChannelInfo
	for _, ch := range channels {
		if ch.Visibility == "frontend" {
			frontendChannels = append(frontendChannels, ch)
		}
	}

	if len(frontendChannels) == 0 {
		return []byte("// No frontend channels defined.\n"), nil
	}

	var buf bytes.Buffer

	// Header
	buf.WriteString("// Code generated by shipq. DO NOT EDIT.\n\n")

	// Svelte imports
	buf.WriteString("import { writable, type Readable } from \"svelte/store\";\n")
	buf.WriteString("import { onDestroy } from \"svelte\";\n")

	// Collect all type/function imports from the base channel client
	buf.WriteString("import {\n")
	for _, ch := range frontendChannels {
		writeSvelteChannelImports(&buf, ch)
	}
	buf.WriteString("} from \"../shipq-channels\";\n")

	// Generate per-channel store factories
	for _, ch := range frontendChannels {
		if err := generateSvelteChannelHook(&buf, ch); err != nil {
			return nil, fmt.Errorf("generate svelte hook for channel %q: %w", ch.Name, err)
		}
	}

	return buf.Bytes(), nil
}

// writeSvelteChannelImports writes the import lines for a single channel's types
// and dispatch function from the base channel client.
func writeSvelteChannelImports(buf *bytes.Buffer, ch codegen.SerializedChannelInfo) {
	pascalName := tsutil.ToPascalCase(ch.Name)

	// Import channel interface type
	fmt.Fprintf(buf, "  type %sChannel,\n", pascalName)

	// Import all message types
	for _, msg := range ch.Messages {
		fmt.Fprintf(buf, "  type %s,\n", msg.TypeName)
	}

	// Import dispatch function
	fmt.Fprintf(buf, "  dispatch%s,\n", pascalName)
}

// generateSvelteChannelHook generates the Svelte store factory for a single channel.
func generateSvelteChannelHook(buf *bytes.Buffer, ch codegen.SerializedChannelInfo) error {
	pascalName := tsutil.ToPascalCase(ch.Name)
	factoryName := "create" + pascalName + "Channel"

	// Classify messages
	var fromServerMsgs []codegen.SerializedMessageInfo
	var fromClientDispatch *codegen.SerializedMessageInfo
	var fromClientMidStream []codegen.SerializedMessageInfo

	for i := range ch.Messages {
		msg := &ch.Messages[i]
		switch msg.Direction {
		case "server_to_client":
			fromServerMsgs = append(fromServerMsgs, *msg)
		case "client_to_server":
			if msg.IsDispatch {
				fromClientDispatch = msg
			} else {
				fromClientMidStream = append(fromClientMidStream, *msg)
			}
		}
	}

	if fromClientDispatch == nil {
		return fmt.Errorf("channel %q has no dispatch message", ch.Name)
	}

	buf.WriteString(fmt.Sprintf("\n// ─── %s ───\n", factoryName))

	// Generate options interface
	fmt.Fprintf(buf, "\nexport interface %sChannelOptions {\n", pascalName)
	for _, msg := range fromServerMsgs {
		fmt.Fprintf(buf, "  on%s?: (msg: %s) => void;\n", msg.TypeName, msg.TypeName)
	}
	fmt.Fprintf(buf, "  onError?: (error: Error) => void;\n")
	buf.WriteString("}\n")

	// Generate store interface
	fmt.Fprintf(buf, "\nexport interface %sChannelStore {\n", pascalName)
	fmt.Fprintf(buf, "  channel: Readable<%sChannel | null>;\n", pascalName)
	fmt.Fprintf(buf, "  isConnecting: Readable<boolean>;\n")
	fmt.Fprintf(buf, "  error: Readable<Error | null>;\n")
	fmt.Fprintf(buf, "  dispatch: (req: %s) => Promise<void>;\n", fromClientDispatch.TypeName)
	for _, msg := range fromClientMidStream {
		fmt.Fprintf(buf, "  send%s: (msg: %s) => void;\n", msg.TypeName, msg.TypeName)
	}
	fmt.Fprintf(buf, "  disconnect: () => void;\n")
	buf.WriteString("}\n")

	// Generate the factory function
	fmt.Fprintf(buf, "\nexport function %s(\n", factoryName)
	fmt.Fprintf(buf, "  options?: %sChannelOptions,\n", pascalName)
	fmt.Fprintf(buf, "): %sChannelStore {\n", pascalName)

	// Writable stores
	fmt.Fprintf(buf, "  const channel = writable<%sChannel | null>(null);\n", pascalName)
	buf.WriteString("  const isConnecting = writable(false);\n")
	buf.WriteString("  const error = writable<Error | null>(null);\n")
	fmt.Fprintf(buf, "  let currentChannel: %sChannel | null = null;\n", pascalName)
	buf.WriteString("  let currentOptions = options;\n")
	buf.WriteString("\n")

	// Auto-cleanup on component destroy
	buf.WriteString("  // Auto-cleanup on component destroy\n")
	buf.WriteString("  onDestroy(() => {\n")
	buf.WriteString("    currentChannel?.unsubscribe();\n")
	buf.WriteString("  });\n")
	buf.WriteString("\n")

	// dispatch function
	fmt.Fprintf(buf, "  async function dispatch(req: %s): Promise<void> {\n", fromClientDispatch.TypeName)
	buf.WriteString("    currentChannel?.unsubscribe();\n")
	buf.WriteString("    isConnecting.set(true);\n")
	buf.WriteString("    error.set(null);\n")
	buf.WriteString("\n")
	buf.WriteString("    try {\n")
	fmt.Fprintf(buf, "      const ch = await dispatch%s(req);\n", pascalName)
	buf.WriteString("      currentChannel = ch;\n")
	buf.WriteString("      channel.set(ch);\n")
	buf.WriteString("      isConnecting.set(false);\n")
	buf.WriteString("\n")

	// Wire up FromServer handlers
	for _, msg := range fromServerMsgs {
		fmt.Fprintf(buf, "      ch.on%s((msg) => currentOptions?.on%s?.(msg));\n", msg.TypeName, msg.TypeName)
	}

	buf.WriteString("    } catch (err) {\n")
	buf.WriteString("      const e = err instanceof Error ? err : new Error(String(err));\n")
	buf.WriteString("      error.set(e);\n")
	buf.WriteString("      isConnecting.set(false);\n")
	buf.WriteString("      currentOptions?.onError?.(e);\n")
	buf.WriteString("    }\n")
	buf.WriteString("  }\n")
	buf.WriteString("\n")

	// send* functions for mid-stream messages
	for _, msg := range fromClientMidStream {
		fmt.Fprintf(buf, "  function send%s(msg: %s): void {\n", msg.TypeName, msg.TypeName)
		fmt.Fprintf(buf, "    currentChannel?.send%s(msg);\n", msg.TypeName)
		buf.WriteString("  }\n")
		buf.WriteString("\n")
	}

	// disconnect function
	buf.WriteString("  function disconnect(): void {\n")
	buf.WriteString("    currentChannel?.unsubscribe();\n")
	buf.WriteString("    currentChannel = null;\n")
	buf.WriteString("    channel.set(null);\n")
	buf.WriteString("  }\n")
	buf.WriteString("\n")

	// Return object with read-only store interfaces
	buf.WriteString("  return {\n")
	buf.WriteString("    channel: { subscribe: channel.subscribe },\n")
	buf.WriteString("    isConnecting: { subscribe: isConnecting.subscribe },\n")
	buf.WriteString("    error: { subscribe: error.subscribe },\n")
	buf.WriteString("    dispatch,\n")
	for _, msg := range fromClientMidStream {
		fmt.Fprintf(buf, "    send%s,\n", msg.TypeName)
	}
	buf.WriteString("    disconnect,\n")
	buf.WriteString("  };\n")
	buf.WriteString("}\n")

	return nil
}
