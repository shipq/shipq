// GenerateTypeScriptChannelClient generates a TypeScript client file (shipq-channels.ts)
// for all frontend channels. Backend channels (Visibility != "frontend") are skipped.
//
// The generated code imports centrifuge-js and provides:
//   - Typed interfaces for all message types
//   - Per-channel interfaces with on<Type> handlers and send<Type> methods
//   - Per-channel dispatch functions that handle HTTP dispatch, token fetching,
//     Centrifugo connection, and publication echo filtering [L1]
//   - Per-channel Centrifugo clients to isolate JWT scoping failures [L3]
func GenerateTypeScriptChannelClient(channels []codegen.SerializedChannelInfo) ([]byte, error) {
	// Filter to frontend-only channels
	var frontendChannels []codegen.SerializedChannelInfo
	for _, ch := range channels {
		if ch.Visibility == "frontend" {
			frontendChannels = append(frontendChannels, ch)
		}
	}

	if len(frontendChannels) == 0 {
		return []byte("// No frontend channels defined.\n"), nil
	}

	var buf bytes.Buffer

	// Header
	buf.WriteString("// Code generated by shipq. DO NOT EDIT.\n\n")

	// Imports
	buf.WriteString("import { Centrifuge } from \"centrifuge\";\n\n")

	// Config interface and configure function
	buf.WriteString("export interface ChannelConfig {\n")
	buf.WriteString("  baseURL: string;\n")
	buf.WriteString("  centrifugoURL: string;\n")
	buf.WriteString("}\n\n")

	buf.WriteString("let config: ChannelConfig | null = null;\n\n")

	buf.WriteString("export function configure(cfg: ChannelConfig): void {\n")
	buf.WriteString("  config = cfg;\n")
	buf.WriteString("}\n\n")

	buf.WriteString("function getConfig(): ChannelConfig {\n")
	buf.WriteString("  if (!config) {\n")
	buf.WriteString("    throw new Error(\"shipq channels not configured — call configure() first\");\n")
	buf.WriteString("  }\n")
	buf.WriteString("  return config;\n")
	buf.WriteString("}\n")

	// Generate per-channel code
	for _, ch := range frontendChannels {
		if err := generateChannelTypeScript(&buf, ch); err != nil {
			return nil, fmt.Errorf("generate typescript for channel %q: %w", ch.Name, err)
		}
	}

	return buf.Bytes(), nil
}

// generateChannelTypeScript generates the TypeScript code for a single channel.
func generateChannelTypeScript(buf *bytes.Buffer, ch codegen.SerializedChannelInfo) error {
	pascalName := toPascalCase(ch.Name)

	// Classify messages
	var fromServerMsgs []codegen.SerializedMessageInfo
	var fromClientDispatch *codegen.SerializedMessageInfo
	var fromClientMidStream []codegen.SerializedMessageInfo

	for i := range ch.Messages {
		msg := &ch.Messages[i]
		switch msg.Direction {
		case "server_to_client":
			fromServerMsgs = append(fromServerMsgs, *msg)
		case "client_to_server":
			if msg.IsDispatch {
				fromClientDispatch = msg
			} else {
				fromClientMidStream = append(fromClientMidStream, *msg)
			}
		}
	}

	buf.WriteString("\n// ─── Channel: " + ch.Name + " ───\n")

	// Generate interfaces for all message types
	// FromClient dispatch type
	if fromClientDispatch != nil {
		generateTSInterface(buf, *fromClientDispatch)
	}

	// FromClient mid-stream types
	for _, msg := range fromClientMidStream {
		generateTSInterface(buf, msg)
	}

	// FromServer types
	for _, msg := range fromServerMsgs {
		generateTSInterface(buf, msg)
	}

	// Generate channel interface
	fmt.Fprintf(buf, "\nexport interface %sChannel {\n", pascalName)
	buf.WriteString("  jobId: string;\n")

	// on<Type> handlers for FromServer types only [L1]
	for _, msg := range fromServerMsgs {
		fmt.Fprintf(buf, "  on%s(handler: (msg: %s) => void): void;\n", msg.TypeName, msg.TypeName)
	}

	// send<Type> methods for mid-stream FromClient types
	for _, msg := range fromClientMidStream {
		fmt.Fprintf(buf, "  send%s(msg: %s): void;\n", msg.TypeName, msg.TypeName)
	}

	buf.WriteString("  unsubscribe(): void;\n")
	buf.WriteString("}\n")

	// Generate dispatch function
	if fromClientDispatch == nil {
		return fmt.Errorf("channel %q has no dispatch message", ch.Name)
	}

	generateDispatchFunction(buf, ch, pascalName, *fromClientDispatch, fromServerMsgs, fromClientMidStream)

	return nil
}

// generateTSInterface generates a TypeScript export interface for a message type.
func generateTSInterface(buf *bytes.Buffer, msg codegen.SerializedMessageInfo) {
	tsutil.GenerateTSInterface(buf, msg.TypeName, msg.Fields)
}

// generateDispatchFunction generates the async dispatch function for a channel.
func generateDispatchFunction(
	buf *bytes.Buffer,
	ch codegen.SerializedChannelInfo,
	pascalName string,
	dispatchMsg codegen.SerializedMessageInfo,
	fromServerMsgs []codegen.SerializedMessageInfo,
	fromClientMidStream []codegen.SerializedMessageInfo,
) {
	funcName := "dispatch" + pascalName

	// Build the set of FromClient type names for echo filtering [L1]
	var fromClientTypeNames []string
	for _, msg := range fromClientMidStream {
		fromClientTypeNames = append(fromClientTypeNames, msg.TypeName)
	}

	fmt.Fprintf(buf, "\nexport async function %s(request: %s): Promise<%sChannel> {\n", funcName, dispatchMsg.TypeName, pascalName)
	buf.WriteString("  const cfg = getConfig();\n\n")

	// POST to dispatch endpoint
	buf.WriteString("  // Step 1: Dispatch the job\n")
	fmt.Fprintf(buf, "  const dispatchRes = await fetch(`${cfg.baseURL}/channels/%s/dispatch`, {\n", ch.Name)
	buf.WriteString("    method: \"POST\",\n")
	buf.WriteString("    headers: { \"Content-Type\": \"application/json\" },\n")
	buf.WriteString("    body: JSON.stringify(request),\n")
	if !ch.IsPublic {
		buf.WriteString("    credentials: \"include\",\n")
	}
	buf.WriteString("  });\n")
	buf.WriteString("  if (!dispatchRes.ok) {\n")
	buf.WriteString("    throw new Error(`dispatch failed: ${dispatchRes.status} ${dispatchRes.statusText}`);\n")
	buf.WriteString("  }\n")
	buf.WriteString("  const dispatchData = await dispatchRes.json();\n")
	buf.WriteString("  const jobId: string = dispatchData.job_id;\n\n")

	// GET token endpoint
	buf.WriteString("  // Step 2: Get connection and subscription tokens\n")
	fmt.Fprintf(buf, "  const tokenRes = await fetch(`${cfg.baseURL}/channels/%s/token?job_id=${jobId}`, {\n", ch.Name)
	buf.WriteString("    method: \"GET\",\n")
	if !ch.IsPublic {
		buf.WriteString("    credentials: \"include\",\n")
	}
	buf.WriteString("  });\n")
	buf.WriteString("  if (!tokenRes.ok) {\n")
	buf.WriteString("    throw new Error(`token fetch failed: ${tokenRes.status} ${tokenRes.statusText}`);\n")
	buf.WriteString("  }\n")
	buf.WriteString("  const tokenData = await tokenRes.json();\n")
	buf.WriteString("  const { connection_token, subscription_token, channel, ws_url } = tokenData;\n\n")

	// Helper: re-fetch tokens from the token endpoint (used by getToken callbacks)
	buf.WriteString("  // Token refresh helper — called by centrifuge-js when a token is about to expire\n")
	fmt.Fprintf(buf, "  const refreshTokens = async (): Promise<{ connection_token: string; subscription_token: string }> => {\n")
	fmt.Fprintf(buf, "    const res = await fetch(`${cfg.baseURL}/channels/%s/token?job_id=${jobId}`, {\n", ch.Name)
	buf.WriteString("      method: \"GET\",\n")
	if !ch.IsPublic {
		buf.WriteString("      credentials: \"include\",\n")
	}
	buf.WriteString("    });\n")
	buf.WriteString("    if (!res.ok) throw new Error(`token refresh failed: ${res.status}`);\n")
	buf.WriteString("    return res.json();\n")
	buf.WriteString("  };\n\n")

	// [L3] Create per-channel Centrifuge client with getToken for refresh
	buf.WriteString("  // Step 3: Create Centrifuge client (per-channel to isolate JWT failures [L3])\n")
	buf.WriteString("  const client = new Centrifuge(ws_url, {\n")
	buf.WriteString("    token: connection_token,\n")
	buf.WriteString("    getToken: async () => {\n")
	buf.WriteString("      const data = await refreshTokens();\n")
	buf.WriteString("      return data.connection_token;\n")
	buf.WriteString("    },\n")
	buf.WriteString("  });\n")
	buf.WriteString("  client.connect();\n\n")

	// Create subscription with initial token + getToken for refresh
	buf.WriteString("  // Step 4: Create subscription with token and refresh callback\n")
	buf.WriteString("  const sub = client.newSubscription(channel, {\n")
	buf.WriteString("    token: subscription_token,\n")
	buf.WriteString("    getToken: async () => {\n")
	buf.WriteString("      const data = await refreshTokens();\n")
	buf.WriteString("      return data.subscription_token;\n")
	buf.WriteString("    },\n")
	buf.WriteString("  });\n\n")

	// Set up handler registry
	buf.WriteString("  // Handler registry for FromServer message types\n")

	// Declare handler maps for each FromServer type
	for _, msg := range fromServerMsgs {
		handlerVar := "on" + msg.TypeName + "Handlers"
		fmt.Fprintf(buf, "  const %s: Array<(msg: %s) => void> = [];\n", handlerVar, msg.TypeName)
	}
	buf.WriteString("\n")

	// [L1] Build the set of FromClient type names for echo filtering
	if len(fromClientTypeNames) > 0 {
		buf.WriteString("  // [L1] FromClient type names — publications with these types are echoes and must be ignored\n")
		buf.WriteString("  const fromClientTypes = new Set<string>([\n")
		for _, typeName := range fromClientTypeNames {
			fmt.Fprintf(buf, "    %q,\n", typeName)
		}
		buf.WriteString("  ]);\n\n")
	}

	// Set up publication handler with demultiplexer
	buf.WriteString("  // Step 5: Demultiplex publications by envelope type\n")
	buf.WriteString("  sub.on(\"publication\", (ctx) => {\n")
	buf.WriteString("    const envelope = ctx.data as { type: string; data: unknown };\n")
	buf.WriteString("    const msgType: string = envelope.type;\n")
	buf.WriteString("    const msgData = envelope.data;\n\n")

	// [L1] Filter echoed FromClient messages
	if len(fromClientTypeNames) > 0 {
		buf.WriteString("    // [L1] Skip echoed FromClient messages\n")
		buf.WriteString("    if (fromClientTypes.has(msgType)) {\n")
		buf.WriteString("      return;\n")
		buf.WriteString("    }\n\n")
	}

	// Dispatch to the correct handler
	buf.WriteString("    switch (msgType) {\n")
	for _, msg := range fromServerMsgs {
		handlerVar := "on" + msg.TypeName + "Handlers"
		fmt.Fprintf(buf, "      case %q:\n", msg.TypeName)
		fmt.Fprintf(buf, "        for (const h of %s) { h(msgData as %s); }\n", handlerVar, msg.TypeName)
		buf.WriteString("        break;\n")
	}
	buf.WriteString("    }\n")
	buf.WriteString("  });\n\n")

	// Subscribe
	buf.WriteString("  sub.subscribe();\n\n")

	// Return the channel interface
	buf.WriteString("  // Step 6: Return the channel interface\n")
	fmt.Fprintf(buf, "  return {\n")
	buf.WriteString("    jobId,\n")

	// on<Type> handlers for FromServer [L1]
	for _, msg := range fromServerMsgs {
		handlerVar := "on" + msg.TypeName + "Handlers"
		fmt.Fprintf(buf, "    on%s(handler: (msg: %s) => void) {\n", msg.TypeName, msg.TypeName)
		fmt.Fprintf(buf, "      %s.push(handler);\n", handlerVar)
		buf.WriteString("    },\n")
	}

	// send<Type> methods for mid-stream FromClient types [L1]
	for _, msg := range fromClientMidStream {
		fmt.Fprintf(buf, "    send%s(msg: %s) {\n", msg.TypeName, msg.TypeName)
		fmt.Fprintf(buf, "      sub.publish({ type: %q, data: msg });\n", msg.TypeName)
		buf.WriteString("    },\n")
	}

	// unsubscribe
	buf.WriteString("    unsubscribe() {\n")
	buf.WriteString("      sub.unsubscribe();\n")
	buf.WriteString("      client.disconnect();\n")
	buf.WriteString("    },\n")

	buf.WriteString("  };\n")
	buf.WriteString("}\n")
}

// goTypeToTS converts a Go type string to its TypeScript equivalent.
// Handles common Go types including slices and maps.
// This is a thin wrapper around tsutil.GoTypeToTS for backward compatibility.
func goTypeToTS(field codegen.SerializedFieldInfo) string {
	return tsutil.GoTypeToTS(field)
}

// goTypeStringToTS converts a Go type string to TypeScript.
// This is a thin wrapper around tsutil.GoTypeStringToTS for backward compatibility.
func goTypeStringToTS(goType string) string {
	return tsutil.GoTypeStringToTS(goType)
}

// goStructToInlineTS generates an inline TypeScript interface for a nested struct.
// This is a thin wrapper around tsutil.GoStructToInlineTS for backward compatibility.
func goStructToInlineTS(structInfo *codegen.SerializedStructInfo) string {
	return tsutil.GoStructToInlineTS(structInfo)
}

// toPascalCase converts a snake_case or hyphenated name to PascalCase.
// This is a thin wrapper around tsutil.ToPascalCase for backward compatibility.
func toPascalCase(name string) string {
	return tsutil.ToPascalCase(name)
}
