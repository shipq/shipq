func TestGenerateReactChannelHooks_UseCallbackEmptyDeps(t *testing.T) {
	channels := []codegen.SerializedChannelInfo{
		makeUnidirectionalEmailChannel(),
	}
	result, err := GenerateReactChannelHooks(channels, nil)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	output := string(result)

	// All useCallback calls should have empty dependency arrays for stable references.
	// Count useCallback occurrences and verify each is closed with ", []);"
	callbackCount := strings.Count(output, "useCallback(")
	// For unidirectional: dispatch + disconnect = 2
	if callbackCount < 2 {
		t.Errorf("expected at least 2 useCallback calls, got %d", callbackCount)
	}
	// Verify no useCallback is missing its empty deps by checking that
	// the number of "}, []);" occurrences (which close useCallback bodies)
	// equals the number of useCallback calls. Note: useEffect also uses
	// "}, []);" so we subtract useEffect count.
	effectCount := strings.Count(output, "useEffect(")
	closedWithEmptyDeps := strings.Count(output, "}, []);")
	closedCallbacks := closedWithEmptyDeps - effectCount
	if callbackCount != closedCallbacks {
		t.Errorf("all useCallback calls should have empty deps []; got %d callbacks but %d closed with empty deps (after subtracting %d useEffect)", callbackCount, closedCallbacks, effectCount)
	}
}

