// ChannelHTTPGenConfig holds configuration for generating channel HTTP route files.
type ChannelHTTPGenConfig struct {
	Channels    []codegen.SerializedChannelInfo
	ModulePath  string
	OutputPkg   string // package name for the generated code (e.g., "api")
	ScopeColumn string // from [db] scope in shipq.ini; controls org-scoped queries
	HasAuth     bool   // true when [auth] section exists; controls author_account_id in INSERT
}

// GeneratedChannelHTTPFile represents a single generated file for channel HTTP routes.
type GeneratedChannelHTTPFile struct {
	RelPath string // relative path from shipq root
	Content []byte
}

// GenerateChannelHTTPRoutes generates the HTTP route registration code for all
// channels. It produces a single file that registers dispatch, token, and job
// status endpoints for each channel.
//
// The generated code depends only on the TaskQueue and RealtimeTransport
// interfaces from the channel runtime library — no direct Machinery/Centrifugo
// imports appear in the generated HTTP code.
//
// Endpoints generated per channel:
//   - POST /channels/<name>/dispatch — enqueue a job
//   - GET  /channels/<name>/token?job_id=<id> — get realtime connection/subscription tokens
//
// Plus one shared endpoint:
//   - GET  /channels/jobs/{id} — get job status by public_id
func GenerateChannelHTTPRoutes(cfg ChannelHTTPGenConfig) ([]GeneratedChannelHTTPFile, error) {
	content, err := generateChannelRoutesFile(cfg)
	if err != nil {
		return nil, err
	}

	return []GeneratedChannelHTTPFile{
		{
			RelPath: cfg.OutputPkg + "/zz_generated_channels.go",
			Content: content,
		},
	}, nil
}

// generateChannelRoutesFile generates the full Go source for channel HTTP routes.
func generateChannelRoutesFile(cfg ChannelHTTPGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by shipq. DO NOT EDIT.\n")
	fmt.Fprintf(&buf, "package %s\n\n", cfg.OutputPkg)

	generateChannelRouteImports(&buf, cfg)
	generateRegisterChannelRoutes(&buf, cfg)
	generateDispatchHandlers(&buf, cfg)
	generateTokenHandlers(&buf, cfg)
	generateJobStatusHandler(&buf, cfg)
	generateChannelHelpers(&buf, cfg)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format channel HTTP routes: %w\nunformatted:\n%s", err, buf.String())
	}
	return formatted, nil
}

// generateChannelRouteImports writes the import block.
func generateChannelRouteImports(buf *bytes.Buffer, cfg ChannelHTTPGenConfig) {
	hasAuth := false
	hasPublic := false
	for _, ch := range cfg.Channels {
		if ch.IsPublic {
			hasPublic = true
		} else {
			hasAuth = true
		}
	}

	buf.WriteString("import (\n")
	buf.WriteString("\t\"database/sql\"\n")
	buf.WriteString("\t\"encoding/json\"\n")
	buf.WriteString("\t\"net/http\"\n")
	if hasPublic {
		buf.WriteString("\t\"sync\"\n")
	}
	buf.WriteString("\t\"time\"\n")
	buf.WriteString("\n")

	fmt.Fprintf(buf, "\t%q\n", cfg.ModulePath+"/config")
	fmt.Fprintf(buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/channel")
	fmt.Fprintf(buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/httperror")
	fmt.Fprintf(buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/httputil")
	fmt.Fprintf(buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/logging")
	fmt.Fprintf(buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/nanoid")
	fmt.Fprintf(buf, "\t%q\n", cfg.ModulePath+"/shipq/queries")

	// Import channel packages for dispatch request types
	seen := make(map[string]bool)
	for _, ch := range cfg.Channels {
		if ch.PackagePath != "" && !seen[ch.PackagePath] {
			seen[ch.PackagePath] = true
			fmt.Fprintf(buf, "\t%s %q\n", ch.PackageName, ch.PackagePath)
		}
	}

	buf.WriteString(")\n\n")

	// Ensure imports are used regardless of configuration.
	buf.WriteString("// Ensure imports are used.\n")
	buf.WriteString("var (\n")
	buf.WriteString("\t_ = json.Marshal\n")
	buf.WriteString("\t_ = time.Now\n")
	buf.WriteString("\t_ = httperror.New\n")
	buf.WriteString("\t_ = httputil.WriteJSON\n")
	buf.WriteString("\t_ = logging.RequestIDFromContext\n")
	buf.WriteString("\t_ = nanoid.New\n")
	buf.WriteString("\t_ = channel.ComputeChannelID\n")
	buf.WriteString("\t_ = config.Settings\n")
	buf.WriteString("\t_ *sql.DB\n")
	buf.WriteString("\t_ queries.Runner\n")
	if hasPublic {
		buf.WriteString("\t_ sync.Mutex\n")
	}
	if hasAuth {
		_ = hasAuth // already used above
	}
	buf.WriteString(")\n\n")
}

// generateRegisterChannelRoutes writes the RegisterChannelRoutes function.
func generateRegisterChannelRoutes(buf *bytes.Buffer, cfg ChannelHTTPGenConfig) {
	hasAuth := false
	for _, ch := range cfg.Channels {
		if !ch.IsPublic {
			hasAuth = true
			break
		}
	}

	buf.WriteString(`// RegisterChannelRoutes registers all channel HTTP routes (dispatch, token, job status).
// Parameters:
//   - mux: the HTTP serve mux to register routes on
//   - queue: the TaskQueue interface for dispatching jobs
//   - transport: the RealtimeTransport interface for generating tokens
//   - db: database connection for job_results persistence
//   - checkAuth: authentication function (nil if no authenticated channels)
//   - checkRBAC: RBAC function (nil if no RBAC channels)
func RegisterChannelRoutes(
	mux *http.ServeMux,
	queue channel.TaskQueue,
	transport channel.RealtimeTransport,
	db *sql.DB,
	runner queries.Runner,
`)
	if hasAuth {
		buf.WriteString("\tcheckAuth func(r *http.Request) (accountID int64, orgID int64, accountPublicID string, err error),\n")
		buf.WriteString("\tcheckRBAC func(r *http.Request, accountID int64, orgID int64, routePath, method string) error,\n")
	}
	buf.WriteString(") {\n")

	// Register per-channel dispatch and token routes
	for _, ch := range cfg.Channels {
		dispatchFn := "handleDispatch_" + sanitizeName(ch.Name)
		tokenFn := "handleToken_" + sanitizeName(ch.Name)

		// Dispatch route
		fmt.Fprintf(buf, "\tmux.HandleFunc(\"POST /channels/%s/dispatch\", func(w http.ResponseWriter, r *http.Request) {\n", ch.Name)
		fmt.Fprintf(buf, "\t\t%s(w, r, queue, transport, db, runner", dispatchFn)
		if !ch.IsPublic && hasAuth {
			buf.WriteString(", checkAuth, checkRBAC")
		}
		buf.WriteString(")\n")
		buf.WriteString("\t})\n")

		// Token route
		fmt.Fprintf(buf, "\tmux.HandleFunc(\"GET /channels/%s/token\", func(w http.ResponseWriter, r *http.Request) {\n", ch.Name)
		fmt.Fprintf(buf, "\t\t%s(w, r, transport, runner", tokenFn)
		if !ch.IsPublic && hasAuth {
			buf.WriteString(", checkAuth, checkRBAC")
		}
		buf.WriteString(")\n")
		buf.WriteString("\t})\n")
	}

	// Job status route (shared across all channels)
	buf.WriteString("\n\t// Job status endpoint (shared across all channels)\n")
	buf.WriteString("\tmux.HandleFunc(\"GET /channels/jobs/{id}\", func(w http.ResponseWriter, r *http.Request) {\n")
	buf.WriteString("\t\thandleJobStatus(w, r, runner")
	if hasAuth {
		buf.WriteString(", checkAuth")
	}
	buf.WriteString(")\n")
	buf.WriteString("\t})\n")

	buf.WriteString("}\n\n")
}

// generateDispatchHandlers writes a dispatch handler for each channel.
func generateDispatchHandlers(buf *bytes.Buffer, cfg ChannelHTTPGenConfig) {
	hasAuth := false
	for _, ch := range cfg.Channels {
		if !ch.IsPublic {
			hasAuth = true
			break
		}
	}

	for _, ch := range cfg.Channels {
		fnName := "handleDispatch_" + sanitizeName(ch.Name)

		// Find the dispatch message type
		var dispatchMsg *codegen.SerializedMessageInfo
		for i := range ch.Messages {
			if ch.Messages[i].IsDispatch {
				dispatchMsg = &ch.Messages[i]
				break
			}
		}
		if dispatchMsg == nil {
			continue
		}

		// Function signature
		fmt.Fprintf(buf, "func %s(\n", fnName)
		buf.WriteString("\tw http.ResponseWriter,\n")
		buf.WriteString("\tr *http.Request,\n")
		buf.WriteString("\tqueue channel.TaskQueue,\n")
		buf.WriteString("\ttransport channel.RealtimeTransport,\n")
		buf.WriteString("\tdb *sql.DB,\n")
		buf.WriteString("\trunner queries.Runner,\n")
		if !ch.IsPublic && hasAuth {
			buf.WriteString("\tcheckAuth func(r *http.Request) (accountID int64, orgID int64, accountPublicID string, err error),\n")
			buf.WriteString("\tcheckRBAC func(r *http.Request, accountID int64, orgID int64, routePath, method string) error,\n")
		}
		buf.WriteString(") {\n")

		// Suppress unused variable warning for transport (used in token handler, not dispatch)
		buf.WriteString("\t_ = transport\n\n")

		// Auth + RBAC (for non-public channels)
		if !ch.IsPublic {
			buf.WriteString("\t// Require authentication\n")
			buf.WriteString("\taccountID, orgID, _, err := checkAuth(r)\n")
			buf.WriteString("\tif err != nil {\n")
			buf.WriteString("\t\thttputil.WriteError(w, httperror.Unauthorized(\"authentication required\"))\n")
			buf.WriteString("\t\treturn\n")
			buf.WriteString("\t}\n\n")

			if ch.RequiredRole != "" {
				fmt.Fprintf(buf, "\t// Check RBAC for required role %q\n", ch.RequiredRole)
				fmt.Fprintf(buf, "\tif err := checkRBAC(r, accountID, orgID, \"/channels/%s/dispatch\", \"POST\"); err != nil {\n", ch.Name)
				buf.WriteString("\t\thttputil.WriteError(w, httperror.Forbidden(\"insufficient permissions\"))\n")
				buf.WriteString("\t\treturn\n")
				buf.WriteString("\t}\n\n")
			}
		} else {
			// Public channels: no auth needed, but apply rate limiting
			buf.WriteString("\t// Public channel: no authentication required\n")
			buf.WriteString("\tvar accountID int64 = 0\n")
			buf.WriteString("\tvar orgID int64 = 0\n\n")

			if ch.RateLimit != nil {
				generateRateLimitCheck(buf, ch)
			}
		}

		// Decode request body
		reqType := ch.PackageName + "." + dispatchMsg.TypeName
		fmt.Fprintf(buf, "\t// Decode request body into %s\n", reqType)
		fmt.Fprintf(buf, "\tvar req %s\n", reqType)
		buf.WriteString("\tif err := json.NewDecoder(r.Body).Decode(&req); err != nil {\n")
		buf.WriteString("\t\thttputil.WriteError(w, httperror.BadRequest(\"invalid request body: \"+err.Error()))\n")
		buf.WriteString("\t\treturn\n")
		buf.WriteString("\t}\n\n")

		// Marshal request to JSON for storage and dispatch
		buf.WriteString("\treqJSON, err := json.Marshal(req)\n")
		buf.WriteString("\tif err != nil {\n")
		buf.WriteString("\t\thttputil.WriteError(w, httperror.InternalError(\"failed to marshal request\"))\n")
		buf.WriteString("\t\treturn\n")
		buf.WriteString("\t}\n\n")

		// Generate a job ID
		buf.WriteString("\t// Generate unique job ID\n")
		buf.WriteString("\tjobID := nanoid.New()\n\n")

		// Insert job_results row via the generated query runner
		buf.WriteString("\t// Insert job_results row (status=\"pending\") via query runner\n")
		buf.WriteString("\t_, err = runner.InsertJobResult(r.Context(), queries.InsertJobResultParams{\n")
		buf.WriteString("\t\tPublicId:        jobID,\n")
		fmt.Fprintf(buf, "\t\tChannelName:     %q,\n", ch.Name)
		buf.WriteString("\t\tAccountId:       accountID,\n")
		if cfg.HasAuth {
			buf.WriteString("\t\tAuthorAccountId: accountID,\n")
		}
		buf.WriteString("\t\tStatus:          \"pending\",\n")
		buf.WriteString("\t\tRequestPayload:  string(reqJSON),\n")
		buf.WriteString("\t})\n")
		buf.WriteString("\tif err != nil {\n")
		fmt.Fprintf(buf, "\t\tconfig.Logger.Error(\"failed to insert job_results\", \"channel\", %q, \"error\", err.Error())\n", ch.Name)
		buf.WriteString("\t\thttputil.WriteError(w, httperror.InternalError(\"failed to create job\"))\n")
		buf.WriteString("\t\treturn\n")
		buf.WriteString("\t}\n\n")

		// Build dispatch payload
		buf.WriteString("\t// Build dispatch payload envelope\n")
		buf.WriteString("\tdispatchPayload := channel.DispatchPayload{\n")
		buf.WriteString("\t\tJobID:       jobID,\n")
		fmt.Fprintf(buf, "\t\tChannelName: %q,\n", ch.Name)
		buf.WriteString("\t\tAccountID:   accountID,\n")
		buf.WriteString("\t\tOrgID:       orgID,\n")
		fmt.Fprintf(buf, "\t\tIsPublic:    %v,\n", ch.IsPublic)
		buf.WriteString("\t\tRequest:     reqJSON,\n")
		buf.WriteString("\t}\n")
		buf.WriteString("\tpayloadJSON, err := json.Marshal(dispatchPayload)\n")
		buf.WriteString("\tif err != nil {\n")
		buf.WriteString("\t\thttputil.WriteError(w, httperror.InternalError(\"failed to marshal dispatch payload\"))\n")
		buf.WriteString("\t\treturn\n")
		buf.WriteString("\t}\n\n")

		// Enqueue task via TaskQueue interface
		buf.WriteString("\t// Enqueue task via TaskQueue interface\n")
		fmt.Fprintf(buf, "\terr = queue.SendTask(%q, string(payloadJSON), channel.TaskOptions{\n", ch.Name)
		fmt.Fprintf(buf, "\t\tRetryCount:    %d,\n", ch.MaxRetries)
		fmt.Fprintf(buf, "\t\tRetryTimeoutS: %d,\n", ch.BackoffSeconds)
		buf.WriteString("\t})\n")
		buf.WriteString("\tif err != nil {\n")
		fmt.Fprintf(buf, "\t\tconfig.Logger.Error(\"failed to enqueue task\", \"channel\", %q, \"error\", err.Error())\n", ch.Name)
		buf.WriteString("\t\thttputil.WriteError(w, httperror.InternalError(\"failed to dispatch job\"))\n")
		buf.WriteString("\t\treturn\n")
		buf.WriteString("\t}\n\n")

		// Return job_id
		buf.WriteString("\thttputil.WriteJSON(w, http.StatusAccepted, map[string]string{\"job_id\": jobID})\n")

		buf.WriteString("}\n\n")
	}
}

// generateTokenHandlers writes a token handler for each channel.
func generateTokenHandlers(buf *bytes.Buffer, cfg ChannelHTTPGenConfig) {
	hasAuth := false
	for _, ch := range cfg.Channels {
		if !ch.IsPublic {
			hasAuth = true
			break
		}
	}

	for _, ch := range cfg.Channels {
		fnName := "handleToken_" + sanitizeName(ch.Name)

		fmt.Fprintf(buf, "func %s(\n", fnName)
		buf.WriteString("\tw http.ResponseWriter,\n")
		buf.WriteString("\tr *http.Request,\n")
		buf.WriteString("\ttransport channel.RealtimeTransport,\n")
		buf.WriteString("\trunner queries.Runner,\n")
		if !ch.IsPublic && hasAuth {
			buf.WriteString("\tcheckAuth func(r *http.Request) (accountID int64, orgID int64, accountPublicID string, err error),\n")
			buf.WriteString("\tcheckRBAC func(r *http.Request, accountID int64, orgID int64, routePath, method string) error,\n")
		}
		buf.WriteString(") {\n")

		// Get job_id from query params
		buf.WriteString("\tjobID := r.URL.Query().Get(\"job_id\")\n")
		buf.WriteString("\tif jobID == \"\" {\n")
		buf.WriteString("\t\thttputil.WriteError(w, httperror.BadRequest(\"job_id query parameter is required\"))\n")
		buf.WriteString("\t\treturn\n")
		buf.WriteString("\t}\n\n")

		// Auth + RBAC (for non-public channels)
		if !ch.IsPublic {
			buf.WriteString("\t// Require authentication\n")
			buf.WriteString("\taccountID, orgID, accountPublicID, err := checkAuth(r)\n")
			buf.WriteString("\tif err != nil {\n")
			buf.WriteString("\t\thttputil.WriteError(w, httperror.Unauthorized(\"authentication required\"))\n")
			buf.WriteString("\t\treturn\n")
			buf.WriteString("\t}\n")
			buf.WriteString("\t_ = orgID // available for org-scoped verification\n\n")

			if ch.RequiredRole != "" {
				fmt.Fprintf(buf, "\t// Check RBAC for required role %q\n", ch.RequiredRole)
				fmt.Fprintf(buf, "\tif err := checkRBAC(r, accountID, orgID, \"/channels/%s/token\", \"GET\"); err != nil {\n", ch.Name)
				buf.WriteString("\t\thttputil.WriteError(w, httperror.Forbidden(\"insufficient permissions\"))\n")
				buf.WriteString("\t\treturn\n")
				buf.WriteString("\t}\n\n")
			}

			// Look up job_results row and verify ownership
			buf.WriteString("\t// Look up job and verify ownership\n")
			buf.WriteString("\tjobResult, err := runner.GetJobResult(r.Context(), queries.GetJobResultParams{\n")
			buf.WriteString("\t\tPublicId: jobID,\n")
			buf.WriteString("\t})\n")
			buf.WriteString("\tif err != nil {\n")
			buf.WriteString("\t\thttputil.WriteError(w, httperror.NotFound(\"job not found\"))\n")
			buf.WriteString("\t\treturn\n")
			buf.WriteString("\t}\n")
			buf.WriteString("\tif jobResult.AccountId != accountID {\n")
			buf.WriteString("\t\thttputil.WriteError(w, httperror.Forbidden(\"you do not own this job\"))\n")
			buf.WriteString("\t\treturn\n")
			buf.WriteString("\t}\n\n")

			// Compute scoped channel name — MUST match worker's channelID() exactly ([L3])
			buf.WriteString("\t// [L3] CRITICAL: Channel name must exactly match the worker's channelID() computation.\n")
			buf.WriteString("\t// A mismatch disconnects the ENTIRE client connection in Centrifugo v6.\n")
			fmt.Fprintf(buf, "\tchannelName := channel.ComputeChannelID(%q, accountID, jobID, false)\n\n", ch.Name)

			// Generate tokens
			buf.WriteString("\t// Generate connection and subscription tokens via RealtimeTransport interface\n")
			buf.WriteString("\tconnToken, err := transport.GenerateConnectionToken(accountPublicID, 5*time.Minute)\n")
			buf.WriteString("\tif err != nil {\n")
			buf.WriteString("\t\thttputil.WriteError(w, httperror.InternalError(\"failed to generate connection token\"))\n")
			buf.WriteString("\t\treturn\n")
			buf.WriteString("\t}\n")
			buf.WriteString("\tsubToken, err := transport.GenerateSubscriptionToken(accountPublicID, channelName, 5*time.Minute)\n")
			buf.WriteString("\tif err != nil {\n")
			buf.WriteString("\t\thttputil.WriteError(w, httperror.InternalError(\"failed to generate subscription token\"))\n")
			buf.WriteString("\t\treturn\n")
			buf.WriteString("\t}\n\n")
		} else {
			// Public channel
			buf.WriteString("\t// Public channel: no authentication required\n")
			buf.WriteString("\tvar accountID int64 = 0\n\n")

			if ch.RateLimit != nil {
				generateRateLimitCheck(buf, ch)
			}

			// Look up job_results row (no ownership check for public)
			buf.WriteString("\t// Look up job (no ownership verification for public channels)\n")
			buf.WriteString("\tjobResult, err := runner.GetJobResult(r.Context(), queries.GetJobResultParams{\n")
			buf.WriteString("\t\tPublicId: jobID,\n")
			buf.WriteString("\t})\n")
			buf.WriteString("\tif err != nil {\n")
			buf.WriteString("\t\thttputil.WriteError(w, httperror.NotFound(\"job not found\"))\n")
			buf.WriteString("\t\treturn\n")
			buf.WriteString("\t}\n")
			buf.WriteString("\t_ = jobResult // fields available for future use\n\n")

			// Compute scoped channel name for public
			buf.WriteString("\t// [L3] CRITICAL: Channel name must exactly match the worker's channelID() computation.\n")
			fmt.Fprintf(buf, "\tchannelName := channel.ComputeChannelID(%q, accountID, jobID, true)\n\n", ch.Name)

			// Generate tokens with empty sub for anonymous
			buf.WriteString("\t// Generate anonymous connection and subscription tokens\n")
			buf.WriteString("\tconnToken, err := transport.GenerateConnectionToken(\"\", 5*time.Minute)\n")
			buf.WriteString("\tif err != nil {\n")
			buf.WriteString("\t\thttputil.WriteError(w, httperror.InternalError(\"failed to generate connection token\"))\n")
			buf.WriteString("\t\treturn\n")
			buf.WriteString("\t}\n")
			buf.WriteString("\tsubToken, err := transport.GenerateSubscriptionToken(\"\", channelName, 5*time.Minute)\n")
			buf.WriteString("\tif err != nil {\n")
			buf.WriteString("\t\thttputil.WriteError(w, httperror.InternalError(\"failed to generate subscription token\"))\n")
			buf.WriteString("\t\treturn\n")
			buf.WriteString("\t}\n\n")
		}

		// Return tokens + channel name + ws_url
		buf.WriteString("\thttputil.WriteJSON(w, http.StatusOK, map[string]string{\n")
		buf.WriteString("\t\t\"connection_token\":  connToken,\n")
		buf.WriteString("\t\t\"subscription_token\": subToken,\n")
		buf.WriteString("\t\t\"channel\":            channelName,\n")
		buf.WriteString("\t\t\"ws_url\":             transport.ConnectionURL(),\n")
		buf.WriteString("\t})\n")

		buf.WriteString("}\n\n")
	}
}

// generateJobStatusHandler writes the shared job status endpoint handler.
func generateJobStatusHandler(buf *bytes.Buffer, cfg ChannelHTTPGenConfig) {
	hasAuth := false
	for _, ch := range cfg.Channels {
		if !ch.IsPublic {
			hasAuth = true
			break
		}
	}

	buf.WriteString("func handleJobStatus(\n")
	buf.WriteString("\tw http.ResponseWriter,\n")
	buf.WriteString("\tr *http.Request,\n")
	buf.WriteString("\trunner queries.Runner,\n")
	if hasAuth {
		buf.WriteString("\tcheckAuth func(r *http.Request) (accountID int64, orgID int64, accountPublicID string, err error),\n")
	}
	buf.WriteString(") {\n")

	buf.WriteString("\tjobID := r.PathValue(\"id\")\n")
	buf.WriteString("\tif jobID == \"\" {\n")
	buf.WriteString("\t\thttputil.WriteError(w, httperror.BadRequest(\"job id is required\"))\n")
	buf.WriteString("\t\treturn\n")
	buf.WriteString("\t}\n\n")

	if hasAuth {
		buf.WriteString("\t// Require authentication for job status\n")
		buf.WriteString("\taccountID, _, _, err := checkAuth(r)\n")
		buf.WriteString("\tif err != nil {\n")
		buf.WriteString("\t\thttputil.WriteError(w, httperror.Unauthorized(\"authentication required\"))\n")
		buf.WriteString("\t\treturn\n")
		buf.WriteString("\t}\n\n")
	}

	// Look up job via query runner
	buf.WriteString("\t// Look up job_results row via query runner\n")

	if hasAuth {
		buf.WriteString("\tjobResult, err := runner.GetJobResult(r.Context(), queries.GetJobResultParams{\n")
	} else {
		buf.WriteString("\tjobResult, err := runner.GetJobResult(r.Context(), queries.GetJobResultParams{\n")
	}
	buf.WriteString("\t\tPublicId: jobID,\n")
	buf.WriteString("\t})\n")
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString("\t\thttputil.WriteError(w, httperror.NotFound(\"job not found\"))\n")
	buf.WriteString("\t\treturn\n")
	buf.WriteString("\t}\n\n")

	if hasAuth {
		buf.WriteString("\t// Verify requesting account owns the job\n")
		buf.WriteString("\tif jobResult.AccountId != accountID {\n")
		buf.WriteString("\t\thttputil.WriteError(w, httperror.Forbidden(\"you do not own this job\"))\n")
		buf.WriteString("\t\treturn\n")
		buf.WriteString("\t}\n\n")
	}

	// Return job status JSON
	buf.WriteString("\tresult := map[string]any{\n")
	buf.WriteString("\t\t\"job_id\":          jobResult.PublicId,\n")
	buf.WriteString("\t\t\"channel\":         jobResult.ChannelName,\n")
	buf.WriteString("\t\t\"status\":          jobResult.Status,\n")
	buf.WriteString("\t\t\"request_payload\": json.RawMessage(jobResult.RequestPayload),\n")
	buf.WriteString("\t\t\"retry_count\":     jobResult.RetryCount,\n")
	buf.WriteString("\t}\n")
	buf.WriteString("\tif jobResult.ResultPayload != nil {\n")
	buf.WriteString("\t\tresult[\"result_payload\"] = json.RawMessage(*jobResult.ResultPayload)\n")
	buf.WriteString("\t}\n")
	buf.WriteString("\tif jobResult.ErrorMessage != nil {\n")
	buf.WriteString("\t\tresult[\"error_message\"] = *jobResult.ErrorMessage\n")
	buf.WriteString("\t}\n")
	buf.WriteString("\tif jobResult.StartedAt != nil {\n")
	buf.WriteString("\t\tresult[\"started_at\"] = *jobResult.StartedAt\n")
	buf.WriteString("\t}\n")
	buf.WriteString("\tif jobResult.CompletedAt != nil {\n")
	buf.WriteString("\t\tresult[\"completed_at\"] = *jobResult.CompletedAt\n")
	buf.WriteString("\t}\n\n")

	buf.WriteString("\thttputil.WriteJSON(w, http.StatusOK, result)\n")
	buf.WriteString("}\n\n")
}

// generateRateLimitCheck writes a simple IP-based rate limit check for a public channel.
// This generates an in-memory token bucket limiter scoped to the channel. For production
// use, users should replace this with a Redis-backed limiter.
func generateRateLimitCheck(buf *bytes.Buffer, ch codegen.SerializedChannelInfo) {
	if ch.RateLimit == nil {
		return
	}
	// Generate a per-handler rate limiter using sync.Mutex + map.
	// This is a simple token-bucket approach. The generated code uses
	// the channel name to create a unique limiter variable.
	safeName := sanitizeName(ch.Name)
	fmt.Fprintf(buf, "\t// Rate limiting: %d requests/minute, burst %d\n", ch.RateLimit.RequestsPerMinute, ch.RateLimit.BurstSize)
	fmt.Fprintf(buf, "\tif !rateLimiter_%s.Allow(r.RemoteAddr) {\n", safeName)
	buf.WriteString("\t\thttputil.WriteError(w, httperror.TooManyRequests(\"rate limit exceeded\"))\n")
	buf.WriteString("\t\treturn\n")
	buf.WriteString("\t}\n\n")
}

// generateChannelHelpers writes helper functions used by the generated route handlers.
func generateChannelHelpers(buf *bytes.Buffer, cfg ChannelHTTPGenConfig) {
	hasPublic := false
	for _, ch := range cfg.Channels {
		if ch.IsPublic {
			hasPublic = true
			break
		}
	}

	if !hasPublic {
		return
	}

	// Simple in-memory rate limiter for public channels.
	// In production, users can replace this with a Redis-backed implementation.
	buf.WriteString(`// simpleRateLimiter is an in-memory IP-based token bucket rate limiter.
// For production, replace with a Redis-backed implementation.
type simpleRateLimiter struct {
	mu              sync.Mutex
	requestsPerMin  int
	burstSize       int
	clients         map[string]*rateBucket
}

type rateBucket struct {
	tokens    int
	lastReset time.Time
}

func newSimpleRateLimiter(requestsPerMin, burstSize int) *simpleRateLimiter {
	return &simpleRateLimiter{
		requestsPerMin: requestsPerMin,
		burstSize:      burstSize,
		clients:        make(map[string]*rateBucket),
	}
}

func (rl *simpleRateLimiter) Allow(clientIP string) bool {
	rl.mu.Lock()
	defer rl.mu.Unlock()

	now := time.Now()
	bucket, ok := rl.clients[clientIP]
	if !ok {
		bucket = &rateBucket{tokens: rl.burstSize, lastReset: now}
		rl.clients[clientIP] = bucket
	}

	// Refill tokens based on elapsed time
	elapsed := now.Sub(bucket.lastReset)
	if elapsed >= time.Minute {
		bucket.tokens = rl.burstSize
		bucket.lastReset = now
	} else {
		refill := int(elapsed.Seconds()) * rl.requestsPerMin / 60
		bucket.tokens += refill
		if bucket.tokens > rl.burstSize {
			bucket.tokens = rl.burstSize
		}
		bucket.lastReset = now
	}

	if bucket.tokens <= 0 {
		return false
	}
	bucket.tokens--
	return true
}

`)

	// Generate per-channel rate limiter variables
	for _, ch := range cfg.Channels {
		if ch.IsPublic && ch.RateLimit != nil {
			safeName := sanitizeName(ch.Name)
			fmt.Fprintf(buf, "var rateLimiter_%s = newSimpleRateLimiter(%d, %d)\n",
				safeName, ch.RateLimit.RequestsPerMinute, ch.RateLimit.BurstSize)
		}
	}
	buf.WriteString("\n")
}

// sanitizeName converts a channel name to a valid Go identifier component.
// Replaces hyphens and other non-alphanumeric characters with underscores.
func sanitizeName(name string) string {
	return strings.Map(func(r rune) rune {
		if r >= 'a' && r <= 'z' || r >= 'A' && r <= 'Z' || r >= '0' && r <= '9' || r == '_' {
			return r
		}
		return '_'
	}, name)
}
