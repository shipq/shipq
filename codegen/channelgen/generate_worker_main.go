package channelgen

import (
	"bytes"
	"fmt"
	"go/format"

	"github.com/shipq/shipq/codegen"
)

// WorkerGenConfig holds all configuration needed to generate cmd/worker/main.go.
type WorkerGenConfig struct {
	Channels             []codegen.SerializedChannelInfo
	ModulePath           string
	DBDialect            string
	RedisAddr            string // host:port extracted from redis_url in shipq.ini
	CentrifugoAPIURL     string
	CentrifugoAPIKey     string
	CentrifugoHMACSecret string
	CentrifugoWSURL      string
}

// GenerateWorkerMain generates the Go source code for cmd/worker/main.go.
// The generated code:
//   - Opens a DB connection (same pattern as http_main_gen.go)
//   - Creates a dialect-specific query runner
//   - Creates concrete RealtimeTransport and TaskQueue implementations (the ONLY
//     place in generated code that references concrete types like MachineryQueue
//     and CentrifugoTransport)
//   - Registers a wrapped dispatch handler for each channel
//   - Starts the worker, blocking until the context is cancelled
func GenerateWorkerMain(cfg WorkerGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by shipq. DO NOT EDIT.\n")
	buf.WriteString("package main\n\n")

	generateWorkerImports(&buf, cfg)
	generateWorkerMainFunc(&buf, cfg)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return unformatted source for debugging
		return buf.Bytes(), fmt.Errorf("failed to format worker main.go: %w\nunformatted:\n%s", err, buf.String())
	}

	return formatted, nil
}

// generateWorkerImports writes the import block for worker main.go.
func generateWorkerImports(buf *bytes.Buffer, cfg WorkerGenConfig) {
	buf.WriteString("import (\n")
	buf.WriteString("\t\"context\"\n")
	buf.WriteString("\t\"database/sql\"\n")
	buf.WriteString("\t\"os\"\n")
	buf.WriteString("\t\"os/signal\"\n")
	buf.WriteString("\t\"syscall\"\n")
	buf.WriteString("\n")

	// Config package
	configPkg := cfg.ModulePath + "/config"
	fmt.Fprintf(buf, "\t%q\n", configPkg)

	// Channel runtime library
	channelPkg := cfg.ModulePath + "/shipq/lib/channel"
	fmt.Fprintf(buf, "\t%q\n", channelPkg)

	// Queries package (for UpdateJobStatus runner calls)
	queriesPkg := cfg.ModulePath + "/shipq/queries"
	fmt.Fprintf(buf, "\t%q\n", queriesPkg)

	// Dialect-specific query runner import
	runnerPkg := cfg.ModulePath + "/shipq/queries/" + cfg.DBDialect
	fmt.Fprintf(buf, "\tdbrunner %q\n", runnerPkg)

	// Channel packages (for handler references)
	seen := make(map[string]bool)
	for _, ch := range cfg.Channels {
		if ch.PackagePath != "" && !seen[ch.PackagePath] {
			seen[ch.PackagePath] = true
			fmt.Fprintf(buf, "\t%s %q\n", ch.PackageName, ch.PackagePath)
		}
	}

	buf.WriteString("\n")

	// Database driver import (blank import)
	driverImport := getWorkerDriverImport(cfg.DBDialect)
	fmt.Fprintf(buf, "\t_ %q\n", driverImport)

	buf.WriteString(")\n\n")
}

// generateWorkerMainFunc writes the main() function body for the worker.
func generateWorkerMainFunc(buf *bytes.Buffer, cfg WorkerGenConfig) {
	buf.WriteString("func main() {\n")

	// Database connection (same pattern as http_main_gen.go)
	buf.WriteString("\t// Database connection\n")
	buf.WriteString("\tdriver, dsn := config.ParseDatabaseURL(config.Settings.DATABASE_URL)\n")
	buf.WriteString("\tdb, err := sql.Open(driver, dsn)\n")
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString("\t\tconfig.Logger.Error(\"failed to open database\", \"error\", err.Error())\n")
	buf.WriteString("\t\tos.Exit(1)\n")
	buf.WriteString("\t}\n")
	buf.WriteString("\tdefer db.Close()\n\n")

	buf.WriteString("\tif err := db.Ping(); err != nil {\n")
	buf.WriteString("\t\tconfig.Logger.Error(\"failed to connect to database\", \"error\", err.Error())\n")
	buf.WriteString("\t\tos.Exit(1)\n")
	buf.WriteString("\t}\n\n")

	// Create query runner for portable database access (no raw SQL).
	buf.WriteString("\t// Create query runner for portable database access.\n")
	buf.WriteString("\trunner := dbrunner.NewQueryRunner(db)\n\n")

	// Build the UpdateJobFunc closure that calls the generated query runner.
	// This keeps the channel runtime library free of raw SQL while using the
	// portable querydefs system for all database operations.
	buf.WriteString("\t// Build UpdateJobFunc using the generated query runner (no raw SQL).\n")
	buf.WriteString("\tupdateJob := func(publicID, status string, startedAt, completedAt, errorMessage, resultPayload *string, retryCount int) error {\n")
	buf.WriteString("\t\tctx := context.Background()\n")
	buf.WriteString("\t\t_, err := runner.UpdateJobStatus(ctx, queries.UpdateJobStatusParams{\n")
	buf.WriteString("\t\t\tPublicId:      publicID,\n")
	buf.WriteString("\t\t\tStatus:        status,\n")
	buf.WriteString("\t\t\tStartedAt:     startedAt,\n")
	buf.WriteString("\t\t\tCompletedAt:   completedAt,\n")
	buf.WriteString("\t\t\tErrorMessage:  errorMessage,\n")
	buf.WriteString("\t\t\tResultPayload: resultPayload,\n")
	buf.WriteString("\t\t\tRetryCount:    retryCount,\n")
	buf.WriteString("\t\t})\n")
	buf.WriteString("\t\treturn err\n")
	buf.WriteString("\t}\n\n")

	// Create concrete implementations via the interfaces.
	// This is the ONLY place in generated code that references concrete types.
	buf.WriteString("\t// These two variables are the ONLY coupling to Machinery/Centrifugo in the entire worker.\n")
	buf.WriteString("\t// To swap backends, replace them (e.g., channel.NewAsynqQueue(...), channel.NewPusherTransport(...)).\n")
	buf.WriteString("\tvar transport channel.RealtimeTransport = channel.NewCentrifugoTransport(\n")
	buf.WriteString("\t\tconfig.Settings.CENTRIFUGO_API_URL,\n")
	buf.WriteString("\t\tconfig.Settings.CENTRIFUGO_API_KEY,\n")
	buf.WriteString("\t\tconfig.Settings.CENTRIFUGO_HMAC_SECRET,\n")
	buf.WriteString("\t\tconfig.Settings.CENTRIFUGO_WS_URL,\n")
	buf.WriteString("\t)\n\n")

	buf.WriteString("\tqueue, err := channel.NewMachineryQueue(config.Settings.REDIS_URL)\n")
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString("\t\tconfig.Logger.Error(\"failed to create task queue\", \"error\", err.Error())\n")
	buf.WriteString("\t\tos.Exit(1)\n")
	buf.WriteString("\t}\n")
	buf.WriteString("\tvar _ channel.TaskQueue = queue\n\n")

	// Register task handlers for each channel.
	buf.WriteString("\t// Register task handlers for each channel.\n")
	for _, ch := range cfg.Channels {
		// Find the dispatch message's handler name
		handlerName := ""
		for _, msg := range ch.Messages {
			if msg.IsDispatch {
				handlerName = msg.HandlerName
				break
			}
		}
		if handlerName == "" {
			continue
		}

		// The handler reference is <packageName>.<HandlerName> (e.g., chatbot.HandleChatRequest).
		//
		// Naming convention: each channel's dispatch handler must be an exported function
		// named Handle<TypeName> in the channel package (e.g., HandleChatRequest for a
		// ChatRequest dispatch type). This is detected automatically by static analysis
		// of the channel package's Go source files (see channel_static_analysis.go).
		// If static analysis hasn't run, the default Handle<TypeName> name from channel
		// registration is used.
		handlerRef := ch.PackageName + "." + handlerName

		if ch.HasSetup {
			setupRef := ch.PackageName + ".Setup"
			fmt.Fprintf(buf, "\tif err := queue.RegisterTask(%q, channel.WrapDispatchHandlerWithUpdater(%s, transport, updateJob, %q, channel.WithSetup(%s), channel.WithDispatchDB(db))); err != nil {\n", ch.Name, handlerRef, ch.Name, setupRef)
		} else {
			fmt.Fprintf(buf, "\tif err := queue.RegisterTask(%q, channel.WrapDispatchHandlerWithUpdater(%s, transport, updateJob, %q, channel.WithDispatchDB(db))); err != nil {\n", ch.Name, handlerRef, ch.Name)
		}
		fmt.Fprintf(buf, "\t\tconfig.Logger.Error(\"failed to register task\", \"task\", %q, \"error\", err.Error())\n", ch.Name)
		buf.WriteString("\t\tos.Exit(1)\n")
		buf.WriteString("\t}\n")
	}
	buf.WriteString("\n")

	// Start the worker with context cancellation for graceful shutdown.
	buf.WriteString("\t// Start the worker. Blocks until context is cancelled.\n")
	buf.WriteString("\tctx, cancel := signal.NotifyContext(context.Background(), syscall.SIGINT, syscall.SIGTERM)\n")
	buf.WriteString("\tdefer cancel()\n\n")

	buf.WriteString("\tconfig.Logger.Info(\"starting worker\", \"concurrency\", 10)\n")
	buf.WriteString("\tif err := queue.StartWorker(ctx, \"shipq-worker\", 10); err != nil {\n")
	buf.WriteString("\t\tif ctx.Err() != nil {\n")
	buf.WriteString("\t\t\tconfig.Logger.Info(\"worker shutting down gracefully\")\n")
	buf.WriteString("\t\t} else {\n")
	buf.WriteString("\t\t\tconfig.Logger.Error(\"worker failed\", \"error\", err.Error())\n")
	buf.WriteString("\t\t\tos.Exit(1)\n")
	buf.WriteString("\t\t}\n")
	buf.WriteString("\t}\n")

	buf.WriteString("}\n")
}

// getWorkerDriverImport returns the import path for the database driver.
func getWorkerDriverImport(dialect string) string {
	switch dialect {
	case "postgres":
		return "github.com/jackc/pgx/v5/stdlib"
	case "sqlite":
		return "modernc.org/sqlite"
	case "mysql":
		return "github.com/go-sql-driver/mysql"
	default:
		return "github.com/go-sql-driver/mysql"
	}
}
