package openapigen

import (
	"bytes"
	"fmt"
	"go/format"

	"github.com/shipq/shipq/codegen"
)

// OpenAPITestGenConfig holds configuration for generating the OpenAPI endpoint test.
type OpenAPITestGenConfig struct {
	ModulePath      string // e.g., "myapp"
	OutputPkg       string // package name for generated code (e.g., "api")
	DBDialect       string // "mysql", "postgres", or "sqlite"
	TestDatabaseURL string // test database URL derived from shipq.ini
}

// GenerateOpenAPITest generates a test file that verifies the OpenAPI
// development endpoints (/openapi, /docs, /openapi/assets/*) return the
// expected responses. The generated test uses the existing test harness
// (NewUnauthenticatedTestServer) to spin up a real HTTP server.
func GenerateOpenAPITest(cfg OpenAPITestGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by shipq.\n")
	fmt.Fprintf(&buf, "package %s\n\n", cfg.OutputPkg)

	generateOpenAPITestImports(&buf, cfg)
	generateOpenAPITestServerSetup(&buf, cfg)
	generateTestOpenAPISpecEndpoint(&buf)
	generateTestDocsEndpoint(&buf)
	generateTestAssetsEndpoint(&buf)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format OpenAPI test code: %w\nunformatted:\n%s", err, buf.String())
	}

	return formatted, nil
}

func generateOpenAPITestImports(buf *bytes.Buffer, cfg OpenAPITestGenConfig) {
	buf.WriteString("import (\n")
	buf.WriteString("\t\"database/sql\"\n")
	buf.WriteString("\t\"encoding/json\"\n")
	buf.WriteString("\t\"io\"\n")
	buf.WriteString("\t\"net/http\"\n")
	buf.WriteString("\t\"os\"\n")
	buf.WriteString("\t\"strings\"\n")
	buf.WriteString("\t\"testing\"\n")
	buf.WriteString("\n")

	// Driver import (dialect-specific)
	fmt.Fprintf(buf, "\t%s\n", codegen.DriverImportForDialect(cfg.DBDialect))
	buf.WriteString("\n")

	// Config package import
	configPkg := cfg.ModulePath + "/config"
	fmt.Fprintf(buf, "\t%q\n", configPkg)

	buf.WriteString(")\n\n")
}

func generateOpenAPITestServerSetup(buf *bytes.Buffer, cfg OpenAPITestGenConfig) {
	buf.WriteString(`// newOpenAPITestServer creates a test server and returns its URL.
// OpenAPI routes are registered when GO_ENV is "development" or "test".
func newOpenAPITestServer(t *testing.T) string {
	t.Helper()

	// Get test database connection
	dbURL := os.Getenv("TEST_DATABASE_URL")
	if dbURL == "" {
`)
	fmt.Fprintf(buf, "\t\tdbURL = %q\n", cfg.TestDatabaseURL)
	buf.WriteString(`	}
	if dbURL == "" {
		t.Skip("TEST_DATABASE_URL not set, skipping OpenAPI integration test")
	}
	if !config.IsLocalhostURL(dbURL) {
		t.Fatal("test database URL must point to localhost")
	}

	driver, dsn := config.ParseDatabaseURL(dbURL)
	db, err := sql.Open(driver, dsn)
	if err != nil {
		t.Fatalf("failed to open database: %v", err)
	}
	t.Cleanup(func() { db.Close() })

	ts := NewUnauthenticatedTestServer(t, db)
	return ts.Server.URL
}

`)
}

func generateTestOpenAPISpecEndpoint(buf *bytes.Buffer) {
	buf.WriteString(`func TestOpenAPISpec(t *testing.T) {
	serverURL := newOpenAPITestServer(t)

	resp, err := http.Get(serverURL + "/openapi")
	if err != nil {
		t.Fatalf("GET /openapi failed: %v", err)
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		t.Fatalf("expected status 200, got %d", resp.StatusCode)
	}

	ct := resp.Header.Get("Content-Type")
	if ct != "application/json" {
		t.Errorf("expected Content-Type application/json, got %q", ct)
	}

	body, err := io.ReadAll(resp.Body)
	if err != nil {
		t.Fatalf("failed to read response body: %v", err)
	}

	// Should be valid JSON
	var spec map[string]any
	if err := json.Unmarshal(body, &spec); err != nil {
		t.Fatalf("response is not valid JSON: %v", err)
	}

	// Should be OpenAPI 3.1
	version, ok := spec["openapi"].(string)
	if !ok || !strings.HasPrefix(version, "3.1") {
		t.Errorf("expected openapi version starting with 3.1, got %v", spec["openapi"])
	}

	// Should have paths
	if _, ok := spec["paths"]; !ok {
		t.Error("expected paths in spec")
	}
}

`)
}

func generateTestDocsEndpoint(buf *bytes.Buffer) {
	buf.WriteString(`func TestOpenAPIDocs(t *testing.T) {
	serverURL := newOpenAPITestServer(t)

	resp, err := http.Get(serverURL + "/docs")
	if err != nil {
		t.Fatalf("GET /docs failed: %v", err)
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		t.Fatalf("expected status 200, got %d", resp.StatusCode)
	}

	ct := resp.Header.Get("Content-Type")
	if !strings.HasPrefix(ct, "text/html") {
		t.Errorf("expected Content-Type starting with text/html, got %q", ct)
	}

	body, err := io.ReadAll(resp.Body)
	if err != nil {
		t.Fatalf("failed to read response body: %v", err)
	}

	bodyStr := string(body)
	if !strings.Contains(bodyStr, "elements-api") {
		t.Error("expected Stoplight Elements web component in docs page")
	}
	if !strings.Contains(bodyStr, "/openapi") {
		t.Error("expected /openapi URL reference in docs page")
	}
}

`)
}

func generateTestAssetsEndpoint(buf *bytes.Buffer) {
	buf.WriteString(`func TestOpenAPIAssets(t *testing.T) {
	serverURL := newOpenAPITestServer(t)

	tests := []struct {
		path        string
		contentType string
	}{
		{"/openapi/assets/web-components.min.js", "application/javascript"},
		{"/openapi/assets/styles.min.css", "text/css"},
	}

	for _, tt := range tests {
		t.Run(tt.path, func(t *testing.T) {
			resp, err := http.Get(serverURL + tt.path)
			if err != nil {
				t.Fatalf("GET %s failed: %v", tt.path, err)
			}
			defer resp.Body.Close()

			if resp.StatusCode != http.StatusOK {
				t.Fatalf("expected status 200, got %d", resp.StatusCode)
			}

			ct := resp.Header.Get("Content-Type")
			if ct != tt.contentType {
				t.Errorf("expected Content-Type %q, got %q", tt.contentType, ct)
			}

			body, err := io.ReadAll(resp.Body)
			if err != nil {
				t.Fatalf("failed to read response body: %v", err)
			}

			if len(body) == 0 {
				t.Error("expected non-empty response body")
			}
		})
	}
}
`)
}
