func TestGenerateSvelteHooks_CustomMutation_NoAutoInvalidation(t *testing.T) {
	handlers := append(makePostsHandlers(), makeCustomHandler())
	result, err := GenerateSvelteHooks(handlers)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	output := string(result)

	if !strings.Contains(output, "export function createPublishPostMutation(") {
		t.Error("should generate createPublishPostMutation")
	}

	// Find the createPublishPostMutation function and check it does NOT have auto-invalidation
	lines := strings.Split(output, "\n")
	inPublishHook := false
	braceDepth := 0
	hasInvalidation := false
	for _, line := range lines {
		if strings.Contains(line, "export function createPublishPostMutation(") {
			inPublishHook = true
			braceDepth = 0
			continue
		}
		if inPublishHook {
			braceDepth += strings.Count(line, "{") - strings.Count(line, "}")
			if strings.Contains(line, "queryClient.invalidateQueries") {
				hasInvalidation = true
			}
			if braceDepth <= 0 && strings.TrimSpace(line) == "}" {
				break
			}
		}
	}
	if hasInvalidation {
		t.Error("custom mutation createPublishPostMutation should NOT have auto-invalidation")
	}

	// Also check that it does NOT use useQueryClient
	inPublishHook = false
	braceDepth = 0
	hasQueryClient := false
	for _, line := range lines {
		if strings.Contains(line, "export function createPublishPostMutation(") {
			inPublishHook = true
			braceDepth = 0
			continue
		}
		if inPublishHook {
			braceDepth += strings.Count(line, "{") - strings.Count(line, "}")
			if strings.Contains(line, "useQueryClient()") {
				hasQueryClient = true
			}
			if braceDepth <= 0 && strings.TrimSpace(line) == "}" {
				break
			}
		}
	}
	if hasQueryClient {
		t.Error("custom mutation createPublishPostMutation should NOT use useQueryClient")
	}
}

