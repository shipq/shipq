func TestGenerateSvelteHooks_QueryKeysSameAsReact(t *testing.T) {
	// The query key structure should be identical between React and Svelte
	reactResult, err := GenerateReactHooks(makePostsHandlers())
	if err != nil {
		t.Fatalf("unexpected error generating React hooks: %v", err)
	}
	svelteResult, err := GenerateSvelteHooks(makePostsHandlers())
	if err != nil {
		t.Fatalf("unexpected error generating Svelte hooks: %v", err)
	}

	reactOutput := string(reactResult)
	svelteOutput := string(svelteResult)

	// Extract the postsKeys block from both outputs
	reactKeysStart := strings.Index(reactOutput, "export const postsKeys = {")
	svelteKeysStart := strings.Index(svelteOutput, "export const postsKeys = {")

	if reactKeysStart < 0 {
		t.Fatal("React output missing postsKeys")
	}
	if svelteKeysStart < 0 {
		t.Fatal("Svelte output missing postsKeys")
	}

	reactKeysEnd := strings.Index(reactOutput[reactKeysStart:], "};")
	svelteKeysEnd := strings.Index(svelteOutput[svelteKeysStart:], "};")

	if reactKeysEnd < 0 || svelteKeysEnd < 0 {
		t.Fatal("Could not find end of postsKeys block")
	}

	reactKeys := reactOutput[reactKeysStart : reactKeysStart+reactKeysEnd+2]
	svelteKeys := svelteOutput[svelteKeysStart : svelteKeysStart+svelteKeysEnd+2]

	if reactKeys != svelteKeys {
		t.Errorf("Query keys should be identical between React and Svelte.\nReact:\n%s\n\nSvelte:\n%s", reactKeys, svelteKeys)
	}
}

