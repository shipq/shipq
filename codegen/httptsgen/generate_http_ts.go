// CRUDRole classifies a handler as a known CRUD operation or a custom handler.
type CRUDRole int

const (
	CRUDRoleNone      CRUDRole = iota // custom handler
	CRUDRoleCreate                    // POST /{table}
	CRUDRoleList                      // GET /{table}
	CRUDRoleGetOne                    // GET /{table}/:id
	CRUDRoleUpdate                    // PATCH /{table}/:id
	CRUDRoleDelete                    // DELETE /{table}/:id
	CRUDRoleAdminList                 // GET /admin/{table}
	CRUDRoleUndelete                  // POST /admin/{table}/:id/undelete
)

// DetectCRUDRole returns the CRUD role for a handler, or CRUDRoleNone if it
// doesn't match any known pattern from handlergen/register_incremental.go.
func DetectCRUDRole(h codegen.SerializedHandlerInfo) CRUDRole {
	funcName := h.FuncName
	method := h.Method
	pathStr := h.Path

	// Extract the resource segment from the path.
	// For standard CRUD: /{table} or /{table}/:id
	// For admin: /admin/{table} or /admin/{table}/:id/undelete
	segments := strings.Split(strings.TrimPrefix(pathStr, "/"), "/")

	// Check admin patterns first
	if len(segments) >= 2 && segments[0] == "admin" {
		tableName := segments[1]
		pluralPascal := tsutil.ToPascalCase(tableName)

		// AdminList: GET /admin/{table}
		if method == "GET" && len(segments) == 2 && funcName == "AdminList"+pluralPascal {
			return CRUDRoleAdminList
		}

		// Undelete: POST /admin/{table}/:id/undelete
		if method == "POST" && len(segments) == 4 && segments[3] == "undelete" {
			singularPascal := singularPascalFromTable(tableName)
			if funcName == "Undelete"+singularPascal {
				return CRUDRoleUndelete
			}
		}

		return CRUDRoleNone
	}

	// Standard CRUD patterns
	if len(segments) == 0 {
		return CRUDRoleNone
	}

	tableName := segments[0]
	pluralPascal := tsutil.ToPascalCase(tableName)
	singularPascal := singularPascalFromTable(tableName)

	switch method {
	case "POST":
		// Create: POST /{table}
		if len(segments) == 1 && funcName == "Create"+singularPascal {
			return CRUDRoleCreate
		}
	case "GET":
		if len(segments) == 1 && funcName == "List"+pluralPascal {
			return CRUDRoleList
		}
		if len(segments) == 2 && funcName == "Get"+singularPascal {
			return CRUDRoleGetOne
		}
	case "PATCH":
		if len(segments) == 2 && funcName == "Update"+singularPascal {
			return CRUDRoleUpdate
		}
	case "DELETE":
		if len(segments) == 2 && funcName == "SoftDelete"+singularPascal {
			return CRUDRoleDelete
		}
	}

	return CRUDRoleNone
}

// singularPascalFromTable derives the PascalCase singular form of a table name.
// The handlergen convention is: resourceName(tableName) strips a trailing "s"
// and then PascalCases the result. We replicate that heuristic here.
func singularPascalFromTable(tableName string) string {
	singular := tableName
	if strings.HasSuffix(singular, "s") && len(singular) > 1 {
		singular = singular[:len(singular)-1]
	}
	return tsutil.ToPascalCase(singular)
}

// GenerateHTTPTypeScriptClient generates the base HTTP client (shipq-api.ts).
// It produces typed interfaces and async fetch-wrapper functions for every handler.
func GenerateHTTPTypeScriptClient(handlers []codegen.SerializedHandlerInfo) ([]byte, error) {
	if len(handlers) == 0 {
		return []byte("// No handlers defined.\n"), nil
	}

	var buf bytes.Buffer

	// Header
	buf.WriteString("// Code generated by shipq. DO NOT EDIT.\n\n")

	// Configuration
	writeConfigSection(&buf)

	// Query string helper
	writeQueryHelper(&buf)

	// Shared fetch wrapper
	writeFetchWrapper(&buf)

	// ApiError class
	writeApiError(&buf)

	// Group handlers by package for organized output
	groups := groupHandlersByPackage(handlers)
	pkgNames := sortedPackageNames(groups)

	// Emit types and functions per package
	for _, pkgName := range pkgNames {
		pkgHandlers := groups[pkgName]

		buf.WriteString(fmt.Sprintf("\n// ─── %s ───\n", pkgName))

		// Sort handlers deterministically within each package
		sort.Slice(pkgHandlers, func(i, j int) bool {
			return pkgHandlers[i].FuncName < pkgHandlers[j].FuncName
		})

		// Emit types
		for _, h := range pkgHandlers {
			writeHandlerTypes(&buf, h)
		}

		// Emit API functions
		for _, h := range pkgHandlers {
			writeHandlerFunction(&buf, h)
		}
	}

	return buf.Bytes(), nil
}

// writeConfigSection writes the ApiConfig interface and configuration helpers.
func writeConfigSection(buf *bytes.Buffer) {
	buf.WriteString("// ─── Configuration ───\n\n")

	buf.WriteString("export interface ApiConfig {\n")
	buf.WriteString("  baseURL: string;\n")
	buf.WriteString("  /** Called on every request. Return headers to merge (e.g., Authorization). */\n")
	buf.WriteString("  getHeaders?: () => Record<string, string> | Promise<Record<string, string>>;\n")
	buf.WriteString("  /** Called when a request returns 401. */\n")
	buf.WriteString("  onUnauthorized?: () => void;\n")
	buf.WriteString("}\n\n")

	buf.WriteString("let _config: ApiConfig | null = null;\n\n")

	buf.WriteString("export function configureApi(cfg: ApiConfig): void {\n")
	buf.WriteString("  _config = cfg;\n")
	buf.WriteString("}\n\n")

	buf.WriteString("function getConfig(): ApiConfig {\n")
	buf.WriteString("  if (!_config) throw new Error(\"shipq API not configured — call configureApi() first\");\n")
	buf.WriteString("  return _config;\n")
	buf.WriteString("}\n")
}

// writeQueryHelper writes the buildQuery helper function.
func writeQueryHelper(buf *bytes.Buffer) {
	buf.WriteString("\n// ─── Query string helper ───\n\n")

	buf.WriteString("function buildQuery(params?: Record<string, unknown>): string {\n")
	buf.WriteString("  if (!params) return \"\";\n")
	buf.WriteString("  const parts: string[] = [];\n")
	buf.WriteString("  for (const [key, value] of Object.entries(params)) {\n")
	buf.WriteString("    if (value !== undefined && value !== null) {\n")
	buf.WriteString("      parts.push(`${encodeURIComponent(key)}=${encodeURIComponent(String(value))}`);\n")
	buf.WriteString("    }\n")
	buf.WriteString("  }\n")
	buf.WriteString("  return parts.length > 0 ? `?${parts.join(\"&\")}` : \"\";\n")
	buf.WriteString("}\n")
}

// writeFetchWrapper writes the shared request<T> function.
func writeFetchWrapper(buf *bytes.Buffer) {
	buf.WriteString("\n// ─── Shared fetch wrapper ───\n\n")

	buf.WriteString("async function request<T>(\n")
	buf.WriteString("  method: string,\n")
	buf.WriteString("  path: string,\n")
	buf.WriteString("  body?: unknown,\n")
	buf.WriteString("): Promise<T> {\n")
	buf.WriteString("  const cfg = getConfig();\n")
	buf.WriteString("  const headers: Record<string, string> = { \"Content-Type\": \"application/json\" };\n")
	buf.WriteString("  if (cfg.getHeaders) {\n")
	buf.WriteString("    Object.assign(headers, await cfg.getHeaders());\n")
	buf.WriteString("  }\n")
	buf.WriteString("  const res = await fetch(`${cfg.baseURL}${path}`, {\n")
	buf.WriteString("    method,\n")
	buf.WriteString("    headers,\n")
	buf.WriteString("    body: body ? JSON.stringify(body) : undefined,\n")
	buf.WriteString("    credentials: \"include\",\n")
	buf.WriteString("  });\n")
	buf.WriteString("  if (res.status === 401 && cfg.onUnauthorized) {\n")
	buf.WriteString("    cfg.onUnauthorized();\n")
	buf.WriteString("  }\n")
	buf.WriteString("  if (!res.ok) {\n")
	buf.WriteString("    const text = await res.text().catch(() => \"\");\n")
	buf.WriteString("    throw new ApiError(res.status, text);\n")
	buf.WriteString("  }\n")
	buf.WriteString("  if (res.status === 204) return undefined as T;\n")
	buf.WriteString("  return res.json();\n")
	buf.WriteString("}\n")
}

// writeApiError writes the ApiError class.
func writeApiError(buf *bytes.Buffer) {
	buf.WriteString("\nexport class ApiError extends Error {\n")
	buf.WriteString("  constructor(public status: number, public body: string) {\n")
	buf.WriteString("    super(`API error ${status}: ${body}`);\n")
	buf.WriteString("    this.name = \"ApiError\";\n")
	buf.WriteString("  }\n")
	buf.WriteString("}\n")
}

// writeHandlerTypes writes request/response TypeScript interfaces for a handler.
func writeHandlerTypes(buf *bytes.Buffer, h codegen.SerializedHandlerInfo) {
	// Request type (if present and has body fields)
	if h.Request != nil && len(h.Request.Fields) > 0 {
		bodyFields := filterBodyFields(h)
		if len(bodyFields) > 0 && codegen.MethodHasBody(h.Method) {
			typeName := h.FuncName + "Request"
			tsutil.GenerateTSInterface(buf, typeName, bodyFields)
		}
	}

	// Query params type (if present)
	queryFields := codegen.FilterQueryFields(h)
	if len(queryFields) > 0 {
		typeName := h.FuncName + "Params"
		writeQueryParamsInterface(buf, typeName, queryFields)
	}

	// Response type
	if h.Response != nil && len(h.Response.Fields) > 0 {
		typeName := h.FuncName + "Response"
		tsutil.GenerateTSInterface(buf, typeName, h.Response.Fields)
	}
}

// writeQueryParamsInterface writes a TypeScript interface for query parameters.
// All query params are optional (they have defaults or are nullable).
func writeQueryParamsInterface(buf *bytes.Buffer, name string, fields []codegen.SerializedFieldInfo) {
	fmt.Fprintf(buf, "\nexport interface %s {\n", name)
	for _, field := range fields {
		queryName := field.Tags["query"]
		tsType := tsutil.GoTypeStringToTS(field.Type)
		fmt.Fprintf(buf, "  %s?: %s;\n", queryName, tsType)
	}
	buf.WriteString("}\n")
}

// writeHandlerFunction writes the async API function for a handler.
func writeHandlerFunction(buf *bytes.Buffer, h codegen.SerializedHandlerInfo) {
	funcName := tsutil.ToCamelCase(h.FuncName)

	// Build the function signature
	var params []string
	var pathExpr string

	// Path params as function arguments
	for _, pp := range h.PathParams {
		params = append(params, pp.Name+": string")
	}

	// Build the URL path expression
	pathExpr = buildPathExpression(h.Path, h.PathParams)

	// Determine request/response types
	hasBody := codegen.MethodHasBody(h.Method) && h.Request != nil && len(filterBodyFields(h)) > 0
	hasResponse := h.Response != nil && len(h.Response.Fields) > 0

	reqTypeName := h.FuncName + "Request"
	respTypeName := h.FuncName + "Response"

	// Derive query params from query: tags
	queryFields := codegen.FilterQueryFields(h)
	hasQueryParams := len(queryFields) > 0
	paramsTypeName := h.FuncName + "Params"

	if hasQueryParams {
		params = append(params, "params?: "+paramsTypeName)
	}

	// Add body param for methods that have a body
	if hasBody {
		params = append(params, "req: "+reqTypeName)
	}

	// Determine return type
	returnType := "void"
	if hasResponse {
		returnType = respTypeName
	}

	// Write JSDoc comment
	fmt.Fprintf(buf, "\n/** %s %s */\n", h.Method, h.Path)

	// Write function signature
	fmt.Fprintf(buf, "export async function %s(%s): Promise<%s> {\n",
		funcName, strings.Join(params, ", "), returnType)

	// Build the path string
	if hasQueryParams {
		fmt.Fprintf(buf, "  const query = buildQuery(params as Record<string, unknown>);\n")
		fmt.Fprintf(buf, "  return request<%s>(\"%s\", `%s${query}`", returnType, h.Method, pathExpr)
	} else {
		fmt.Fprintf(buf, "  return request<%s>(\"%s\", `%s`", returnType, h.Method, pathExpr)
	}

	if hasBody {
		buf.WriteString(", req")
	}

	buf.WriteString(");\n")
	buf.WriteString("}\n")
}

// buildPathExpression converts a route path with :param placeholders into a
// TypeScript template literal expression with encodeURIComponent calls.
// e.g., "/posts/:id" -> "/posts/${encodeURIComponent(id)}"
func buildPathExpression(routePath string, pathParams []codegen.SerializedPathParam) string {
	result := routePath
	for _, pp := range pathParams {
		placeholder := ":" + pp.Name
		replacement := fmt.Sprintf("${encodeURIComponent(%s)}", pp.Name)
		result = strings.Replace(result, placeholder, replacement, 1)
	}
	return result
}

// filterBodyFields returns request fields that are NOT path parameters.
// This mirrors the logic in openapigen.filterBodyFields.
func filterBodyFields(h codegen.SerializedHandlerInfo) []codegen.SerializedFieldInfo {
	if h.Request == nil {
		return nil
	}

	pathParamNames := make(map[string]bool)
	for _, pp := range h.PathParams {
		pathParamNames[strings.ToLower(pp.Name)] = true
	}

	var bodyFields []codegen.SerializedFieldInfo
	for _, f := range h.Request.Fields {
		// Exclude fields that are path parameters
		if f.Tags != nil && f.Tags["path"] != "" && pathParamNames[strings.ToLower(f.Tags["path"])] {
			continue
		}
		if pathParamNames[strings.ToLower(f.JSONName)] || pathParamNames[strings.ToLower(f.Name)] {
			continue
		}
		// Exclude fields that are query parameters
		if f.Tags != nil && f.Tags["query"] != "" {
			continue
		}
		bodyFields = append(bodyFields, f)
	}

	return bodyFields
}

// groupHandlersByPackage groups handlers by the last segment of their PackagePath.
func groupHandlersByPackage(handlers []codegen.SerializedHandlerInfo) map[string][]codegen.SerializedHandlerInfo {
	groups := make(map[string][]codegen.SerializedHandlerInfo)
	for _, h := range handlers {
		pkgName := path.Base(h.PackagePath)
		if pkgName == "." || pkgName == "" {
			pkgName = "api"
		}
		groups[pkgName] = append(groups[pkgName], h)
	}
	return groups
}

// sortedPackageNames returns the package names from a grouped map in sorted order.
func sortedPackageNames(groups map[string][]codegen.SerializedHandlerInfo) []string {
	names := make([]string, 0, len(groups))
	for name := range groups {
		names = append(names, name)
	}
	sort.Strings(names)
	return names
}
