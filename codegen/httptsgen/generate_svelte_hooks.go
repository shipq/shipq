package httptsgen

import (
	"bytes"
	"fmt"
	"sort"
	"strings"

	"github.com/shipq/shipq/codegen"
	"github.com/shipq/shipq/codegen/tsutil"
)

// GenerateSvelteHooks generates the Svelte integration layer (svelte/shipq-api.ts).
// It produces TanStack Svelte Query functions (createQuery/createMutation) for every handler,
// with per-package query key factories and automatic CRUD cache invalidation.
func GenerateSvelteHooks(handlers []codegen.SerializedHandlerInfo) ([]byte, error) {
	if len(handlers) == 0 {
		return []byte("// No handlers defined.\n"), nil
	}

	var buf bytes.Buffer

	// Header
	buf.WriteString("// Code generated by shipq. DO NOT EDIT.\n\n")

	// Collect all imports needed from the base client
	groups := groupHandlersByPackage(handlers)
	pkgNames := sortedPackageNames(groups)

	// Build import list — track which base API function names collide with
	// Svelte wrapper names so we can alias them.
	var typeImports []string
	var funcImports []string       // non-aliased
	var aliasedImports [][2]string // [original, alias]

	for _, pkgName := range pkgNames {
		pkgHandlers := groups[pkgName]
		sort.Slice(pkgHandlers, func(i, j int) bool {
			return pkgHandlers[i].FuncName < pkgHandlers[j].FuncName
		})
		for _, h := range pkgHandlers {
			camelName := tsutil.ToCamelCase(h.FuncName)
			svelteWrapperName := svelteHookName(h)

			// If the base API function name starts with "create" and the Svelte
			// wrapper also starts with "create", we may have a collision.
			// e.g., base: createPost, wrapper: createCreatePostMutation
			// No collision in that case. But if the base func is literally
			// "createPost" and the wrapper is also "createPost..." they won't
			// collide because the wrapper has a suffix. The real collision is
			// when the base name equals the wrapper name exactly, which happens
			// when a base function is named like "createGetPostQuery" etc.
			// In practice, the main collision is: base "createPost" vs wrapper
			// "createCreatePostMutation" — no collision.
			//
			// However, per the spec, when a base API function name collides with
			// the Svelte wrapper, we alias. The canonical example: base function
			// "createPost" and Svelte wrapper "createCreatePostMutation" — no
			// collision. But let's be safe and check for exact collision.
			if camelName == svelteWrapperName {
				aliasedImports = append(aliasedImports, [2]string{camelName, "api" + h.FuncName})
			} else {
				// Also check if the camelName starts with "create" which could
				// collide with a createQuery/createMutation import from svelte-query.
				// The TanStack imports are createQuery and createMutation — if a
				// base function is literally named "createQuery" or "createMutation"
				// that would be a problem, but that's astronomically unlikely.
				funcImports = append(funcImports, camelName)
			}

			hasBody := codegen.MethodHasBody(h.Method) && h.Request != nil && len(filterBodyFields(h)) > 0
			if hasBody {
				typeImports = append(typeImports, h.FuncName+"Request")
			}

			hasResponse := h.Response != nil && len(h.Response.Fields) > 0
			if hasResponse {
				typeImports = append(typeImports, h.FuncName+"Response")
			}
		}
	}

	// Write TanStack Svelte Query imports
	buf.WriteString("import {\n")
	buf.WriteString("  createQuery,\n")
	buf.WriteString("  createMutation,\n")
	buf.WriteString("  useQueryClient,\n")
	buf.WriteString("  type CreateQueryOptions,\n")
	buf.WriteString("  type CreateMutationOptions,\n")
	buf.WriteString("} from \"@tanstack/svelte-query\";\n\n")

	// Write base client imports
	buf.WriteString("import {\n")
	for _, ti := range typeImports {
		fmt.Fprintf(&buf, "  type %s,\n", ti)
	}
	for _, ai := range aliasedImports {
		fmt.Fprintf(&buf, "  %s as %s,\n", ai[0], ai[1])
	}
	for _, fi := range funcImports {
		fmt.Fprintf(&buf, "  %s,\n", fi)
	}
	buf.WriteString("} from \"../shipq-api\";\n")

	// Generate per-package keys and hooks
	for _, pkgName := range pkgNames {
		pkgHandlers := groups[pkgName]
		sort.Slice(pkgHandlers, func(i, j int) bool {
			return pkgHandlers[i].FuncName < pkgHandlers[j].FuncName
		})

		writeSvelteQueryKeys(&buf, pkgName, pkgHandlers)
		writeSvelteHooks(&buf, pkgName, pkgHandlers)
	}

	return buf.Bytes(), nil
}

// svelteHookName returns the Svelte wrapper function name for a handler.
// GET handlers → create{FuncName}Query
// Non-GET handlers → create{FuncName}Mutation
func svelteHookName(h codegen.SerializedHandlerInfo) string {
	if h.Method == "GET" {
		return "create" + h.FuncName + "Query"
	}
	return "create" + h.FuncName + "Mutation"
}

// svelteBaseFuncRef returns the name used to call the base API function.
// If the base function name collides with the Svelte wrapper name, it returns
// the aliased name ("api" + FuncName).
func svelteBaseFuncRef(h codegen.SerializedHandlerInfo) string {
	camelName := tsutil.ToCamelCase(h.FuncName)
	wrapperName := svelteHookName(h)
	if camelName == wrapperName {
		return "api" + h.FuncName
	}
	return camelName
}

// writeSvelteQueryKeys writes the query key factory object for a package.
// The key structure is identical to the React layer.
func writeSvelteQueryKeys(buf *bytes.Buffer, pkgName string, handlers []codegen.SerializedHandlerInfo) {
	fmt.Fprintf(buf, "\n// ─── Query Keys: %s ───\n\n", pkgName)
	fmt.Fprintf(buf, "export const %sKeys = {\n", pkgName)
	fmt.Fprintf(buf, "  all: [\"%s\"] as const,\n", pkgName)

	for _, h := range handlers {
		camelName := tsutil.ToCamelCase(h.FuncName)
		if len(h.PathParams) > 0 {
			var params []string
			for _, pp := range h.PathParams {
				params = append(params, pp.Name+": string")
			}
			var paramNames []string
			for _, pp := range h.PathParams {
				paramNames = append(paramNames, pp.Name)
			}
			fmt.Fprintf(buf, "  %s: (%s) => [...%sKeys.all, \"%s\", %s] as const,\n",
				camelName, strings.Join(params, ", "), pkgName, camelName, strings.Join(paramNames, ", "))
		} else {
			fmt.Fprintf(buf, "  %s: () => [...%sKeys.all, \"%s\"] as const,\n",
				camelName, pkgName, camelName)
		}
	}

	buf.WriteString("};\n")
}

// writeSvelteHooks writes the Svelte hooks for a package's handlers.
func writeSvelteHooks(buf *bytes.Buffer, pkgName string, handlers []codegen.SerializedHandlerInfo) {
	for _, h := range handlers {
		if h.Method == "GET" {
			writeSvelteQueryHook(buf, pkgName, h)
		} else {
			writeSvelteMutationHook(buf, pkgName, h)
		}
	}
}

// writeSvelteQueryHook writes a createQuery wrapper for a GET handler.
func writeSvelteQueryHook(buf *bytes.Buffer, pkgName string, h codegen.SerializedHandlerInfo) {
	funcName := tsutil.ToCamelCase(h.FuncName)
	wrapperName := svelteHookName(h)
	baseFuncRef := svelteBaseFuncRef(h)
	role := DetectCRUDRole(h)
	isList := role == CRUDRoleList || role == CRUDRoleAdminList

	hasResponse := h.Response != nil && len(h.Response.Fields) > 0
	respType := "void"
	if hasResponse {
		respType = h.FuncName + "Response"
	}

	buf.WriteString("\n")

	// Build function params
	var params []string
	for _, pp := range h.PathParams {
		params = append(params, pp.Name+": string")
	}
	if isList {
		params = append(params, "params?: { cursor?: string; limit?: number }")
	}
	params = append(params, fmt.Sprintf("options?: Partial<CreateQueryOptions<%s>>", respType))

	fmt.Fprintf(buf, "export function %s(\n", wrapperName)
	for _, p := range params {
		fmt.Fprintf(buf, "  %s,\n", p)
	}
	buf.WriteString(") {\n")

	// Query key
	if isList {
		var keyArgs []string
		for _, pp := range h.PathParams {
			keyArgs = append(keyArgs, pp.Name)
		}
		buf.WriteString("  return createQuery(() => ({\n")
		if len(keyArgs) > 0 {
			fmt.Fprintf(buf, "    queryKey: [...%sKeys.%s(%s), params] as const,\n", pkgName, funcName, strings.Join(keyArgs, ", "))
		} else {
			fmt.Fprintf(buf, "    queryKey: [...%sKeys.%s(), params] as const,\n", pkgName, funcName)
		}
	} else {
		var keyArgs []string
		for _, pp := range h.PathParams {
			keyArgs = append(keyArgs, pp.Name)
		}
		buf.WriteString("  return createQuery(() => ({\n")
		fmt.Fprintf(buf, "    queryKey: %sKeys.%s(%s),\n", pkgName, funcName, strings.Join(keyArgs, ", "))
	}

	// Query fn
	var callArgs []string
	for _, pp := range h.PathParams {
		callArgs = append(callArgs, pp.Name)
	}
	if isList {
		callArgs = append(callArgs, "params")
	}
	fmt.Fprintf(buf, "    queryFn: () => %s(%s),\n", baseFuncRef, strings.Join(callArgs, ", "))

	buf.WriteString("    ...options,\n")
	buf.WriteString("  }));\n")
	buf.WriteString("}\n")
}

// writeSvelteMutationHook writes a createMutation wrapper for a non-GET handler.
func writeSvelteMutationHook(buf *bytes.Buffer, pkgName string, h codegen.SerializedHandlerInfo) {
	wrapperName := svelteHookName(h)
	baseFuncRef := svelteBaseFuncRef(h)
	role := DetectCRUDRole(h)
	isCRUD := role != CRUDRoleNone

	hasBody := codegen.MethodHasBody(h.Method) && h.Request != nil && len(filterBodyFields(h)) > 0
	hasResponse := h.Response != nil && len(h.Response.Fields) > 0
	hasPathParams := len(h.PathParams) > 0

	respType := "void"
	if hasResponse {
		respType = h.FuncName + "Response"
	}

	// Determine the mutation variable type
	var varsType string
	if hasPathParams && hasBody {
		var parts []string
		for _, pp := range h.PathParams {
			parts = append(parts, pp.Name+": string")
		}
		parts = append(parts, "data: "+h.FuncName+"Request")
		varsType = "{ " + strings.Join(parts, "; ") + " }"
	} else if hasPathParams {
		if len(h.PathParams) == 1 {
			varsType = "string"
		} else {
			var parts []string
			for _, pp := range h.PathParams {
				parts = append(parts, pp.Name+": string")
			}
			varsType = "{ " + strings.Join(parts, "; ") + " }"
		}
	} else if hasBody {
		varsType = h.FuncName + "Request"
	} else {
		varsType = "void"
	}

	buf.WriteString("\n")
	fmt.Fprintf(buf, "export function %s(\n", wrapperName)
	fmt.Fprintf(buf, "  options?: CreateMutationOptions<%s, Error, %s>,\n", respType, varsType)
	buf.WriteString(") {\n")

	if isCRUD {
		buf.WriteString("  const queryClient = useQueryClient();\n")
	}

	buf.WriteString("  return createMutation(() => ({\n")

	// mutationFn
	if hasPathParams && hasBody {
		var destructure []string
		for _, pp := range h.PathParams {
			destructure = append(destructure, pp.Name)
		}
		destructure = append(destructure, "data")
		destructureType := varsType

		var callArgs []string
		for _, pp := range h.PathParams {
			callArgs = append(callArgs, pp.Name)
		}
		callArgs = append(callArgs, "data")

		fmt.Fprintf(buf, "    mutationFn: ({ %s }: %s) => %s(%s),\n",
			strings.Join(destructure, ", "), destructureType, baseFuncRef, strings.Join(callArgs, ", "))
	} else if hasPathParams {
		if len(h.PathParams) == 1 {
			pp := h.PathParams[0]
			fmt.Fprintf(buf, "    mutationFn: (%s: string) => %s(%s),\n", pp.Name, baseFuncRef, pp.Name)
		} else {
			var destructure []string
			var callArgs []string
			for _, pp := range h.PathParams {
				destructure = append(destructure, pp.Name)
				callArgs = append(callArgs, pp.Name)
			}
			fmt.Fprintf(buf, "    mutationFn: ({ %s }: %s) => %s(%s),\n",
				strings.Join(destructure, ", "), varsType, baseFuncRef, strings.Join(callArgs, ", "))
		}
	} else if hasBody {
		fmt.Fprintf(buf, "    mutationFn: (req: %s) => %s(req),\n", varsType, baseFuncRef)
	} else {
		fmt.Fprintf(buf, "    mutationFn: () => %s(),\n", baseFuncRef)
	}

	// Auto-invalidation for CRUD mutations
	if isCRUD {
		buf.WriteString("    onSuccess: () => {\n")
		fmt.Fprintf(buf, "      queryClient.invalidateQueries({ queryKey: %sKeys.all });\n", pkgName)
		buf.WriteString("    },\n")
	}

	buf.WriteString("    ...options,\n")
	buf.WriteString("  }));\n")
	buf.WriteString("}\n")
}
