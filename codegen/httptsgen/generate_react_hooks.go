package httptsgen

import (
	"bytes"
	"fmt"
	"sort"
	"strings"

	"github.com/shipq/shipq/codegen"
	"github.com/shipq/shipq/codegen/tsutil"
)

// GenerateReactHooks generates the React integration layer (react/shipq-api.ts).
// It produces TanStack Query hooks (useQuery/useMutation) for every handler,
// with per-package query key factories and automatic CRUD cache invalidation.
func GenerateReactHooks(handlers []codegen.SerializedHandlerInfo) ([]byte, error) {
	if len(handlers) == 0 {
		return []byte("// No handlers defined.\n"), nil
	}

	var buf bytes.Buffer

	// Header
	buf.WriteString("// Code generated by shipq. DO NOT EDIT.\n\n")

	// Collect all imports needed from the base client
	groups := groupHandlersByPackage(handlers)
	pkgNames := sortedPackageNames(groups)

	// Build import list
	var typeImports []string
	var funcImports []string

	for _, pkgName := range pkgNames {
		pkgHandlers := groups[pkgName]
		sort.Slice(pkgHandlers, func(i, j int) bool {
			return pkgHandlers[i].FuncName < pkgHandlers[j].FuncName
		})
		for _, h := range pkgHandlers {
			funcImports = append(funcImports, tsutil.ToCamelCase(h.FuncName))

			hasBody := codegen.MethodHasBody(h.Method) && h.Request != nil && len(filterBodyFields(h)) > 0
			if hasBody {
				typeImports = append(typeImports, h.FuncName+"Request")
			}

			hasResponse := h.Response != nil && len(h.Response.Fields) > 0
			if hasResponse {
				typeImports = append(typeImports, h.FuncName+"Response")
			}
		}
	}

	// Write TanStack imports
	buf.WriteString("import {\n")
	buf.WriteString("  useQuery,\n")
	buf.WriteString("  useMutation,\n")
	buf.WriteString("  useQueryClient,\n")
	buf.WriteString("  type UseQueryOptions,\n")
	buf.WriteString("  type UseMutationOptions,\n")
	buf.WriteString("} from \"@tanstack/react-query\";\n\n")

	// Write base client imports
	buf.WriteString("import {\n")
	for _, ti := range typeImports {
		fmt.Fprintf(&buf, "  type %s,\n", ti)
	}
	for _, fi := range funcImports {
		fmt.Fprintf(&buf, "  %s,\n", fi)
	}
	buf.WriteString("} from \"../shipq-api\";\n")

	// Generate per-package keys and hooks
	for _, pkgName := range pkgNames {
		pkgHandlers := groups[pkgName]
		sort.Slice(pkgHandlers, func(i, j int) bool {
			return pkgHandlers[i].FuncName < pkgHandlers[j].FuncName
		})

		writeReactQueryKeys(&buf, pkgName, pkgHandlers)
		writeReactHooks(&buf, pkgName, pkgHandlers)
	}

	return buf.Bytes(), nil
}

// writeReactQueryKeys writes the query key factory object for a package.
func writeReactQueryKeys(buf *bytes.Buffer, pkgName string, handlers []codegen.SerializedHandlerInfo) {
	fmt.Fprintf(buf, "\n// ─── Query Keys: %s ───\n\n", pkgName)
	fmt.Fprintf(buf, "export const %sKeys = {\n", pkgName)
	fmt.Fprintf(buf, "  all: [\"%s\"] as const,\n", pkgName)

	for _, h := range handlers {
		camelName := tsutil.ToCamelCase(h.FuncName)
		if len(h.PathParams) > 0 {
			var params []string
			for _, pp := range h.PathParams {
				params = append(params, pp.Name+": string")
			}
			var paramNames []string
			for _, pp := range h.PathParams {
				paramNames = append(paramNames, pp.Name)
			}
			fmt.Fprintf(buf, "  %s: (%s) => [...%sKeys.all, \"%s\", %s] as const,\n",
				camelName, strings.Join(params, ", "), pkgName, camelName, strings.Join(paramNames, ", "))
		} else {
			fmt.Fprintf(buf, "  %s: () => [...%sKeys.all, \"%s\"] as const,\n",
				camelName, pkgName, camelName)
		}
	}

	buf.WriteString("};\n")
}

// writeReactHooks writes the React hooks for a package's handlers.
func writeReactHooks(buf *bytes.Buffer, pkgName string, handlers []codegen.SerializedHandlerInfo) {
	for _, h := range handlers {
		if h.Method == "GET" {
			writeReactQueryHook(buf, pkgName, h)
		} else {
			writeReactMutationHook(buf, pkgName, h)
		}
	}
}

// writeReactQueryHook writes a useQuery hook for a GET handler.
func writeReactQueryHook(buf *bytes.Buffer, pkgName string, h codegen.SerializedHandlerInfo) {
	funcName := tsutil.ToCamelCase(h.FuncName)
	hookName := "use" + h.FuncName
	role := DetectCRUDRole(h)
	isList := role == CRUDRoleList || role == CRUDRoleAdminList

	hasResponse := h.Response != nil && len(h.Response.Fields) > 0
	respType := "void"
	if hasResponse {
		respType = h.FuncName + "Response"
	}

	buf.WriteString("\n")

	// Build function params
	var params []string
	for _, pp := range h.PathParams {
		params = append(params, pp.Name+": string")
	}
	if isList {
		params = append(params, "params?: { cursor?: string; limit?: number }")
	}
	params = append(params, fmt.Sprintf("options?: Partial<UseQueryOptions<%s>>", respType))

	fmt.Fprintf(buf, "export function %s(\n", hookName)
	for _, p := range params {
		fmt.Fprintf(buf, "  %s,\n", p)
	}
	buf.WriteString(") {\n")

	// Query key
	if isList {
		var keyArgs []string
		for _, pp := range h.PathParams {
			keyArgs = append(keyArgs, pp.Name)
		}
		if len(keyArgs) > 0 {
			fmt.Fprintf(buf, "  return useQuery({\n")
			fmt.Fprintf(buf, "    queryKey: [...%sKeys.%s(%s), params] as const,\n", pkgName, funcName, strings.Join(keyArgs, ", "))
		} else {
			fmt.Fprintf(buf, "  return useQuery({\n")
			fmt.Fprintf(buf, "    queryKey: [...%sKeys.%s(), params] as const,\n", pkgName, funcName)
		}
	} else {
		var keyArgs []string
		for _, pp := range h.PathParams {
			keyArgs = append(keyArgs, pp.Name)
		}
		fmt.Fprintf(buf, "  return useQuery({\n")
		fmt.Fprintf(buf, "    queryKey: %sKeys.%s(%s),\n", pkgName, funcName, strings.Join(keyArgs, ", "))
	}

	// Query fn
	var callArgs []string
	for _, pp := range h.PathParams {
		callArgs = append(callArgs, pp.Name)
	}
	if isList {
		callArgs = append(callArgs, "params")
	}
	fmt.Fprintf(buf, "    queryFn: () => %s(%s),\n", funcName, strings.Join(callArgs, ", "))

	buf.WriteString("    ...options,\n")
	buf.WriteString("  });\n")
	buf.WriteString("}\n")
}

// writeReactMutationHook writes a useMutation hook for a non-GET handler.
func writeReactMutationHook(buf *bytes.Buffer, pkgName string, h codegen.SerializedHandlerInfo) {
	hookName := "use" + h.FuncName
	role := DetectCRUDRole(h)
	isCRUD := role != CRUDRoleNone

	hasBody := codegen.MethodHasBody(h.Method) && h.Request != nil && len(filterBodyFields(h)) > 0
	hasResponse := h.Response != nil && len(h.Response.Fields) > 0
	hasPathParams := len(h.PathParams) > 0

	respType := "void"
	if hasResponse {
		respType = h.FuncName + "Response"
	}

	// Determine the mutation variable type
	var varsType string
	if hasPathParams && hasBody {
		// Combine path params and body into one object
		var parts []string
		for _, pp := range h.PathParams {
			parts = append(parts, pp.Name+": string")
		}
		parts = append(parts, "data: "+h.FuncName+"Request")
		varsType = "{ " + strings.Join(parts, "; ") + " }"
	} else if hasPathParams {
		if len(h.PathParams) == 1 {
			varsType = "string"
		} else {
			var parts []string
			for _, pp := range h.PathParams {
				parts = append(parts, pp.Name+": string")
			}
			varsType = "{ " + strings.Join(parts, "; ") + " }"
		}
	} else if hasBody {
		varsType = h.FuncName + "Request"
	} else {
		varsType = "void"
	}

	buf.WriteString("\n")
	fmt.Fprintf(buf, "export function %s(\n", hookName)
	fmt.Fprintf(buf, "  options?: UseMutationOptions<%s, Error, %s>,\n", respType, varsType)
	buf.WriteString(") {\n")

	if isCRUD {
		buf.WriteString("  const queryClient = useQueryClient();\n")
	}

	buf.WriteString("  return useMutation({\n")

	// mutationFn
	if hasPathParams && hasBody {
		var destructure []string
		for _, pp := range h.PathParams {
			destructure = append(destructure, pp.Name)
		}
		destructure = append(destructure, "data")
		destructureType := varsType

		var callArgs []string
		for _, pp := range h.PathParams {
			callArgs = append(callArgs, pp.Name)
		}
		callArgs = append(callArgs, "data")

		funcName := tsutil.ToCamelCase(h.FuncName)
		fmt.Fprintf(buf, "    mutationFn: ({ %s }: %s) => %s(%s),\n",
			strings.Join(destructure, ", "), destructureType, funcName, strings.Join(callArgs, ", "))
	} else if hasPathParams {
		funcName := tsutil.ToCamelCase(h.FuncName)
		if len(h.PathParams) == 1 {
			pp := h.PathParams[0]
			fmt.Fprintf(buf, "    mutationFn: (%s: string) => %s(%s),\n", pp.Name, funcName, pp.Name)
		} else {
			var destructure []string
			var callArgs []string
			for _, pp := range h.PathParams {
				destructure = append(destructure, pp.Name)
				callArgs = append(callArgs, pp.Name)
			}
			fmt.Fprintf(buf, "    mutationFn: ({ %s }: %s) => %s(%s),\n",
				strings.Join(destructure, ", "), varsType, funcName, strings.Join(callArgs, ", "))
		}
	} else if hasBody {
		funcName := tsutil.ToCamelCase(h.FuncName)
		fmt.Fprintf(buf, "    mutationFn: (req: %s) => %s(req),\n", varsType, funcName)
	} else {
		funcName := tsutil.ToCamelCase(h.FuncName)
		fmt.Fprintf(buf, "    mutationFn: () => %s(),\n", funcName)
	}

	// Auto-invalidation for CRUD mutations
	if isCRUD {
		buf.WriteString("    onSuccess: () => {\n")
		fmt.Fprintf(buf, "      queryClient.invalidateQueries({ queryKey: %sKeys.all });\n", pkgName)
		buf.WriteString("    },\n")
	}

	buf.WriteString("    ...options,\n")
	buf.WriteString("  });\n")
	buf.WriteString("}\n")
}
