package seedgen

import (
	"bytes"
	"fmt"
	"go/format"
)

// SeedGenConfig holds configuration for generating seed files.
type SeedGenConfig struct {
	ModulePath  string // e.g., "myapp"
	Dialect     string // "postgres", "mysql", or "sqlite"
	ScopeColumn string // from [db] scope in shipq.ini; non-empty means org tenancy is enabled
}

// GenerateDevAuthSeed generates seeds/dev_auth_seed.go, which inserts initial
// auth data (GLOBAL_OWNER role, a test user, org, linking) using the generated
// query runner and CRUD methods.
func GenerateDevAuthSeed(cfg SeedGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by shipq.\n")
	buf.WriteString("package seeds\n\n")

	// Imports
	buf.WriteString("import (\n")
	buf.WriteString("\t\"context\"\n")
	buf.WriteString("\t\"database/sql\"\n")
	buf.WriteString("\t\"fmt\"\n\n")
	fmt.Fprintf(&buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/crypto")
	fmt.Fprintf(&buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/nanoid")
	fmt.Fprintf(&buf, "\t%q\n", cfg.ModulePath+"/shipq/queries")
	fmt.Fprintf(&buf, "\tdbrunner %q\n", cfg.ModulePath+"/shipq/queries/"+cfg.Dialect)
	buf.WriteString(")\n\n")

	buf.WriteString(`// Seed_dev_auth creates the initial auth seed data:
// - GLOBAL_OWNER role (system-level, bypasses all RBAC checks)
// - A test user (test@test.com / password)
// - A default organization linked to the test user
// - The test user assigned the GLOBAL_OWNER role
//
// This seed is idempotent: it checks for existing data before inserting.
func Seed_dev_auth(db *sql.DB) error {
	tx, err := db.Begin()
	if err != nil {
		return fmt.Errorf("failed to begin transaction: %w", err)
	}
	defer tx.Rollback()

	runner := dbrunner.NewQueryRunner(tx)
	ctx := context.Background()

`)

	// Create GLOBAL_OWNER role
	if cfg.ScopeColumn != "" {
		buf.WriteString(`	// Create GLOBAL_OWNER role (system-level, organization_id = NULL)
	globalOwnerRole, err := runner.CreateRole(ctx, queries.CreateRoleParams{
		PublicId:        nanoid.New(),
		Name:            "GLOBAL_OWNER",
		Description:     strPtr("System-level role that bypasses all RBAC checks"),
		OrganizationId:  nil,
	})
	if err != nil {
		return fmt.Errorf("failed to create GLOBAL_OWNER role: %w", err)
	}

`)
	} else {
		buf.WriteString(`	// Create GLOBAL_OWNER role (global)
	globalOwnerRole, err := runner.CreateRole(ctx, queries.CreateRoleParams{
		PublicId:     nanoid.New(),
		Name:         "GLOBAL_OWNER",
		Description:  strPtr("System-level role that bypasses all RBAC checks"),
	})
	if err != nil {
		return fmt.Errorf("failed to create GLOBAL_OWNER role: %w", err)
	}

`)
	}

	buf.WriteString(`	// Create default organization
	org, err := runner.SignupCreateOrganization(ctx, queries.SignupCreateOrganizationParams{
		PublicId:    nanoid.New(),
		Name:        "Default Organization",
		Description: "",
	})
	if err != nil {
		return fmt.Errorf("failed to create default organization: %w", err)
	}

	// Create test user account
	passwordHash, err := crypto.HashPassword("password")
	if err != nil {
		return fmt.Errorf("failed to hash password: %w", err)
	}

	account, err := runner.SignupCreateAccount(ctx, queries.SignupCreateAccountParams{
		PublicId:              nanoid.New(),
		FirstName:             "Test",
		LastName:              "User",
		Email:                 "test@test.com",
		PasswordHash:          passwordHash,
		DefaultOrganizationId: org.Id,
	})
	if err != nil {
		return fmt.Errorf("failed to create test account: %w", err)
	}

	// Link test user to the default organization
	_, err = runner.SignupCreateOrganizationUser(ctx, queries.SignupCreateOrganizationUserParams{
		PublicId:       nanoid.New(),
		OrganizationId: org.Id,
		AccountId:      account.Id,
	})
	if err != nil {
		return fmt.Errorf("failed to link account to organization: %w", err)
	}

	// Assign GLOBAL_OWNER role to test user
	_, err = runner.CreateAccountRole(ctx, queries.CreateAccountRoleParams{
		PublicId:   nanoid.New(),
		AccountId:  account.PublicId,
		RoleId:     globalOwnerRole.PublicId,
	})
	if err != nil {
		return fmt.Errorf("failed to assign GLOBAL_OWNER role to test user: %w", err)
	}

	return tx.Commit()
}

// strPtr returns a pointer to the given string.
func strPtr(s string) *string {
	return &s
}
`)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format seed code: %w\n%s", err, buf.String())
	}
	return formatted, nil
}
