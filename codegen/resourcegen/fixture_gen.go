package resourcegen

import (
	"bytes"
	"fmt"
	"go/format"

	"github.com/shipq/shipq/db/portsql/ddl"
	"github.com/shipq/shipq/dbstrings"
)

// FixtureGenConfig holds configuration for generating a fixture package.
type FixtureGenConfig struct {
	ModulePath  string               // e.g., "myapp"
	TableName   string               // e.g., "books"
	Table       ddl.Table            // The table definition
	Schema      map[string]ddl.Table // Full schema for FK detection
	Dialect     string               // "postgres", "mysql", or "sqlite"
	ScopeColumn string               // e.g., "organization_id" (empty if unscoped)
}

// GenerateFixture generates a fixture.go file for a resource.
// The fixture creates a valid resource via the query runner (direct DB insert),
// automatically creating any required dependencies.
func GenerateFixture(cfg FixtureGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	singular := dbstrings.ToSingular(cfg.TableName)
	pascal := dbstrings.ToPascalCase(singular)

	// Collect FK dependencies
	type fkDep struct {
		ColName   string // e.g., "author_id"
		Table     string // e.g., "authors"
		Nullable  bool
		FieldName string // PascalCase field name, e.g., "AuthorId"
	}

	var deps []fkDep
	for _, col := range cfg.Table.Columns {
		if col.References != "" && !isFixtureAutoColumn(col.Name) {
			deps = append(deps, fkDep{
				ColName:   col.Name,
				Table:     col.References,
				Nullable:  col.Nullable,
				FieldName: dbstrings.ToPascalCase(col.Name),
			})
		}
	}

	// Detect whether the table has author_account_id
	hasAuthor := false
	for _, col := range cfg.Table.Columns {
		if col.Name == "author_account_id" {
			hasAuthor = true
			break
		}
	}

	buf.WriteString("// Code generated by shipq.\n")
	buf.WriteString("package fixture\n\n")

	// Imports
	buf.WriteString("import (\n")
	buf.WriteString("\t\"context\"\n")
	buf.WriteString("\t\"database/sql\"\n")
	buf.WriteString("\t\"testing\"\n\n")

	// Nanoid for public_id generation
	hasPublicID := false
	for _, col := range cfg.Table.Columns {
		if col.Name == "public_id" {
			hasPublicID = true
			break
		}
	}
	if hasPublicID {
		fmt.Fprintf(&buf, "\t%q\n\n", cfg.ModulePath+"/shipq/lib/nanoid")
	}

	// Query runner packages
	fmt.Fprintf(&buf, "\t%q\n", cfg.ModulePath+"/shipq/queries")
	fmt.Fprintf(&buf, "\tdbrunner %q\n", cfg.ModulePath+"/shipq/queries/"+cfg.Dialect)

	// Import dependency fixtures
	// Track whether we already imported the accounts fixture (to avoid
	// duplicating it when hasAuthor is also true).
	accountsImported := false
	for _, dep := range deps {
		if !dep.Nullable {
			if dep.Table == "accounts" && hasAuthor {
				// Will be imported by the hasAuthor block below — skip to
				// avoid a duplicate import.
				accountsImported = true
				continue
			}
			depFixturePkg := cfg.ModulePath + "/api/" + dep.Table + "/fixture"
			depAlias := dbstrings.ToSingular(dep.Table) + "fixture"
			fmt.Fprintf(&buf, "\t%s %q\n", depAlias, depFixturePkg)
		}
	}

	// Import accounts fixture when table has author_account_id
	if hasAuthor {
		accountFixturePkg := cfg.ModulePath + "/api/accounts/fixture"
		fmt.Fprintf(&buf, "\taccountfixture %q\n", accountFixturePkg)
		accountsImported = true
	}
	_ = accountsImported // used only as a guard above

	buf.WriteString(")\n\n")

	// Create function
	fmt.Fprintf(&buf, "// Create creates a valid %s via direct DB insert and returns the result.\n", singular)
	buf.WriteString("// It automatically creates any required dependencies.\n")
	fmt.Fprintf(&buf, "func Create(t *testing.T, ctx context.Context, tx *sql.Tx) *queries.Create%sResult {\n", pascal)
	buf.WriteString("\tt.Helper()\n\n")
	buf.WriteString("\trunner := dbrunner.NewQueryRunner(tx)\n\n")

	// Create non-nullable FK dependencies.
	// Skip any dep that references "accounts" when hasAuthor is true,
	// because the hasAuthor block below will emit the single declaration.
	hasNonNullDep := false
	for _, dep := range deps {
		if !dep.Nullable {
			if dep.Table == "accounts" && hasAuthor {
				continue
			}
			hasNonNullDep = true
			depSingular := dbstrings.ToSingular(dep.Table)
			depAlias := depSingular + "fixture"
			fmt.Fprintf(&buf, "\t%s := %s.Create(t, ctx, tx)\n", depSingular, depAlias)
		}
	}
	if hasNonNullDep {
		buf.WriteString("\n")
	}

	// Create account dependency for author_account_id (also reused by
	// any account_id FK that references accounts).
	if hasAuthor {
		buf.WriteString("\taccount := accountfixture.Create(t, ctx, tx)\n\n")
	}

	// Build create request via runner
	fmt.Fprintf(&buf, "\tresult, err := runner.Create%s(ctx, queries.Create%sParams{\n", pascal, pascal)
	if hasPublicID {
		buf.WriteString("\t\tPublicId: nanoid.New(),\n")
	}
	if hasAuthor {
		buf.WriteString("\t\tAuthorAccountId: account.Id,\n")
	}

	for _, col := range cfg.Table.Columns {
		if isFixtureAutoColumn(col.Name) || col.Nullable {
			continue
		}
		fieldName := dbstrings.ToPascalCase(col.Name)

		// Check if this is an FK column
		if col.References != "" {
			depSingular := dbstrings.ToSingular(col.References)
			// Scope columns use the internal ID (int64), not the public_id (string),
			// because the INSERT SQL uses a direct placeholder for scope columns.
			if cfg.ScopeColumn != "" && col.Name == cfg.ScopeColumn {
				fmt.Fprintf(&buf, "\t\t%s: %s.Id,\n", fieldName, depSingular)
			} else {
				fmt.Fprintf(&buf, "\t\t%s: %s.PublicId,\n", fieldName, depSingular)
			}
		} else {
			sampleVal := getSampleValue(goBaseTypeForFixture(col.Type), col.Name)
			fmt.Fprintf(&buf, "\t\t%s: %s,\n", fieldName, sampleVal)
		}
	}

	buf.WriteString("\t})\n")
	buf.WriteString("\tif err != nil {\n")
	fmt.Fprintf(&buf, "\t\tt.Fatalf(\"fixture: failed to create %s: %%v\", err)\n", singular)
	buf.WriteString("\t}\n")
	buf.WriteString("\treturn result\n")
	buf.WriteString("}\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format fixture code: %w\nunformatted:\n%s", err, buf.String())
	}
	return formatted, nil
}

func isFixtureAutoColumn(name string) bool {
	switch name {
	case "id", "public_id", "created_at", "updated_at", "deleted_at", "author_account_id":
		return true
	default:
		return false
	}
}

func goBaseTypeForFixture(colType string) string {
	switch colType {
	case ddl.IntegerType:
		return "int32"
	case ddl.BigintType:
		return "int64"
	case ddl.DecimalType, ddl.FloatType:
		return "float64"
	case ddl.BooleanType:
		return "bool"
	default:
		return "string"
	}
}
