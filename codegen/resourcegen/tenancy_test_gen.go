package resourcegen

import (
	"bytes"
	"fmt"
	"go/format"

	"github.com/shipq/shipq/dbstrings"
)

// TenancyTestGenConfig holds configuration for generating tenancy isolation tests.
type TenancyTestGenConfig struct {
	ModulePath      string // e.g., "myapp"
	OutputPkg       string // e.g., "api"
	Dialect         string // "postgres", "mysql", or "sqlite"
	TestDatabaseURL string // test database URL
	ScopeColumn     string // e.g., "organization_id"
}

// GenerateTenancyTest generates an E2E tenancy isolation test file for a scoped resource.
// The test creates two users in different organizations and verifies that
// CRUD operations are scoped to each user's organization.
func GenerateTenancyTest(cfg TenancyTestGenConfig, resource ResourceInfo) ([]byte, error) {
	if !resource.IsFullResource() {
		return nil, fmt.Errorf("resource %q is not a full resource (missing CRUD operations)", resource.PackagePath)
	}

	var buf bytes.Buffer
	singular := dbstrings.ToSingular(resource.PackageName)
	pluralPascal := dbstrings.ToPascalCase(resource.PackageName)
	pkgName := resource.PackageName

	buf.WriteString("// Code generated by shipq.\n")
	buf.WriteString("package spec\n\n")

	// Generate imports
	generateTenancyTestImports(&buf, cfg, resource)

	// Main test function
	buf.WriteString(fmt.Sprintf("func TestTenancyIsolation_%s(t *testing.T) {\n", pluralPascal))

	// --- Setup: single test server, two users in different orgs ---
	buf.WriteString("\t// Create a single test server with two users in distinct organizations.\n")
	buf.WriteString("\t// Using one server/transaction avoids SQLite SQLITE_BUSY errors.\n")
	buf.WriteString("\tts := api.NewUnauthenticatedTestServer(t, testDB)\n")
	buf.WriteString("\tcookieA := createTestUser(t, ts, \"user_a@test.com\", \"password123\", \"UserA\", \"Test\")\n")
	buf.WriteString("\tclientA := api.NewAuthenticatedTestClient(ts.Server, cookieA)\n")
	buf.WriteString("\tctxA := context.Background()\n\n")

	buf.WriteString("\tcookieB := createTestUser(t, ts, \"user_b@test.com\", \"password456\", \"UserB\", \"Test\")\n")
	buf.WriteString("\tclientB := api.NewAuthenticatedTestClient(ts.Server, cookieB)\n")
	buf.WriteString("\tctxB := context.Background()\n\n")

	// --- Test: Create scoped to org ---
	buf.WriteString("\t// --- Test: Create resource is scoped to user's org ---\n")

	// Build a create request with sample values for required fields
	createFn := resource.CreateHandler.FuncName
	createReqType := fmt.Sprintf("%s.%s", pkgName, resource.CreateHandler.Request.Name)

	buf.WriteString(fmt.Sprintf("\tcreated, err := clientA.%s(ctxA, %s{\n", createFn, createReqType))
	if resource.CreateHandler.Request != nil {
		for _, field := range resource.CreateHandler.Request.Fields {
			if field.Required && !isIDField(field.JSONName) {
				sampleValue := getSampleValue(field.Type, field.Name)
				fmt.Fprintf(&buf, "\t\t%s: %s,\n", field.Name, sampleValue)
			}
		}
	}
	buf.WriteString("\t})\n")
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString(fmt.Sprintf("\t\tt.Fatalf(\"User A: %s failed: %%v\", err)\n", createFn))
	buf.WriteString("\t}\n\n")

	// Extract the resource ID
	idJSONName := GetResourceIDJSONName(resource)
	responseIDField := findResponseIDField(resource.CreateHandler.Response, idJSONName)
	if responseIDField == "" {
		responseIDField = "PublicId"
	}
	buf.WriteString(fmt.Sprintf("\tresourceID := created.%s\n", responseIDField))
	buf.WriteString("\tif resourceID == \"\" {\n")
	buf.WriteString("\t\tt.Fatal(\"expected non-empty resource ID\")\n")
	buf.WriteString("\t}\n\n")

	// --- Test: List only shows own org ---
	listFn := resource.ListHandler.FuncName
	listReqType := fmt.Sprintf("%s.%s", pkgName, resource.ListHandler.Request.Name)

	buf.WriteString("\t// --- Test: List only shows resources from same org ---\n")
	buf.WriteString(fmt.Sprintf("\tlistA, err := clientA.%s(ctxA, %s{})\n", listFn, listReqType))
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString(fmt.Sprintf("\t\tt.Fatalf(\"User A: %s failed: %%v\", err)\n", listFn))
	buf.WriteString("\t}\n")
	buf.WriteString("\tif len(listA.Items) != 1 {\n")
	buf.WriteString("\t\tt.Errorf(\"User A: expected 1 item, got %d\", len(listA.Items))\n")
	buf.WriteString("\t}\n\n")

	buf.WriteString(fmt.Sprintf("\tlistB, err := clientB.%s(ctxB, %s{})\n", listFn, listReqType))
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString(fmt.Sprintf("\t\tt.Fatalf(\"User B: %s failed: %%v\", err)\n", listFn))
	buf.WriteString("\t}\n")
	buf.WriteString("\tif len(listB.Items) != 0 {\n")
	buf.WriteString("\t\tt.Errorf(\"User B: expected 0 items (different org), got %d\", len(listB.Items))\n")
	buf.WriteString("\t}\n\n")

	// --- Test: Get cross-org returns not found ---
	getOneFn := resource.GetOneHandler.FuncName
	getOneReqType := fmt.Sprintf("%s.%s", pkgName, resource.GetOneHandler.Request.Name)
	getIDField := GetResourceIDField(resource)

	buf.WriteString("\t// --- Test: Get cross-org returns not found ---\n")
	buf.WriteString(fmt.Sprintf("\t_, getErr := clientB.%s(ctxB, %s{%s: resourceID})\n", getOneFn, getOneReqType, getIDField))
	buf.WriteString("\tif getErr == nil {\n")
	buf.WriteString(fmt.Sprintf("\t\tt.Error(\"User B should NOT be able to GET User A's %s (different org)\")\n", singular))
	buf.WriteString("\t}\n\n")

	// --- Test: Delete cross-org fails ---
	deleteFn := resource.DeleteHandler.FuncName
	deleteReqType := fmt.Sprintf("%s.%s", pkgName, resource.DeleteHandler.Request.Name)

	buf.WriteString("\t// --- Test: Delete cross-org fails ---\n")
	buf.WriteString(fmt.Sprintf("\t_, delErr := clientB.%s(ctxB, %s{%s: resourceID})\n", deleteFn, deleteReqType, getIDField))
	buf.WriteString("\tif delErr == nil {\n")
	buf.WriteString("\t\t// Verify the resource still exists for User A\n")
	buf.WriteString(fmt.Sprintf("\t\t_, stillErr := clientA.%s(ctxA, %s{%s: resourceID})\n", getOneFn, getOneReqType, getIDField))
	buf.WriteString("\t\tif stillErr != nil {\n")
	buf.WriteString(fmt.Sprintf("\t\t\tt.Error(\"User B was able to delete User A's %s (cross-org violation)\")\n", singular))
	buf.WriteString("\t\t}\n")
	buf.WriteString("\t}\n\n")

	// --- Verify User A can still access own resource ---
	buf.WriteString(fmt.Sprintf("\t// --- Verify: User A can still access own %s ---\n", singular))
	buf.WriteString(fmt.Sprintf("\townResp, err := clientA.%s(ctxA, %s{%s: resourceID})\n", getOneFn, getOneReqType, getIDField))
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString(fmt.Sprintf("\t\tt.Fatalf(\"User A should still access own %s: %%v\", err)\n", singular))
	buf.WriteString("\t}\n")
	buf.WriteString(fmt.Sprintf("\tif ownResp.%s != resourceID {\n", responseIDField))
	buf.WriteString(fmt.Sprintf("\t\tt.Errorf(\"ID mismatch: got %%q, want %%q\", ownResp.%s, resourceID)\n", responseIDField))
	buf.WriteString("\t}\n")

	buf.WriteString("}\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format tenancy test code: %w\nunformatted:\n%s", err, buf.String())
	}

	return formatted, nil
}

// generateTenancyTestImports writes the import block for tenancy tests.
func generateTenancyTestImports(buf *bytes.Buffer, cfg TenancyTestGenConfig, resource ResourceInfo) {
	buf.WriteString("import (\n")
	buf.WriteString("\t\"context\"\n")
	buf.WriteString("\t\"testing\"\n")
	buf.WriteString("\n")

	// API package import
	apiPkg := cfg.ModulePath + "/" + cfg.OutputPkg
	fmt.Fprintf(buf, "\t%q\n", apiPkg)

	// Resource package (for request types)
	fmt.Fprintf(buf, "\t%q\n", resource.PackagePath)

	buf.WriteString(")\n\n")
}
