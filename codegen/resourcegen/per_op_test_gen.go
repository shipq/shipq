// GenerateUpdateTest generates update_test.go for a resource.
func GenerateUpdateTest(cfg PerOpTestGenConfig) ([]byte, error) {
	var buf bytes.Buffer
	res := dbstrings.ToPascalCase(dbstrings.ToSingular(cfg.TableName))
	pkgName := cfg.TableName

	buf.WriteString("// Code generated by shipq.\n")
	buf.WriteString("package spec\n\n")

	writeUpdateTestImports(&buf, cfg)

	// Collect updatable columns (non-auto, non-nullable).
	// The generated SQL sets ALL columns in the UPDATE, so we must supply
	// values for every NOT NULL column to avoid constraint violations.
	type updCol struct {
		Name    string
		Pascal  string
		GoType  string
		IsFK    bool
		FKTable string // e.g., "authors"
	}
	var updCols []updCol
	updateField := ""
	for _, col := range cfg.Table.Columns {
		if isFixtureAutoColumn(col.Name) || col.Nullable {
			continue
		}
		// Scope column is not updatable (excluded from update request)
		if cfg.ScopeColumn != "" && col.Name == cfg.ScopeColumn {
			continue
		}
		pascal := dbstrings.ToPascalCase(col.Name)
		if col.References != "" {
			updCols = append(updCols, updCol{Name: col.Name, Pascal: pascal, GoType: "string", IsFK: true, FKTable: col.References})
		} else {
			goType := goBaseTypeForFixture(col.Type)
			updCols = append(updCols, updCol{Name: col.Name, Pascal: pascal, GoType: goType})
			if updateField == "" && goType == "string" {
				updateField = col.Name
			}
		}
	}

	// TestUpdate_Success
	buf.WriteString(fmt.Sprintf("func TestUpdate%s_Success(t *testing.T) {\n", res))
	writeTestSetup(&buf, cfg)
	buf.WriteString("\n")
	if cfg.ScopeColumn != "" {
		writeCreateDeps(&buf, cfg)
		writeScopedCreateHelper(&buf, cfg)
		buf.WriteString("\tcreated := createResource()\n\n")
	} else {
		buf.WriteString("\tcreated := fixture.Create(t, ctx, tx)\n\n")
	}

	// Emit updated value for the target field
	if updateField != "" {
		buf.WriteString(fmt.Sprintf("\tupdatedVal := \"updated_%s\"\n", updateField))
	}

	// Build the update request with ALL updatable fields.
	// For non-target, non-FK columns we use sample values because
	// CreateResult only has Id/PublicId — we cannot reference created.<Field>.
	// For FK columns, create the dependency via its fixture to get a valid
	// public_id (since the create response returns the internal integer).
	for _, uc := range updCols {
		if uc.IsFK {
			depSingular := dbstrings.ToSingular(uc.FKTable)
			depAlias := depSingular + "fixture"
			buf.WriteString(fmt.Sprintf("\t%sForUpdate := %s.Create(t, ctx, tx)\n", depSingular, depAlias))
		} else if uc.Name != updateField {
			sampleVal := getSampleValue(uc.GoType, uc.Name)
			varName := "keep" + uc.Pascal
			buf.WriteString(fmt.Sprintf("\t%s := %s\n", varName, sampleVal))
		}
	}

	buf.WriteString(fmt.Sprintf("\tupdated, err := client.Update%s(ctx, %s.Update%sRequest{\n", res, pkgName, res))
	buf.WriteString("\t\tID: created.PublicId,\n")
	for _, uc := range updCols {
		if uc.Name == updateField {
			buf.WriteString(fmt.Sprintf("\t\t%s: &updatedVal,\n", uc.Pascal))
		} else if uc.IsFK {
			depSingular := dbstrings.ToSingular(uc.FKTable)
			buf.WriteString(fmt.Sprintf("\t\t%s: &%sForUpdate.PublicId,\n", uc.Pascal, depSingular))
		} else {
			// Use a sample value — CreateResult only has Id/PublicId,
			// so we cannot reference created.<Field>.
			varName := "keep" + uc.Pascal
			buf.WriteString(fmt.Sprintf("\t\t%s: &%s,\n", uc.Pascal, varName))
		}
	}
	buf.WriteString("\t})\n")

	buf.WriteString("\tif err != nil {\n")
	buf.WriteString(fmt.Sprintf("\t\tt.Fatalf(\"Update%s failed: %%v\", err)\n", res))
	buf.WriteString("\t}\n")

	if updateField != "" {
		fieldPascal := dbstrings.ToPascalCase(updateField)
		buf.WriteString(fmt.Sprintf("\tif updated.%s != updatedVal {\n", fieldPascal))
		buf.WriteString(fmt.Sprintf("\t\tt.Errorf(\"%s mismatch: got %%q, want %%q\", updated.%s, updatedVal)\n", fieldPascal, fieldPascal))
		buf.WriteString("\t}\n")
	} else {
		buf.WriteString("\t_ = updated\n")
	}
	buf.WriteString("}\n\n")

	// TestUpdate_NotFound
	buf.WriteString(fmt.Sprintf("func TestUpdate%s_NotFound(t *testing.T) {\n", res))
	writeTestSetup(&buf, cfg)
	buf.WriteString("\n")
	buf.WriteString(fmt.Sprintf("\t_, updateErr := client.Update%s(ctx, %s.Update%sRequest{ID: \"nonexistent\"})\n", res, pkgName, res))
	buf.WriteString("\tif updateErr == nil {\n")
	buf.WriteString("\t\tt.Error(\"expected error for nonexistent resource\")\n")
	buf.WriteString("\t}\n")
	buf.WriteString("}\n\n")

	// TestUpdate_Unauthenticated (if auth required)
	if cfg.RequireAuth {
		buf.WriteString(fmt.Sprintf("func TestUpdate%s_Unauthenticated(t *testing.T) {\n", res))
		writeUnauthTestSetup(&buf, cfg)
		buf.WriteString(fmt.Sprintf("\t_, updateErr := unauthClient.Update%s(ctx, %s.Update%sRequest{ID: \"any\"})\n", res, pkgName, res))
		buf.WriteString("\tif updateErr == nil {\n")
		buf.WriteString("\t\tt.Error(\"expected error for unauthenticated request\")\n")
		buf.WriteString("\t}\n")
		buf.WriteString("}\n\n")
	}

	return formatSource(buf.Bytes())
}

