func testNeedsJSONImport(table ddl.Table) bool {
	for _, col := range table.Columns {
		if col.Type == ddl.JSONType && !isFixtureAutoColumn(col.Name) {
			return true
		}
	}
	return false
}

// PerOpTestGenConfig holds configuration for generating per-operation test files.
type PerOpTestGenConfig struct {
	ModulePath      string               // e.g., "myapp"
	TableName       string               // e.g., "books"
	Table           ddl.Table            // The table definition
	Schema          map[string]ddl.Table // Full schema for FK detection
	RequireAuth     bool                 // true if handlers require authentication
	Dialect         string               // "postgres", "mysql", or "sqlite"
	TestDatabaseURL string               // test database URL
	ScopeColumn     string               // e.g., "organization_id" (empty if unscoped)
}

// GenerateCreateTest generates create_test.go for a resource.
// The create test calls the route directly (not via fixture).
func GenerateCreateTest(cfg PerOpTestGenConfig) ([]byte, error) {
	var buf bytes.Buffer
	res := dbstrings.ToPascalCase(dbstrings.ToSingular(cfg.TableName))
	pkgName := cfg.TableName

	buf.WriteString("// Code generated by shipq.\n")
	buf.WriteString("package spec\n\n")

	// Imports -- create test needs resource pkg + any FK dep fixtures
	writeCreateTestImports(&buf, cfg)

	// TestCreate_Success -- call the route directly
	buf.WriteString(fmt.Sprintf("func TestCreate%s_Success(t *testing.T) {\n", res))
	writeTestSetup(&buf, cfg)
	buf.WriteString("\n")

	// Create non-nullable FK dependencies via their fixtures
	// (skip scope column -- the auth middleware provides it from session context)
	for _, col := range cfg.Table.Columns {
		if col.References == "" || col.Nullable || isFixtureAutoColumn(col.Name) {
			continue
		}
		if cfg.ScopeColumn != "" && col.Name == cfg.ScopeColumn {
			continue
		}
		depSingular := dbstrings.ToSingular(col.References)
		depAlias := depSingular + "fixture"
		buf.WriteString(fmt.Sprintf("\t%s := %s.Create(t, ctx, tx)\n", depSingular, depAlias))
	}

	// Build the create request inline with sample values
	buf.WriteString(fmt.Sprintf("\tresp, err := client.Create%s(ctx, %s.Create%sRequest{\n", res, pkgName, res))
	for _, col := range cfg.Table.Columns {
		if isFixtureAutoColumn(col.Name) || col.Nullable {
			continue
		}
		// Scope column is injected by the handler from auth context, not from the request
		if cfg.ScopeColumn != "" && col.Name == cfg.ScopeColumn {
			continue
		}
		fieldName := dbstrings.ToPascalCase(col.Name)
		if col.References != "" {
			depSingular := dbstrings.ToSingular(col.References)
			buf.WriteString(fmt.Sprintf("\t\t%s: %s.PublicId,\n", fieldName, depSingular))
		} else {
			sampleVal := getSampleValue(goBaseTypeForFixture(col.Type), col.Name)
			buf.WriteString(fmt.Sprintf("\t\t%s: %s,\n", fieldName, sampleVal))
		}
	}
	buf.WriteString("\t})\n")
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString(fmt.Sprintf("\t\tt.Fatalf(\"Create%s failed: %%v\", err)\n", res))
	buf.WriteString("\t}\n")
	buf.WriteString("\tif resp.PublicId == \"\" {\n")
	buf.WriteString("\t\tt.Fatal(\"expected non-empty public_id\")\n")
	buf.WriteString("\t}\n")
	buf.WriteString("}\n\n")

	// Nullable FK tests: with and without optional dep
	for _, col := range cfg.Table.Columns {
		if col.References == "" || !col.Nullable {
			continue
		}
		depRes := dbstrings.ToPascalCase(dbstrings.ToSingular(col.References))
		fieldName := dbstrings.ToPascalCase(col.Name)

		// TestCreate_WithOptionalDep
		buf.WriteString(fmt.Sprintf("func TestCreate%s_WithOptional%s(t *testing.T) {\n", res, depRes))
		writeTestSetup(&buf, cfg)
		buf.WriteString("\n")
		// Create the optional dep via fixture
		depSingular := dbstrings.ToSingular(col.References)
		depAlias := depSingular + "fixture"
		buf.WriteString(fmt.Sprintf("\t%s := %s.Create(t, ctx, tx)\n\n", depSingular, depAlias))

		writeCreateRequestWithOptionalFK(&buf, cfg, res, pkgName, col, true)
		buf.WriteString(fmt.Sprintf("\tresp, err := client.Create%s(ctx, req)\n", res))
		buf.WriteString("\tif err != nil {\n")
		buf.WriteString(fmt.Sprintf("\t\tt.Fatalf(\"Create%s with optional %s failed: %%v\", err)\n", res, depRes))
		buf.WriteString("\t}\n")
		buf.WriteString(fmt.Sprintf("\tif resp.%s == nil {\n", fieldName))
		buf.WriteString(fmt.Sprintf("\t\tt.Error(\"expected %s to be set\")\n", fieldName))
		buf.WriteString("\t}\n")
		buf.WriteString("}\n\n")

		// TestCreate_WithoutOptionalDep
		buf.WriteString(fmt.Sprintf("func TestCreate%s_WithoutOptional%s(t *testing.T) {\n", res, depRes))
		writeTestSetup(&buf, cfg)
		buf.WriteString("\n")

		writeCreateRequestWithOptionalFK(&buf, cfg, res, pkgName, col, false)
		buf.WriteString(fmt.Sprintf("\tresp, err := client.Create%s(ctx, req)\n", res))
		buf.WriteString("\tif err != nil {\n")
		buf.WriteString(fmt.Sprintf("\t\tt.Fatalf(\"Create%s without optional %s failed: %%v\", err)\n", res, depRes))
		buf.WriteString("\t}\n")
		buf.WriteString(fmt.Sprintf("\tif resp.%s != nil {\n", fieldName))
		buf.WriteString(fmt.Sprintf("\t\tt.Error(\"expected %s to be nil\")\n", fieldName))
		buf.WriteString("\t}\n")
		buf.WriteString("}\n\n")
	}

	// TestCreate_Unauthenticated (if auth required)
	if cfg.RequireAuth {
		buf.WriteString(fmt.Sprintf("func TestCreate%s_Unauthenticated(t *testing.T) {\n", res))
		writeUnauthTestSetup(&buf, cfg)
		buf.WriteString(fmt.Sprintf("\t_, createErr := unauthClient.Create%s(ctx, %s.Create%sRequest{})\n", res, pkgName, res))
		buf.WriteString("\tif createErr == nil {\n")
		buf.WriteString("\t\tt.Error(\"expected error for unauthenticated request\")\n")
		buf.WriteString("\t}\n")
		buf.WriteString("}\n\n")
	}

	return formatSource(buf.Bytes())
}

