	buf.WriteString("\t\t}\n")
	buf.WriteString("\t\tfor _, item := range page2.Items {\n")
	buf.WriteString("\t\t\tif page1IDs[item.PublicId] {\n")
	buf.WriteString("\t\t\t\tt.Errorf(\"item %s appears on both page 1 and page 2\", item.PublicId)\n")
	buf.WriteString("\t\t\t}\n")
