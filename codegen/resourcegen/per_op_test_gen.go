func writeCreateRequestWithOptionalFK(buf *bytes.Buffer, cfg PerOpTestGenConfig, res, pkgName string, targetCol ddl.ColumnDefinition, includeOptional bool) {
	// For the optional FK test, we still need non-nullable FK deps
	// via their fixtures (skip scope column)
	for _, col := range cfg.Table.Columns {
		if col.References != "" && !col.Nullable && !isFixtureAutoColumn(col.Name) {
			if cfg.ScopeColumn != "" && col.Name == cfg.ScopeColumn {
				continue
			}
			depSingular := dbstrings.ToSingular(col.References)
			depAlias := depSingular + "fixture"
			buf.WriteString(fmt.Sprintf("\t%sDep := %s.Create(t, ctx, tx)\n", depSingular, depAlias))
		}
	}

	buf.WriteString(fmt.Sprintf("\treq := %s.Create%sRequest{\n", pkgName, res))
	for _, col := range cfg.Table.Columns {
		if isFixtureAutoColumn(col.Name) {
			continue
		}
		if cfg.ScopeColumn != "" && col.Name == cfg.ScopeColumn {
			continue
		}
		fieldName := dbstrings.ToPascalCase(col.Name)
		if col.Name == targetCol.Name {
			if includeOptional {
				depSingular := dbstrings.ToSingular(col.References)
				buf.WriteString(fmt.Sprintf("\t\t%s: &%s.PublicId,\n", fieldName, depSingular))
			}
			continue
		}
		if col.Nullable {
			continue
		}
		if col.References != "" {
			depSingular := dbstrings.ToSingular(col.References)
			buf.WriteString(fmt.Sprintf("\t\t%s: %sDep.PublicId,\n", fieldName, depSingular))
		} else {
			sampleVal := getSampleValue(goBaseTypeForFixture(col.Type), col.Name)
			buf.WriteString(fmt.Sprintf("\t\t%s: %s,\n", fieldName, sampleVal))
		}
	}
	buf.WriteString("\t}\n")
}

// writeScopedCreateHelper generates a test-local createResource helper function
// that creates a resource via the HTTP client. FK dependencies are created once
// at the enclosing scope via writeCreateDeps. This avoids scoping issues where
// fixtures create resources in a different org than the test user.
func writeScopedCreateHelper(buf *bytes.Buffer, cfg PerOpTestGenConfig) {
	res := dbstrings.ToPascalCase(dbstrings.ToSingular(cfg.TableName))
	pkgName := cfg.TableName
	singular := dbstrings.ToSingular(cfg.TableName)

	buf.WriteString(fmt.Sprintf("\tcreateResource := func() %s.Create%sResponse {\n", pkgName, res))
	buf.WriteString("\t\tt.Helper()\n")
	buf.WriteString(fmt.Sprintf("\t\tresp, err := client.Create%s(ctx, %s.Create%sRequest{\n", res, pkgName, res))
	for _, col := range cfg.Table.Columns {
		if isFixtureAutoColumn(col.Name) || col.Nullable {
			continue
		}
		if cfg.ScopeColumn != "" && col.Name == cfg.ScopeColumn {
			continue
		}
		fieldName := dbstrings.ToPascalCase(col.Name)
		if col.References != "" {
			depSingular := dbstrings.ToSingular(col.References)
			buf.WriteString(fmt.Sprintf("\t\t\t%s: %sDep.PublicId,\n", fieldName, depSingular))
		} else {
			sampleVal := getSampleValue(goBaseTypeForFixture(col.Type), col.Name)
			buf.WriteString(fmt.Sprintf("\t\t\t%s: %s,\n", fieldName, sampleVal))
		}
	}
	buf.WriteString("\t\t})\n")
	buf.WriteString("\t\tif err != nil {\n")
	buf.WriteString(fmt.Sprintf("\t\t\tt.Fatalf(\"failed to create %s: %%v\", err)\n", singular))
	buf.WriteString("\t\t}\n")
	buf.WriteString("\t\treturn resp\n")
	buf.WriteString("\t}\n\n")
}

// writeCreateDeps generates variable declarations for non-nullable FK
// dependencies (excluding the scope column). Must be called before
// writeScopedCreateHelper.
func writeCreateDeps(buf *bytes.Buffer, cfg PerOpTestGenConfig) {
	for _, col := range cfg.Table.Columns {
		if col.References == "" || col.Nullable || isFixtureAutoColumn(col.Name) {
			continue
		}
		if cfg.ScopeColumn != "" && col.Name == cfg.ScopeColumn {
			continue
		}
		depSingular := dbstrings.ToSingular(col.References)
		depAlias := depSingular + "fixture"
		buf.WriteString(fmt.Sprintf("\t%sDep := %s.Create(t, ctx, tx)\n", depSingular, depAlias))
	}
}

func formatSource(src []byte) ([]byte, error) {
	formatted, err := format.Source(src)
	if err != nil {
		return src, fmt.Errorf("failed to format test code: %w\nunformatted:\n%s", err, string(src))
	}
	return formatted, nil
}
