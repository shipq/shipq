// writeSimpleTestImports writes imports for test files that only need the
// resource package and the fixture package (get_one, list, delete tests).
func writeSimpleTestImports(buf *bytes.Buffer, cfg PerOpTestGenConfig, needsFixture bool) {
	buf.WriteString("import (\n")
	buf.WriteString("\t\"testing\"\n\n")
	buf.WriteString(fmt.Sprintf("\t%q\n", cfg.ModulePath+"/api/"+cfg.TableName))
	if needsFixture && cfg.ScopeColumn == "" {
		// Only import the resource fixture for unscoped resources.
		// Scoped resources create via the HTTP client instead.
		buf.WriteString(fmt.Sprintf("\t%q\n", cfg.ModulePath+"/api/"+cfg.TableName+"/fixture"))
	}
	// FK dep fixtures (both nullable deps and non-nullable deps for scoped resources)
	for _, col := range cfg.Table.Columns {
		if col.References == "" || isFixtureAutoColumn(col.Name) {
			continue
		}
		if cfg.ScopeColumn != "" && col.Name == cfg.ScopeColumn {
			continue
		}
		// For scoped resources, import all non-nullable FK dep fixtures
		// (they're needed by writeCreateDeps/writeScopedCreateHelper).
		// For unscoped resources, only import nullable FK dep fixtures.
		if col.Nullable || (cfg.ScopeColumn != "" && needsFixture) {
			depSingular := dbstrings.ToSingular(col.References)
			depAlias := depSingular + "fixture"
			buf.WriteString(fmt.Sprintf("\t%s %q\n", depAlias, cfg.ModulePath+"/api/"+col.References+"/fixture"))
		}
	}
	buf.WriteString(")\n\n")
}

