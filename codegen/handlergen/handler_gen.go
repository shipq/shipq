// AuthorJoinConflictsWithFK returns true when the table has an explicit FK
// column whose resolved alias would collide with the author-account join
// aliases ("author_id", "author_email", …).  The most common case is a
// user-defined `author_id` FK pointing at a domain table (e.g. "authors")
// which would clash with the auto-generated `author_account_id` → accounts
// join that also produces an "author_id" SelectAs.
func AuthorJoinConflictsWithFK(table ddl.Table) bool {
	authorAliases := map[string]bool{
		"author_id":         true,
		"author_email":      true,
		"author_first_name": true,
		"author_last_name":  true,
	}
	for _, col := range table.Columns {
		if col.References != "" && authorAliases[col.Name] {
			return true
		}
	}
	return false
}

