// testClientTypeName returns a unique type name for a resource's test client.
// e.g., "accounts" -> "AccountsTestClient", "organization_users" -> "OrganizationUsersTestClient"
func testClientTypeName(resourceName string) string {
	// Capitalize first letter of each word (split on underscore)
	parts := strings.Split(resourceName, "_")
	var b strings.Builder
	for _, part := range parts {
		if len(part) > 0 {
			runes := []rune(part)
			runes[0] = unicode.ToUpper(runes[0])
			b.WriteString(string(runes))
		}
	}
	b.WriteString("TestClient")
	return b.String()
}

// HTTPTestClientGenConfig holds configuration for generating the HTTP test client.
type HTTPTestClientGenConfig struct {
	ModulePath string                          // e.g., "myapp"
	Handlers   []codegen.SerializedHandlerInfo // handlers from registry
	OutputPkg  string                          // package name for generated code (e.g., "api")
}

// GeneratedTestClientFile represents a single generated test client file.
type GeneratedTestClientFile struct {
	RelPath string // e.g., "api/accounts/http/zz_generated_testclient.go"
	Content []byte
}

// GenerateHTTPTestClient generates the test client files.
// Returns multiple files: per-resource test clients and a top-level client that embeds them.
func GenerateHTTPTestClient(cfg HTTPTestClientGenConfig) ([]GeneratedTestClientFile, error) {
	var files []GeneratedTestClientFile

	groups := server.GroupHandlersByPackage(cfg.ModulePath, cfg.Handlers)

	// Generate per-resource test client files
	for _, group := range groups {
		content, err := generateResourceTestClient(cfg.ModulePath, group)
		if err != nil {
			return nil, fmt.Errorf("failed to generate %s test client: %w", group.ResourceName, err)
		}
		files = append(files, GeneratedTestClientFile{
			RelPath: group.RelDir + "/zz_generated_testclient.go",
			Content: content,
		})
	}

	// Generate top-level test client
	topLevel, err := generateTopLevelTestClient(cfg, groups)
	if err != nil {
		return nil, fmt.Errorf("failed to generate top-level test client: %w", err)
	}
	files = append(files, GeneratedTestClientFile{
		RelPath: cfg.OutputPkg + "/zz_generated_testclient.go",
		Content: topLevel,
	})

	return files, nil
}

// generateResourceTestClient generates a TestClient struct for a single resource.
func generateResourceTestClient(modulePath string, group server.ResourceGroup) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by shipq.\n")
	fmt.Fprintf(&buf, "package %s\n\n", group.HTTPPkgName)

	// Collect handler packages for this group
	handlerPkgs := codegen.CollectHandlerPackages(group.Handlers)

	// Imports
	needsStrings := false
	needsBytes := false
	for _, h := range group.Handlers {
		if len(h.PathParams) > 0 {
			needsStrings = true
		}
		if codegen.MethodHasBody(h.Method) && h.Request != nil && len(h.Request.Fields) > 0 {
			needsBytes = true
		}
	}

	buf.WriteString("import (\n")
	if needsBytes {
		buf.WriteString("\t\"bytes\"\n")
	}
	buf.WriteString("\t\"context\"\n")
	buf.WriteString("\t\"encoding/json\"\n")
	buf.WriteString("\t\"fmt\"\n")
	buf.WriteString("\t\"io\"\n")
	buf.WriteString("\t\"net/http\"\n")
	buf.WriteString("\t\"net/http/httptest\"\n")
	if needsStrings {
		buf.WriteString("\t\"strings\"\n")
	}
	buf.WriteString("\n")
	fmt.Fprintf(&buf, "\t%q\n", modulePath+"/shipq/lib/httputil")

	if len(handlerPkgs) > 0 {
		buf.WriteString("\n")
		var aliases []codegen.PackageAlias
		for _, pkg := range handlerPkgs {
			aliases = append(aliases, pkg)
		}
		sort.Slice(aliases, func(i, j int) bool {
			return aliases[i].Alias < aliases[j].Alias
		})
		for _, pkg := range aliases {
			fmt.Fprintf(&buf, "\t%s %q\n", pkg.Alias, pkg.Path)
		}
	}

	buf.WriteString(")\n\n")

	typeName := testClientTypeName(group.ResourceName)

	// TestClient struct with unexported fields to avoid collision when embedded
	buf.WriteString(fmt.Sprintf(`// %s is a per-resource test client for %s.
type %s struct {
	server        *httptest.Server
	http          *http.Client
	sessionCookie string
}

// New%s creates a new %s.
func New%s(ts *httptest.Server, httpClient *http.Client, sessionCookie string) *%s {
	return &%s{
		server:        ts,
		http:          httpClient,
		sessionCookie: sessionCookie,
	}
}

`, typeName, group.ResourceName, typeName, typeName, typeName, typeName, typeName, typeName))

	// Generate method for each handler
	for _, h := range group.Handlers {
		generateTestClientMethod(&buf, h, handlerPkgs, typeName)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format %s test client: %w\nunformatted:\n%s", group.ResourceName, err, buf.String())
	}
	return formatted, nil
}

// generateTopLevelTestClient generates the top-level Client that embeds per-resource TestClients.
func generateTopLevelTestClient(cfg HTTPTestClientGenConfig, groups []server.ResourceGroup) ([]byte, error) {
	// If there are no groups, return a simplified version
	if len(groups) == 0 {
		return generateEmptyTestClient(cfg)
	}

	var buf bytes.Buffer

	buf.WriteString("// Code generated by shipq.\n")
	fmt.Fprintf(&buf, "package %s\n\n", cfg.OutputPkg)

	buf.WriteString("import (\n")
	buf.WriteString("\t\"net/http/httptest\"\n")

	buf.WriteString("\n")
	for _, g := range groups {
		fmt.Fprintf(&buf, "\t%s %q\n", g.HTTPPkgName, g.HTTPPkgPath)
	}
	buf.WriteString(")\n\n")

	// Client struct with embedded per-resource test client pointers
	buf.WriteString(`// Client is a type-safe HTTP client for testing.
// It embeds per-resource test client structs, so all handler methods are promoted.
type Client struct {
`)
	for _, g := range groups {
		typeName := testClientTypeName(g.ResourceName)
		fmt.Fprintf(&buf, "\t*%s.%s\n", g.HTTPPkgName, typeName)
	}
	buf.WriteString("}\n\n")

	// NewUnauthenticatedTestClient
	buf.WriteString(`// NewUnauthenticatedTestClient creates a test client for the given server.
func NewUnauthenticatedTestClient(ts *httptest.Server) *Client {
	httpClient := ts.Client()
	return &Client{
`)
	for _, g := range groups {
		typeName := testClientTypeName(g.ResourceName)
		fmt.Fprintf(&buf, "\t\t%s: %s.New%s(ts, httpClient, \"\"),\n", typeName, g.HTTPPkgName, typeName)
	}
	buf.WriteString("\t}\n}\n\n")

	// NewAuthenticatedTestClient
	buf.WriteString(`// NewAuthenticatedTestClient creates a test client with a session cookie.
func NewAuthenticatedTestClient(ts *httptest.Server, sessionCookie string) *Client {
	httpClient := ts.Client()
	return &Client{
`)
	for _, g := range groups {
		typeName := testClientTypeName(g.ResourceName)
		fmt.Fprintf(&buf, "\t\t%s: %s.New%s(ts, httpClient, sessionCookie),\n", typeName, g.HTTPPkgName, typeName)
	}
	buf.WriteString("\t}\n}\n\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format top-level test client: %w\nunformatted:\n%s", err, buf.String())
	}
	return formatted, nil
}

// generateEmptyTestClient generates a minimal test client when there are no handlers.
func generateEmptyTestClient(cfg HTTPTestClientGenConfig) ([]byte, error) {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by shipq.\n")
	fmt.Fprintf(&buf, "package %s\n\n", cfg.OutputPkg)
	buf.WriteString("import (\n")
	buf.WriteString("\t\"net/http\"\n")
	buf.WriteString("\t\"net/http/httptest\"\n")
	buf.WriteString(")\n\n")
	buf.WriteString(`// Client is a type-safe HTTP client for testing.
type Client struct {
	server        *httptest.Server
	http          *http.Client
	sessionCookie string
}

// NewUnauthenticatedTestClient creates a test client for the given server.
func NewUnauthenticatedTestClient(ts *httptest.Server) *Client {
	return &Client{
		server: ts,
		http:   ts.Client(),
	}
}

// NewAuthenticatedTestClient creates a test client with a session cookie.
func NewAuthenticatedTestClient(ts *httptest.Server, sessionCookie string) *Client {
	return &Client{
		server:        ts,
		http:          ts.Client(),
		sessionCookie: sessionCookie,
	}
}
`)
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format empty test client: %w\nunformatted:\n%s", err, buf.String())
	}
	return formatted, nil
}

// generateTestClientMethod generates a typed method for a single handler.
func generateTestClientMethod(buf *bytes.Buffer, h codegen.SerializedHandlerInfo, handlerPkgs map[string]codegen.PackageAlias, typeName string) {
	pkgAlias := ""
	if pkg, ok := handlerPkgs[h.PackagePath]; ok {
		pkgAlias = pkg.Alias
	}

	reqType := "struct{}"
	if h.Request != nil && h.Request.Name != "" {
		reqType = h.Request.Name
		if pkgAlias != "" {
			reqType = pkgAlias + "." + reqType
		}
	}

	respType := "struct{}"
	if h.Response != nil && h.Response.Name != "" {
		respType = h.Response.Name
		if pkgAlias != "" {
			respType = pkgAlias + "." + respType
		}
	}

	isDeleteNoBody := h.Method == "DELETE" && (h.Response == nil || len(h.Response.Fields) == 0)

	convertedPath := codegen.ConvertPathSyntax(h.Path)
	fmt.Fprintf(buf, "// %s calls %s %s\n", h.FuncName, h.Method, h.Path)

	if isDeleteNoBody {
		fmt.Fprintf(buf, "func (c *%s) %s(ctx context.Context, req %s) error {\n", typeName, h.FuncName, reqType)
	} else {
		fmt.Fprintf(buf, "func (c *%s) %s(ctx context.Context, req %s) (%s, error) {\n", typeName, h.FuncName, reqType, respType)
	}

	if !isDeleteNoBody {
		fmt.Fprintf(buf, "\tvar resp %s\n", respType)
	}

	generateURLConstruction(buf, convertedPath, h)
	generateRequestCreation(buf, h)

	// Use httputil.AddAuth instead of inline method
	buf.WriteString("\thttputil.AddAuth(httpReq, c.sessionCookie)\n\n")

	buf.WriteString("\thttpResp, err := c.http.Do(httpReq)\n")
	buf.WriteString("\tif err != nil {\n")
	if isDeleteNoBody {
		buf.WriteString("\t\treturn err\n")
	} else {
		buf.WriteString("\t\treturn resp, err\n")
	}
	buf.WriteString("\t}\n")
	buf.WriteString("\tdefer httpResp.Body.Close()\n\n")

	buf.WriteString("\tif httpResp.StatusCode >= 400 {\n")
	buf.WriteString("\t\tbodyBytes, _ := io.ReadAll(httpResp.Body)\n")
	if isDeleteNoBody {
		buf.WriteString("\t\treturn fmt.Errorf(\"HTTP %d: %s\", httpResp.StatusCode, string(bodyBytes))\n")
	} else {
		buf.WriteString("\t\treturn resp, fmt.Errorf(\"HTTP %d: %s\", httpResp.StatusCode, string(bodyBytes))\n")
	}
	buf.WriteString("\t}\n\n")

	if isDeleteNoBody {
		buf.WriteString("\treturn nil\n")
	} else {
		buf.WriteString("\tif err := json.NewDecoder(httpResp.Body).Decode(&resp); err != nil {\n")
		buf.WriteString("\t\treturn resp, fmt.Errorf(\"failed to decode response: %w\", err)\n")
		buf.WriteString("\t}\n\n")
		buf.WriteString("\treturn resp, nil\n")
	}

	buf.WriteString("}\n\n")

	// Generate WithCookies variant for POST/DELETE methods
	if h.Method == "POST" || h.Method == "DELETE" {
		generateTestClientMethodWithCookies(buf, h, handlerPkgs, reqType, respType, isDeleteNoBody, convertedPath, typeName)
	}
}

// generateTestClientMethodWithCookies generates a variant that also returns cookies.
func generateTestClientMethodWithCookies(buf *bytes.Buffer, h codegen.SerializedHandlerInfo, handlerPkgs map[string]codegen.PackageAlias, reqType, respType string, isDeleteNoBody bool, convertedPath string, typeName string) {
	fmt.Fprintf(buf, "// %sWithCookies calls %s %s and returns response cookies.\n", h.FuncName, h.Method, h.Path)

	if isDeleteNoBody {
		fmt.Fprintf(buf, "func (c *%s) %sWithCookies(ctx context.Context, req %s) ([]*http.Cookie, error) {\n", typeName, h.FuncName, reqType)
	} else {
		fmt.Fprintf(buf, "func (c *%s) %sWithCookies(ctx context.Context, req %s) (%s, []*http.Cookie, error) {\n", typeName, h.FuncName, reqType, respType)
	}

	if !isDeleteNoBody {
		fmt.Fprintf(buf, "\tvar resp %s\n", respType)
	}

	generateURLConstruction(buf, convertedPath, h)
	generateRequestCreationWithCookies(buf, h, isDeleteNoBody)

	buf.WriteString("\thttputil.AddAuth(httpReq, c.sessionCookie)\n\n")

	buf.WriteString("\thttpResp, err := c.http.Do(httpReq)\n")
	buf.WriteString("\tif err != nil {\n")
	if isDeleteNoBody {
		buf.WriteString("\t\treturn nil, err\n")
	} else {
		buf.WriteString("\t\treturn resp, nil, err\n")
	}
	buf.WriteString("\t}\n")
	buf.WriteString("\tdefer httpResp.Body.Close()\n\n")

	buf.WriteString("\tcookies := httpResp.Cookies()\n\n")

	buf.WriteString("\tif httpResp.StatusCode >= 400 {\n")
	buf.WriteString("\t\tbodyBytes, _ := io.ReadAll(httpResp.Body)\n")
	if isDeleteNoBody {
		buf.WriteString("\t\treturn cookies, fmt.Errorf(\"HTTP %d: %s\", httpResp.StatusCode, string(bodyBytes))\n")
	} else {
		buf.WriteString("\t\treturn resp, cookies, fmt.Errorf(\"HTTP %d: %s\", httpResp.StatusCode, string(bodyBytes))\n")
	}
	buf.WriteString("\t}\n\n")

	if isDeleteNoBody {
		buf.WriteString("\treturn cookies, nil\n")
	} else {
		buf.WriteString("\tif err := json.NewDecoder(httpResp.Body).Decode(&resp); err != nil {\n")
		buf.WriteString("\t\treturn resp, cookies, fmt.Errorf(\"failed to decode response: %w\", err)\n")
		buf.WriteString("\t}\n\n")
		buf.WriteString("\treturn resp, cookies, nil\n")
	}

	buf.WriteString("}\n\n")
}

// generateRequestCreationWithCookies generates code to create the HTTP request (for WithCookies variant).
func generateRequestCreationWithCookies(buf *bytes.Buffer, h codegen.SerializedHandlerInfo, isDeleteNoBody bool) {
	hasBody := codegen.MethodHasBody(h.Method)

	if hasBody && h.Request != nil && len(h.Request.Fields) > 0 {
		buf.WriteString("\tbody, err := json.Marshal(req)\n")
		buf.WriteString("\tif err != nil {\n")
		if isDeleteNoBody {
			buf.WriteString("\t\treturn nil, fmt.Errorf(\"failed to marshal request: %w\", err)\n")
		} else {
			buf.WriteString("\t\treturn resp, nil, fmt.Errorf(\"failed to marshal request: %w\", err)\n")
		}
		buf.WriteString("\t}\n\n")

		fmt.Fprintf(buf, "\thttpReq, err := http.NewRequestWithContext(ctx, %q, url, bytes.NewReader(body))\n", h.Method)
		buf.WriteString("\tif err != nil {\n")
		if isDeleteNoBody {
			buf.WriteString("\t\treturn nil, fmt.Errorf(\"failed to create request: %w\", err)\n")
		} else {
			buf.WriteString("\t\treturn resp, nil, fmt.Errorf(\"failed to create request: %w\", err)\n")
		}
		buf.WriteString("\t}\n")
		buf.WriteString("\thttpReq.Header.Set(\"Content-Type\", \"application/json\")\n\n")
	} else {
		fmt.Fprintf(buf, "\thttpReq, err := http.NewRequestWithContext(ctx, %q, url, nil)\n", h.Method)
		buf.WriteString("\tif err != nil {\n")
		if isDeleteNoBody {
			buf.WriteString("\t\treturn nil, fmt.Errorf(\"failed to create request: %w\", err)\n")
		} else {
			buf.WriteString("\t\treturn resp, nil, fmt.Errorf(\"failed to create request: %w\", err)\n")
		}
		buf.WriteString("\t}\n\n")
	}
}

// generateURLConstruction generates code to build the URL with path parameter substitution.
func generateURLConstruction(buf *bytes.Buffer, convertedPath string, h codegen.SerializedHandlerInfo) {
	if len(h.PathParams) == 0 {
		fmt.Fprintf(buf, "\turl := c.server.URL + %q\n\n", convertedPath)
		return
	}

	buf.WriteString("\turl := c.server.URL + ")

	pathParts := parsePathForReplacement(convertedPath)

	if len(pathParts) == 1 && pathParts[0].isLiteral {
		fmt.Fprintf(buf, "%q\n\n", convertedPath)
		return
	}

	buf.WriteString("strings.NewReplacer(\n")
	for _, param := range h.PathParams {
		fieldName := findRequestFieldForParam(h, param.Name)
		fmt.Fprintf(buf, "\t\t\"{%s}\", req.%s,\n", param.Name, fieldName)
	}
	fmt.Fprintf(buf, "\t).Replace(%q)\n\n", convertedPath)
}

// pathPart represents either a literal string or a parameter placeholder.
type pathPart struct {
	value     string
	isLiteral bool
}

// parsePathForReplacement parses a path like "/users/{id}/posts" into parts.
func parsePathForReplacement(path string) []pathPart {
	var parts []pathPart
	re := regexp.MustCompile(`\{([a-zA-Z_][a-zA-Z0-9_]*)\}`)
	matches := re.FindAllStringSubmatchIndex(path, -1)

	if len(matches) == 0 {
		return []pathPart{{value: path, isLiteral: true}}
	}

	lastEnd := 0
	for _, match := range matches {
		if match[0] > lastEnd {
			parts = append(parts, pathPart{value: path[lastEnd:match[0]], isLiteral: true})
		}
		parts = append(parts, pathPart{value: path[match[2]:match[3]], isLiteral: false})
		lastEnd = match[1]
	}
	if lastEnd < len(path) {
		parts = append(parts, pathPart{value: path[lastEnd:], isLiteral: true})
	}

	return parts
}

// findRequestFieldForParam finds the Go field name for a path parameter.
func findRequestFieldForParam(h codegen.SerializedHandlerInfo, paramName string) string {
	if h.Request == nil {
		return paramName
	}

	for _, field := range h.Request.Fields {
		// Match by path tag first (most explicit), then JSON name, then Go field name
		if field.Tags["path"] == paramName ||
			strings.EqualFold(field.JSONName, paramName) ||
			strings.EqualFold(field.Name, paramName) {
			return field.Name
		}
	}

	if len(paramName) > 0 {
		return strings.ToUpper(paramName[:1]) + paramName[1:]
	}
	return paramName
}

// generateRequestCreation generates code to create the HTTP request.
func generateRequestCreation(buf *bytes.Buffer, h codegen.SerializedHandlerInfo) {
	hasBody := codegen.MethodHasBody(h.Method)

	if hasBody && h.Request != nil && len(h.Request.Fields) > 0 {
		buf.WriteString("\tbody, err := json.Marshal(req)\n")
		buf.WriteString("\tif err != nil {\n")
		buf.WriteString("\t\treturn resp, fmt.Errorf(\"failed to marshal request: %w\", err)\n")
		buf.WriteString("\t}\n\n")

		fmt.Fprintf(buf, "\thttpReq, err := http.NewRequestWithContext(ctx, %q, url, bytes.NewReader(body))\n", h.Method)
		buf.WriteString("\tif err != nil {\n")
		buf.WriteString("\t\treturn resp, fmt.Errorf(\"failed to create request: %w\", err)\n")
		buf.WriteString("\t}\n")
		buf.WriteString("\thttpReq.Header.Set(\"Content-Type\", \"application/json\")\n\n")
	} else {
		isDeleteNoBody := h.Method == "DELETE" && (h.Response == nil || len(h.Response.Fields) == 0)
		fmt.Fprintf(buf, "\thttpReq, err := http.NewRequestWithContext(ctx, %q, url, nil)\n", h.Method)
		buf.WriteString("\tif err != nil {\n")
		if isDeleteNoBody {
			buf.WriteString("\t\treturn fmt.Errorf(\"failed to create request: %w\", err)\n")
		} else {
			buf.WriteString("\t\treturn resp, fmt.Errorf(\"failed to create request: %w\", err)\n")
		}
		buf.WriteString("\t}\n\n")
	}
}
