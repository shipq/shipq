// HTTPServerGenConfig holds configuration for generating the HTTP server.
type HTTPServerGenConfig struct {
	ModulePath      string                          // e.g., "myapp"
	Handlers        []codegen.SerializedHandlerInfo // handlers from registry
	OutputPkg       string                          // package name for generated code (e.g., "api")
	OpenAPISpec     string                          // OpenAPI spec JSON string (empty = skip dev routes)
	OpenAPIDocsHTML string                          // Stoplight Elements HTML page (empty = skip dev routes)
	AdminHTML       string                          // Admin panel HTML page (empty = skip admin routes)
	ScopeColumn     string                          // from [db] scope in shipq.ini; controls RBAC query variant
	HasChannels     bool                            // true when [workers] channels exist; generates SetupMux
	HasOAuth        bool                            // true when any OAuth provider is enabled; registers OAuth routes
}

// GeneratedHTTPFile represents a single generated file.
type GeneratedHTTPFile struct {
	RelPath string // relative path from shipq root, e.g., "api/accounts/http/zz_generated_http.go"
	Content []byte
}

// ResourceGroup groups handlers by their package path.
type ResourceGroup struct {
	PackagePath  string                          // e.g., "myapp/api/accounts"
	ResourceName string                          // e.g., "accounts"
	Handlers     []codegen.SerializedHandlerInfo // handlers in this group
	HTTPPkgName  string                          // Go package name, e.g., "accountshttp"
	HTTPPkgPath  string                          // import path, e.g., "myapp/api/accounts/http"
	RelDir       string                          // relative dir under shipq root, e.g., "api/accounts/http"
}

// GenerateHTTPServer generates the HTTP server code from the handler registry.
// Returns multiple files: per-resource sub-packages and a top-level orchestrator.
func GenerateHTTPServer(cfg HTTPServerGenConfig) ([]GeneratedHTTPFile, error) {
	var files []GeneratedHTTPFile

	// Group handlers by package
	groups := GroupHandlersByPackage(cfg.ModulePath, cfg.Handlers)

	// Determine if auth is needed
	authPkgPath := findAuthPackagePath(cfg.Handlers)

	// Generate per-resource http/ sub-packages
	for _, group := range groups {
		content, err := generateResourceHTTPFile(cfg.ModulePath, group, authPkgPath, cfg.ScopeColumn)
		if err != nil {
			return nil, fmt.Errorf("failed to generate %s: %w", group.RelDir, err)
		}
		files = append(files, GeneratedHTTPFile{
			RelPath: group.RelDir + "/zz_generated_http.go",
			Content: content,
		})
	}

	// Generate top-level api/zz_generated_http.go
	topLevel, err := generateTopLevelHTTP(cfg, groups)
	if err != nil {
		return nil, fmt.Errorf("failed to generate top-level http: %w", err)
	}
	files = append(files, GeneratedHTTPFile{
		RelPath: cfg.OutputPkg + "/zz_generated_http.go",
		Content: topLevel,
	})

	return files, nil
}

// GroupHandlersByPackage groups handlers by their PackagePath and computes metadata.
func GroupHandlersByPackage(modulePath string, handlers []codegen.SerializedHandlerInfo) []ResourceGroup {
	// Collect handlers by package path
	byPkg := make(map[string][]codegen.SerializedHandlerInfo)
	for _, h := range handlers {
		byPkg[h.PackagePath] = append(byPkg[h.PackagePath], h)
	}

	// Sort package paths for deterministic output
	var pkgPaths []string
	for p := range byPkg {
		pkgPaths = append(pkgPaths, p)
	}
	sort.Strings(pkgPaths)

	var groups []ResourceGroup
	for _, pkgPath := range pkgPaths {
		// Extract resource name from package path: "myapp/api/accounts" -> "accounts"
		resourceName := path.Base(pkgPath)

		// Compute the relative dir: strip modulePath prefix to get e.g. "api/accounts"
		relPkg := strings.TrimPrefix(pkgPath, modulePath+"/")
		relDir := relPkg + "/http"

		// Package name: e.g., "accountshttp"
		httpPkgName := resourceName + "http"

		// Import path: e.g., "myapp/api/accounts/http"
		httpPkgPath := pkgPath + "/http"

		groups = append(groups, ResourceGroup{
			PackagePath:  pkgPath,
			ResourceName: resourceName,
			Handlers:     byPkg[pkgPath],
			HTTPPkgName:  httpPkgName,
			HTTPPkgPath:  httpPkgPath,
			RelDir:       relDir,
		})
	}

	return groups
}

// generateResourceHTTPFile generates a single per-resource http sub-package file.
func generateResourceHTTPFile(modulePath string, group ResourceGroup, authPkgPath string, scopeColumn string) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by shipq.\n")
	fmt.Fprintf(&buf, "package %s\n\n", group.HTTPPkgName)

	// Generate imports
	generateResourceImports(&buf, modulePath, group, authPkgPath)

	// Generate RegisterRoutes function
	generateRegisterRoutes(&buf, modulePath, group, authPkgPath, scopeColumn)

	// Generate handler wrappers
	for _, h := range group.Handlers {
		generateResourceHandlerWrapper(&buf, h, group.ResourceName)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format %s HTTP code: %w\nunformatted:\n%s", group.ResourceName, err, buf.String())
	}
	return formatted, nil
}

// generateResourceImports writes the import block for a per-resource http file.
func generateResourceImports(buf *bytes.Buffer, modulePath string, group ResourceGroup, authPkgPath string) {
	needsAuth := false
	for _, h := range group.Handlers {
		if h.RequireAuth || h.OptionalAuth {
			needsAuth = true
			break
		}
	}

	jsonNeeded := needsJSONImport(group.Handlers)
	httperrorNeeded := jsonNeeded || needsHTTPError(group.Handlers)

	buf.WriteString("import (\n")
	buf.WriteString("\t\"context\"\n")
	if jsonNeeded {
		buf.WriteString("\t\"encoding/json\"\n")
	}

	// OptionalAuth closures need "errors" for errors.Is
	needsOptionalAuth := false
	for _, h := range group.Handlers {
		if h.OptionalAuth {
			needsOptionalAuth = true
			break
		}
	}
	if needsOptionalAuth {
		buf.WriteString("\t\"errors\"\n")
	}

	buf.WriteString("\t\"net/http\"\n")

	if needsStrconv(group.Handlers) {
		buf.WriteString("\t\"strconv\"\n")
	}

	buf.WriteString("\n")
	fmt.Fprintf(buf, "\t%q\n", modulePath+"/config")
	if httperrorNeeded {
		fmt.Fprintf(buf, "\t%q\n", modulePath+"/shipq/lib/httperror")
	}
	fmt.Fprintf(buf, "\t%q\n", modulePath+"/shipq/lib/httpserver")
	fmt.Fprintf(buf, "\t%q\n", modulePath+"/shipq/lib/httputil")
	fmt.Fprintf(buf, "\t%q\n", modulePath+"/shipq/lib/logging")
	fmt.Fprintf(buf, "\t%q\n", modulePath+"/shipq/queries")

	// Import the handler package (the parent resource package)
	buf.WriteString("\n")
	fmt.Fprintf(buf, "\t%s %q\n", group.ResourceName, group.PackagePath)

	// Import auth package if needed for auth checking
	if needsAuth && authPkgPath != "" && authPkgPath != group.PackagePath {
		authAlias := path.Base(authPkgPath)
		fmt.Fprintf(buf, "\t%s %q\n", authAlias, authPkgPath)
	}

	buf.WriteString(")\n\n")
}

// generateRegisterRoutes generates the RegisterRoutes function for a resource.
func generateRegisterRoutes(buf *bytes.Buffer, modulePath string, group ResourceGroup, authPkgPath string, scopeColumn string) {
	needsAuth := false
	needsOptionalAuth := false
	for _, h := range group.Handlers {
		if h.RequireAuth {
			needsAuth = true
		}
		if h.OptionalAuth {
			needsOptionalAuth = true
		}
	}

	buf.WriteString(`// RegisterRoutes registers all HTTP routes for the `)
	buf.WriteString(group.ResourceName)
	buf.WriteString(` resource.
func RegisterRoutes(mux *http.ServeMux, q httpserver.PingableQuerier, runner queries.Runner) {
	injectCtx := func(ctx context.Context) context.Context {
		return queries.NewContextWithRunner(ctx, runner)
	}
`)

	if (needsAuth || needsOptionalAuth) && authPkgPath != "" {
		authAlias := path.Base(authPkgPath)
		// If this IS the auth package, use the resource alias
		if authPkgPath == group.PackagePath {
			authAlias = group.ResourceName
		}

		if needsAuth {
			buf.WriteString(`
	checkAuth := func(ctx context.Context) (accountID int64, orgID int64, err error) {
		qRunner := queries.RunnerFromContext(ctx)
`)
			fmt.Fprintf(buf, "\t\tsession, err := %s.GetCurrentSession(ctx, qRunner)\n", authAlias)
			buf.WriteString(`		if err != nil {
			return 0, 0, err
		}
		var defaultOrgID int64
		if session.DefaultOrganizationId != nil {
			defaultOrgID = *session.DefaultOrganizationId
		}
		return session.AccountId, defaultOrgID, nil
	}
`)

			// Emit checkRBAC closure -- always receives orgID; unscoped variant ignores it
			if scopeColumn != "" {
				fmt.Fprintf(buf, `
	checkRBAC := func(ctx context.Context, accountID int64, orgID int64, routePath, method string) error {
		qRunner := queries.RunnerFromContext(ctx)
		return %s.CheckRBAC(ctx, qRunner, accountID, orgID, routePath, method)
	}
`, authAlias)
			} else {
				fmt.Fprintf(buf, `
	checkRBAC := func(ctx context.Context, accountID int64, _ int64, routePath, method string) error {
		qRunner := queries.RunnerFromContext(ctx)
		return %s.CheckRBAC(ctx, qRunner, accountID, 0, routePath, method)
	}
`, authAlias)
			}
		}

		if needsOptionalAuth {
			// tryAuth uses TryGetCurrentSession which classifies errors properly
			buf.WriteString(`
	tryAuth := func(ctx context.Context) (accountID int64, orgID int64, err error) {
		qRunner := queries.RunnerFromContext(ctx)
`)
			fmt.Fprintf(buf, "\t\tsession, err := %s.TryGetCurrentSession(ctx, qRunner)\n", authAlias)
			buf.WriteString(`		if err != nil {
			return 0, 0, err
		}
		var defaultOrgID int64
		if session.DefaultOrganizationId != nil {
			defaultOrgID = *session.DefaultOrganizationId
		}
		return session.AccountId, defaultOrgID, nil
	}
`)
			fmt.Fprintf(buf, `
	isNoSession := func(err error) bool {
		return errors.Is(err, %s.ErrNoValidSession)
	}
`, authAlias)
		}
	}

	buf.WriteString("\n")

	for _, h := range group.Handlers {
		convertedPath := codegen.ConvertPathSyntax(h.Path)
		wrapperName := handlerWrapperName(h)
		if h.RequireAuth {
			// Use WrapRBACHandler for auth routes -- it enforces both auth and RBAC.
			// The routePath uses the original :param syntax to match role_actions.route_path.
			fmt.Fprintf(buf, "\tmux.Handle(\"%s %s\", httputil.WrapRBACHandler(q, injectCtx, checkAuth, checkRBAC, %q, %q, %s))\n", h.Method, convertedPath, h.Path, h.Method, wrapperName)
		} else if h.OptionalAuth {
			// Use WrapOptionalAuthHandler -- attempts auth but proceeds unauthenticated if no session.
			fmt.Fprintf(buf, "\tmux.Handle(\"%s %s\", httputil.WrapOptionalAuthHandler(q, injectCtx, tryAuth, isNoSession, %s))\n", h.Method, convertedPath, wrapperName)
		} else {
			fmt.Fprintf(buf, "\tmux.Handle(\"%s %s\", httputil.WrapHandler(q, injectCtx, %s))\n", h.Method, convertedPath, wrapperName)
		}
	}

	buf.WriteString("}\n\n")
}

// generateResourceHandlerWrapper writes a handler wrapper for a per-resource file.
// In the sub-package, the handler package is imported as the resource name.
func generateResourceHandlerWrapper(buf *bytes.Buffer, h codegen.SerializedHandlerInfo, resourceAlias string) {
	wrapperName := handlerWrapperName(h)
	fmt.Fprintf(buf, "func %s(w http.ResponseWriter, r *http.Request) {\n", wrapperName)

	pkgAlias := resourceAlias

	// Determine if we need to declare a request variable
	hasRequest := h.Request != nil && (len(h.Request.Fields) > 0 || len(h.PathParams) > 0)
	needsJSONBody := hasRequest && codegen.MethodHasBody(h.Method)

	if hasRequest {
		reqType := pkgAlias + "." + h.Request.Name
		fmt.Fprintf(buf, "\tvar req %s\n\n", reqType)

		if len(h.PathParams) > 0 {
			generatePathParamBinding(buf, h)
		}

		if needsJSONBody {
			generateJSONBodyBinding(buf, h)
		}
	}

	// Call the actual handler
	buf.WriteString("\t// Call the actual handler\n")
	handlerCall := pkgAlias + "." + h.FuncName

	if hasRequest {
		fmt.Fprintf(buf, "\tresp, err := %s(r.Context(), &req)\n", handlerCall)
	} else {
		reqType := "struct{}"
		if h.Request != nil {
			reqType = pkgAlias + "." + h.Request.Name
		}
		fmt.Fprintf(buf, "\tresp, err := %s(r.Context(), &%s{})\n", handlerCall, reqType)
	}

	buf.WriteString("\tif err != nil {\n")
	fmt.Fprintf(buf, "\t\tconfig.Logger.Error(%q, \"error\", err.Error(), \"handler\", %q, \"request_id\", logging.RequestIDFromContext(r.Context()))\n", h.FuncName+" failed", h.FuncName)
	buf.WriteString("\t\thttputil.WriteError(w, err)\n")
	buf.WriteString("\t\treturn\n")
	buf.WriteString("\t}\n\n")

	statusCode := successStatusCode(h.Method)
	fmt.Fprintf(buf, "\thttputil.WriteJSON(w, %s, resp)\n", statusCode)

	buf.WriteString("}\n\n")
}

// hasOpenAPI returns true if the config has OpenAPI spec data to embed.
func hasOpenAPI(cfg HTTPServerGenConfig) bool {
	return cfg.OpenAPISpec != "" && cfg.OpenAPIDocsHTML != ""
}

// hasAdmin returns true if the config has admin panel HTML to embed.
func hasAdmin(cfg HTTPServerGenConfig) bool {
	return cfg.AdminHTML != ""
}

// generateTopLevelHTTP generates the thin top-level api/zz_generated_http.go.
func generateTopLevelHTTP(cfg HTTPServerGenConfig, groups []ResourceGroup) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by shipq.\n")
	fmt.Fprintf(&buf, "package %s\n\n", cfg.OutputPkg)

	// Imports
	buf.WriteString("import (\n")
	buf.WriteString("\t\"log/slog\"\n")
	buf.WriteString("\t\"net/http\"\n")
	if hasOpenAPI(cfg) {
		buf.WriteString("\t\"os\"\n")
	}
	buf.WriteString("\n")

	if hasOpenAPI(cfg) || hasAdmin(cfg) {
		fmt.Fprintf(&buf, "\tshipqassets %q\n", cfg.ModulePath+"/shipq/assets")
	}
	fmt.Fprintf(&buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/httpserver")
	fmt.Fprintf(&buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/httputil")
	fmt.Fprintf(&buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/logging")
	fmt.Fprintf(&buf, "\t%q\n", cfg.ModulePath+"/shipq/queries")

	// Import auth package when OAuth is enabled (for RegisterOAuthRoutes)
	if cfg.HasOAuth {
		authPkgPath := findAuthPackagePath(cfg.Handlers)
		if authPkgPath == "" {
			// Fallback: construct from module path (auth handlers may not
			// have been compiled yet on the very first run).
			authPkgPath = cfg.ModulePath + "/api/auth"
		}
		// Only add a separate import if auth is not already imported via groups
		alreadyImported := false
		for _, g := range groups {
			if g.HTTPPkgPath == authPkgPath {
				alreadyImported = true
				break
			}
		}
		if !alreadyImported {
			buf.WriteString("\n")
			fmt.Fprintf(&buf, "\tauth %q\n", authPkgPath)
		}
	}

	if len(groups) > 0 {
		buf.WriteString("\n")
		for _, g := range groups {
			fmt.Fprintf(&buf, "\t%s %q\n", g.HTTPPkgName, g.HTTPPkgPath)
		}
	}
	buf.WriteString(")\n\n")

	// Emit OpenAPI constants if we have spec data
	if hasOpenAPI(cfg) {
		generateOpenAPIConstants(&buf, cfg)
	}

	// Emit admin HTML constant if we have admin panel data
	if hasAdmin(cfg) {
		generateAdminConstants(&buf, cfg)
	}

	// When channels exist, generate SetupMux so that cmd/server/main.go can
	// register channel routes on the raw *http.ServeMux before applying the
	// logging middleware. NewMux delegates to SetupMux internally.
	if cfg.HasChannels {
		generateSetupMux(&buf, cfg, groups)
	}

	// NewMux function
	if cfg.HasChannels {
		// Thin wrapper that delegates to SetupMux + logging.
		buf.WriteString(`// NewMux creates an http.ServeMux with all registered handlers.
// When channel routes need to be registered, use SetupMux directly to obtain the
// raw *http.ServeMux, register channel routes, then wrap with logging.Decorate.
func NewMux(q httpserver.PingableQuerier, runner queries.Runner, logger *slog.Logger) http.Handler {
	mux := SetupMux(q, runner)
	return logging.Decorate([]string{"/health"}, logger, mux)
}
`)
	} else {
		buf.WriteString(`// NewMux creates an http.ServeMux with all registered handlers.
// The provided PingableQuerier will be injected into each request's context
// and used for health checks.
// The provided Runner will be injected into each request's context for query execution.
// The provided logger will be used for request logging.
// Returns the mux wrapped with logging middleware (excluding /health).
func NewMux(q httpserver.PingableQuerier, runner queries.Runner, logger *slog.Logger) http.Handler {
	mux := http.NewServeMux()

	// Health check endpoint - excluded from logging
	mux.HandleFunc("GET /health", func(w http.ResponseWriter, r *http.Request) {
		if err := q.Ping(); err != nil {
			httputil.WriteJSON(w, http.StatusOK, map[string]bool{"healthy": false})
			return
		}
		httputil.WriteJSON(w, http.StatusOK, map[string]bool{"healthy": true})
	})

`)

		for _, g := range groups {
			fmt.Fprintf(&buf, "\t%s.RegisterRoutes(mux, q, runner)\n", g.HTTPPkgName)
		}

		// Dev/test-mode OpenAPI routes
		if hasOpenAPI(cfg) {
			buf.WriteString(`
	// OpenAPI documentation routes (development and test modes only)
	if goEnv := os.Getenv("GO_ENV"); goEnv == "" || goEnv == "development" || goEnv == "test" {
		registerOpenAPIRoutes(mux)
	}
`)
		}

		// Admin panel routes (available in all environments)
		if hasAdmin(cfg) {
			buf.WriteString(`
	// Admin panel routes (auth is handled by the SPA itself)
	registerAdminRoutes(mux)
`)
		}

		// OAuth routes
		if cfg.HasOAuth {
			buf.WriteString(`
	// OAuth redirect/callback routes
	auth.RegisterOAuthRoutes(mux, runner)
`)
		}

		buf.WriteString(`
	// Wrap with logging middleware, excluding /health
	return logging.Decorate([]string{"/health"}, logger, mux)
}
`)
	}

	// Generate the registerOpenAPIRoutes helper function
	if hasOpenAPI(cfg) {
		generateOpenAPIRoutesFunc(&buf)
	}

	// Generate the registerAdminRoutes helper function
	if hasAdmin(cfg) {
		generateAdminRoutesFunc(&buf)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format top-level HTTP code: %w\nunformatted:\n%s", err, buf.String())
	}
	return formatted, nil
}

// generateSetupMux writes a SetupMux function that creates and configures the
// raw *http.ServeMux without applying logging middleware. This allows callers
// (notably cmd/server/main.go) to register channel routes on the mux before
// wrapping with logging.Decorate.
func generateSetupMux(buf *bytes.Buffer, cfg HTTPServerGenConfig, groups []ResourceGroup) {
	buf.WriteString(`// SetupMux creates an *http.ServeMux with health check and all handler routes
// registered, but WITHOUT logging middleware. Use this when you need to register
// additional routes (e.g., channel routes) before applying logging.Decorate.
func SetupMux(q httpserver.PingableQuerier, runner queries.Runner) *http.ServeMux {
	mux := http.NewServeMux()

	// Health check endpoint - excluded from logging
	mux.HandleFunc("GET /health", func(w http.ResponseWriter, r *http.Request) {
		if err := q.Ping(); err != nil {
			httputil.WriteJSON(w, http.StatusOK, map[string]bool{"healthy": false})
			return
		}
		httputil.WriteJSON(w, http.StatusOK, map[string]bool{"healthy": true})
	})

`)

	for _, g := range groups {
		fmt.Fprintf(buf, "\t%s.RegisterRoutes(mux, q, runner)\n", g.HTTPPkgName)
	}

	// Dev/test-mode OpenAPI routes
	if hasOpenAPI(cfg) {
		buf.WriteString(`
	// OpenAPI documentation routes (development and test modes only)
	if goEnv := os.Getenv("GO_ENV"); goEnv == "" || goEnv == "development" || goEnv == "test" {
		registerOpenAPIRoutes(mux)
	}
`)
	}

	// Admin panel routes (available in all environments)
	if hasAdmin(cfg) {
		buf.WriteString(`
	// Admin panel routes (auth is handled by the SPA itself)
	registerAdminRoutes(mux)
`)
	}

	// OAuth routes
	if cfg.HasOAuth {
		buf.WriteString(`
	// OAuth redirect/callback routes
	auth.RegisterOAuthRoutes(mux, runner)
`)
	}

	buf.WriteString(`
	return mux
}
`)
}

// generateOpenAPIConstants writes the OpenAPI spec and docs HTML as Go constants.
func generateOpenAPIConstants(buf *bytes.Buffer, cfg HTTPServerGenConfig) {
	buf.WriteString("// openAPISpec is the OpenAPI 3.1 JSON spec generated at compile time.\n")
	buf.WriteString("var openAPISpec = ")
	// Use a raw string literal with backtick quoting
	fmt.Fprintf(buf, "`%s`\n\n", cfg.OpenAPISpec)

	buf.WriteString("// openAPIDocsHTML is the Stoplight Elements HTML page.\n")
	buf.WriteString("var openAPIDocsHTML = ")
	fmt.Fprintf(buf, "`%s`\n\n", cfg.OpenAPIDocsHTML)
}

// generateOpenAPIRoutesFunc writes the registerOpenAPIRoutes helper function.
func generateOpenAPIRoutesFunc(buf *bytes.Buffer) {
	buf.WriteString(`// registerOpenAPIRoutes adds OpenAPI documentation routes to the mux.
// These routes are only registered in development mode.
func registerOpenAPIRoutes(mux *http.ServeMux) {
	// Serve OpenAPI spec as JSON
	mux.HandleFunc("GET /openapi", func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Content-Type", "application/json")
		w.WriteHeader(http.StatusOK)
		w.Write([]byte(openAPISpec))
	})

	// Serve Stoplight Elements documentation page
	mux.HandleFunc("GET /docs", func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Content-Type", "text/html; charset=utf-8")
		w.WriteHeader(http.StatusOK)
		w.Write([]byte(openAPIDocsHTML))
	})

	// Serve embedded assets for the documentation page
	mux.HandleFunc("GET /openapi/assets/web-components.min.js", func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Content-Type", "application/javascript")
		w.WriteHeader(http.StatusOK)
		w.Write(shipqassets.WebComponentsJS)
	})

	mux.HandleFunc("GET /openapi/assets/styles.min.css", func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Content-Type", "text/css")
		w.WriteHeader(http.StatusOK)
		w.Write(shipqassets.StylesCSS)
	})
}
`)
}

