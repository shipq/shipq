package server

import (
	"bytes"
	"fmt"
	"go/format"
)

// HTTPMainGenConfig holds configuration for generating the main.go entrypoint.
type HTTPMainGenConfig struct {
	ModulePath  string // e.g., "myapp"
	OutputPkg   string // package containing generated HTTP server (e.g., "api")
	DBDialect   string // "mysql", "postgres", or "sqlite"
	HasChannels bool   // true when [workers] channels exist; wires channel routes into the server
	HasAuth     bool   // true when at least one channel requires auth (i.e., is not public)
	AutoMigrate bool   // true when [db] auto_migrate = true and schema.json exists; emits migrate-on-boot block
}

// GenerateHTTPMain generates the main.go entrypoint for the HTTP server.
func GenerateHTTPMain(cfg HTTPMainGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by shipq.\n")
	buf.WriteString("package main\n\n")

	// Generate imports
	generateMainImports(&buf, cfg)

	// Generate main function
	generateMainFunc(&buf, cfg)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return unformatted source for debugging
		return buf.Bytes(), fmt.Errorf("failed to format main.go: %w\nunformatted:\n%s", err, buf.String())
	}

	return formatted, nil
}

// generateMainImports writes the import block for main.go.
func generateMainImports(buf *bytes.Buffer, cfg HTTPMainGenConfig) {
	buf.WriteString("import (\n")
	if cfg.AutoMigrate {
		buf.WriteString("\t\"context\"\n")
	}
	buf.WriteString("\t\"database/sql\"\n")
	buf.WriteString("\t\"net/http\"\n")
	buf.WriteString("\t\"os\"\n")
	buf.WriteString("\n")

	// API package import
	apiPkg := cfg.ModulePath + "/" + cfg.OutputPkg
	fmt.Fprintf(buf, "\t%q\n", apiPkg)

	// Auth package import (needed for channel auth wrappers)
	if cfg.HasChannels && cfg.HasAuth {
		authPkg := cfg.ModulePath + "/" + cfg.OutputPkg + "/auth"
		fmt.Fprintf(buf, "\t%q\n", authPkg)
	}

	// Config package import
	configPkg := cfg.ModulePath + "/config"
	fmt.Fprintf(buf, "\t%q\n", configPkg)

	if cfg.HasChannels {
		// Channel library import
		channelPkg := cfg.ModulePath + "/shipq/lib/channel"
		fmt.Fprintf(buf, "\t%q\n", channelPkg)
		if cfg.HasAuth {
			// HTTP server import (for WithRequestCookies in channel auth wrappers)
			httpserverPkg := cfg.ModulePath + "/shipq/lib/httpserver"
			fmt.Fprintf(buf, "\t%q\n", httpserverPkg)
		}
		// Logging import (for manual Decorate call)
		loggingPkg := cfg.ModulePath + "/shipq/lib/logging"
		fmt.Fprintf(buf, "\t%q\n", loggingPkg)
		if cfg.HasAuth {
			// Queries import (for runner type in auth wrappers)
			queriesPkg := cfg.ModulePath + "/shipq/queries"
			fmt.Fprintf(buf, "\t%q\n", queriesPkg)
		}
	}

	// Auto-migrate import
	if cfg.AutoMigrate {
		migratePkg := cfg.ModulePath + "/shipq/db/migrate"
		fmt.Fprintf(buf, "\tdbmigrate %q\n", migratePkg)
	}

	// Dialect-specific query runner import
	runnerPkg := cfg.ModulePath + "/shipq/queries/" + cfg.DBDialect
	fmt.Fprintf(buf, "\tdbrunner %q\n", runnerPkg)
	buf.WriteString("\n")

	// Database driver import
	driverImport := getDriverImport(cfg.DBDialect)
	fmt.Fprintf(buf, "\t_ %q\n", driverImport)

	buf.WriteString(")\n\n")
}

// generateMainFunc writes the main function.
func generateMainFunc(buf *bytes.Buffer, cfg HTTPMainGenConfig) {
	buf.WriteString("func main() {\n")

	// Database connection using config.ParseDatabaseURL
	buf.WriteString("\tdriver, dsn := config.ParseDatabaseURL(config.Settings.DATABASE_URL)\n")
	buf.WriteString("\tdb, err := sql.Open(driver, dsn)\n")
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString("\t\tconfig.Logger.Error(\"failed to open database\", \"error\", err.Error())\n")
	buf.WriteString("\t\tos.Exit(1)\n")
	buf.WriteString("\t}\n")
	buf.WriteString("\tdefer db.Close()\n\n")

	// Verify connection
	buf.WriteString("\tif err := db.Ping(); err != nil {\n")
	buf.WriteString("\t\tconfig.Logger.Error(\"failed to connect to database\", \"error\", err.Error())\n")
	buf.WriteString("\t\tos.Exit(1)\n")
	buf.WriteString("\t}\n\n")

	// Auto-migrate block (configured via [db] auto_migrate = true in shipq.ini)
	if cfg.AutoMigrate {
		buf.WriteString("\t// Auto-migrate (configured via [db] auto_migrate = true in shipq.ini)\n")
		buf.WriteString("\tconfig.Logger.Info(\"running database migrations\")\n")
		buf.WriteString("\tif err := dbmigrate.RunWithDB(context.Background(), db); err != nil {\n")
		buf.WriteString("\t\tconfig.Logger.Error(\"database migration failed\", \"error\", err.Error())\n")
		buf.WriteString("\t\tos.Exit(1)\n")
		buf.WriteString("\t}\n")
		buf.WriteString("\tconfig.Logger.Info(\"database migrations complete\")\n\n")
	}

	// Create query runner
	buf.WriteString("\trunner := dbrunner.NewQueryRunner(db)\n\n")

	if cfg.HasChannels {
		generateMainFuncWithChannels(buf, cfg)
	} else {
		generateMainFuncWithoutChannels(buf, cfg)
	}

	buf.WriteString("}\n")
}

// generateMainFuncWithoutChannels writes the simple handler + ListenAndServe path.
func generateMainFuncWithoutChannels(buf *bytes.Buffer, cfg HTTPMainGenConfig) {
	buf.WriteString("\thandler := api.NewMux(db, runner, config.Logger)\n\n")

	buf.WriteString("\taddr := \":\" + config.Settings.PORT\n")
	buf.WriteString("\tconfig.Logger.Info(\"starting server\", \"addr\", addr)\n")
	buf.WriteString("\tif err := http.ListenAndServe(addr, handler); err != nil {\n")
	buf.WriteString("\t\tconfig.Logger.Error(\"server failed\", \"error\", err.Error())\n")
	buf.WriteString("\t\tos.Exit(1)\n")
	buf.WriteString("\t}\n")
}

// generateMainFuncWithChannels writes the extended path that creates channel
// infrastructure (MachineryQueue, CentrifugoTransport), builds auth wrappers,
// registers channel routes on the raw mux, then applies logging middleware.
func generateMainFuncWithChannels(buf *bytes.Buffer, cfg HTTPMainGenConfig) {
	// Create CentrifugoTransport
	buf.WriteString("\t// Channel infrastructure: realtime transport\n")
	buf.WriteString("\ttransport := channel.NewCentrifugoTransport(\n")
	buf.WriteString("\t\tconfig.Settings.CENTRIFUGO_API_URL,\n")
	buf.WriteString("\t\tconfig.Settings.CENTRIFUGO_API_KEY,\n")
	buf.WriteString("\t\tconfig.Settings.CENTRIFUGO_HMAC_SECRET,\n")
	buf.WriteString("\t\tconfig.Settings.CENTRIFUGO_WS_URL,\n")
	buf.WriteString("\t)\n\n")

	// Create MachineryQueue
	buf.WriteString("\t// Channel infrastructure: task queue\n")
	buf.WriteString("\tqueue, err := channel.NewMachineryQueue(config.Settings.REDIS_URL)\n")
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString("\t\tconfig.Logger.Error(\"failed to create task queue\", \"error\", err.Error())\n")
	buf.WriteString("\t\tos.Exit(1)\n")
	buf.WriteString("\t}\n\n")

	if cfg.HasAuth {
		// Build auth wrappers that adapt the auth package's context-based functions
		// to the *http.Request-based signatures expected by RegisterChannelRoutes.
		buf.WriteString("\t// Auth wrappers for channel routes\n")
		buf.WriteString("\tcheckAuth := func(r *http.Request) (accountID int64, orgID int64, accountPublicID string, err error) {\n")
		buf.WriteString("\t\tctx := queries.NewContextWithRunner(r.Context(), runner)\n")
		buf.WriteString("\t\tctx = httpserver.WithRequestCookies(ctx, r.Cookies())\n")
		buf.WriteString("\t\tsession, err := auth.GetCurrentSession(ctx, runner)\n")
		buf.WriteString("\t\tif err != nil {\n")
		buf.WriteString("\t\t\treturn 0, 0, \"\", err\n")
		buf.WriteString("\t\t}\n")
		buf.WriteString("\t\tvar defaultOrgID int64\n")
		buf.WriteString("\t\tif session.DefaultOrganizationId != nil {\n")
		buf.WriteString("\t\t\tdefaultOrgID = *session.DefaultOrganizationId\n")
		buf.WriteString("\t\t}\n")
		buf.WriteString("\t\treturn session.AccountId, defaultOrgID, session.AccountPublicId, nil\n")
		buf.WriteString("\t}\n\n")

		buf.WriteString("\tcheckRBAC := func(r *http.Request, accountID int64, orgID int64, routePath, method string) error {\n")
		buf.WriteString("\t\tctx := queries.NewContextWithRunner(r.Context(), runner)\n")
		buf.WriteString("\t\treturn auth.CheckRBAC(ctx, runner, accountID, orgID, routePath, method)\n")
		buf.WriteString("\t}\n\n")
	}

	// Use SetupMux to get the raw mux, register channel routes, then wrap
	buf.WriteString("\t// Build mux: handler routes + channel routes + logging middleware\n")
	buf.WriteString("\tmux := api.SetupMux(db, runner)\n")
	if cfg.HasAuth {
		buf.WriteString("\tapi.RegisterChannelRoutes(mux, queue, transport, db, runner, checkAuth, checkRBAC)\n")
	} else {
		buf.WriteString("\tapi.RegisterChannelRoutes(mux, queue, transport, db, runner)\n")
	}
	buf.WriteString("\thandler := logging.Decorate([]string{\"/health\"}, config.Logger, mux)\n\n")

	buf.WriteString("\taddr := \":\" + config.Settings.PORT\n")
	buf.WriteString("\tconfig.Logger.Info(\"starting server\", \"addr\", addr)\n")
	buf.WriteString("\tif err := http.ListenAndServe(addr, handler); err != nil {\n")
	buf.WriteString("\t\tconfig.Logger.Error(\"server failed\", \"error\", err.Error())\n")
	buf.WriteString("\t\tos.Exit(1)\n")
	buf.WriteString("\t}\n")
}

// getDriverImport returns the import path for the database driver.
func getDriverImport(dialect string) string {
	switch dialect {
	case "postgres":
		return "github.com/jackc/pgx/v5/stdlib"
	case "sqlite":
		return "modernc.org/sqlite"
	case "mysql":
		return "github.com/go-sql-driver/mysql"
	default:
		// Default to mysql
		return "github.com/go-sql-driver/mysql"
	}
}
