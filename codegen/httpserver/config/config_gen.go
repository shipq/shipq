// CustomEnvVar represents a user-defined environment variable declared in the
// [env] section of shipq.ini. The Name is the uppercase env var name (e.g.,
// "OPENAI_API_KEY"), Required controls whether the program exits (true) or
// warns (false) when the variable is missing in production, and Default is an
// optional compile-time dev default (empty string if none).
type CustomEnvVar struct {
	Name     string // e.g., "OPENAI_API_KEY"
	Required bool   // true → RequiredConfig, false → OptionalConfig
	Default  string // dev default value (usually empty)
}

// DevDefaults holds compile-time dev default values read from shipq.ini.
// These are baked into the generated config so that GO_ENV != "production"
// requires zero env vars to run locally.
type DevDefaults struct {
	DatabaseURL  string
	Port         string
	CookieSecret string
	// Files
	S3Bucket             string
	S3Region             string
	S3Endpoint           string
	AWSAccessKeyID       string
	AWSSecretAccessKey   string
	MaxUploadSizeMB      string
	MultipartThresholdMB string
	// Workers
	RedisURL             string
	CentrifugoAPIURL     string
	CentrifugoAPIKey     string
	CentrifugoHMACSecret string
	CentrifugoWSURL      string
	// OAuth
	OAuthRedirectURL     string
	OAuthRedirectBaseURL string
	// Email
	SMTPHost     string
	SMTPPort     string
	SMTPUsername string
	SMTPPassword string
	AppURL       string
}

// ConfigGenConfig holds configuration for generating the config package.
type ConfigGenConfig struct {
	ModulePath     string         // e.g., "myapp" - the user's module path
	Dialect        string         // "postgres", "mysql", or "sqlite"
	FilesEnabled   bool           // true if [files] section exists in shipq.ini
	WorkersEnabled bool           // true if [workers] section exists in shipq.ini
	OAuthGoogle    bool           // true if [auth] oauth_google = true in shipq.ini
	OAuthGitHub    bool           // true if [auth] oauth_github = true in shipq.ini
	EmailEnabled   bool           // true if [email] section exists in shipq.ini
	DevDefaults    DevDefaults    // compile-time dev defaults from shipq.ini
	CustomEnvVars  []CustomEnvVar // user-defined env vars from [env] section
}

// GenerateConfig generates the config/config.go file.
func GenerateConfig(cfg ConfigGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by shipq.\n")
	buf.WriteString("package config\n\n")

	// Generate imports
	generateConfigImports(&buf, cfg)

	// Generate helper functions (mustExist, loadRequired, loadOptional)
	generateHelpers(&buf)

	// Generate SettingsConfig struct
	generateSettingsConfig(&buf, cfg)

	// Generate global vars
	generateGlobalVars(&buf)

	// Generate getLogger function
	generateGetLogger(&buf)

	// Generate dev default constants and loadWithDefaults
	generateDevDefaults(&buf, cfg)

	// Generate init function
	generateInit(&buf, cfg)

	// Generate ParseDatabaseURL and IsLocalhostURL
	generateParseDatabaseURL(&buf, cfg)
	generateIsLocalhostURL(&buf)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return unformatted source for debugging
		return buf.Bytes(), fmt.Errorf("failed to format config.go: %w\nunformatted:\n%s", err, buf.String())
	}

	return formatted, nil
}

// generateConfigImports writes the import block for config.go.
func generateConfigImports(buf *bytes.Buffer, cfg ConfigGenConfig) {
	buf.WriteString("import (\n")
	buf.WriteString("\t\"fmt\"\n")
	buf.WriteString("\t\"log/slog\"\n")
	buf.WriteString("\t\"os\"\n")
	buf.WriteString("\t\"reflect\"\n")
	buf.WriteString("\t\"strings\"\n")
	buf.WriteString("\n")
	buf.WriteString(fmt.Sprintf("\t%q\n", cfg.ModulePath+"/shipq/lib/logging"))
	buf.WriteString(")\n\n")
}

// generateHelpers writes the mustExist and warnMissing helper functions.
func generateHelpers(buf *bytes.Buffer) {
	buf.WriteString(`func mustExist(name string) string {
	envVar := os.Getenv(name)
	if envVar == "" {
		fmt.Fprintf(os.Stderr, "FATAL: environment variable %s is not set\n", name)
		os.Exit(1)
	}
	return envVar
}

func loadRequired(s any) {
	val := reflect.ValueOf(s).Elem()
	for i := range val.NumField() {
		name := val.Type().Field(i).Name
		val.Field(i).SetString(mustExist(name))
	}
}

func loadOptional(s any) {
	val := reflect.ValueOf(s).Elem()
	for i := range val.NumField() {
		name := val.Type().Field(i).Name
		envVar := os.Getenv(name)
		if envVar == "" {
			fmt.Fprintf(os.Stderr, "WARNING: environment variable %s is not set\n", name)
		}
		val.Field(i).SetString(envVar)
	}
}

`)
}

// generateDevDefaults writes compile-time dev default constants, the devDefaultMap,
// and the loadWithDefaults function.
func generateDevDefaults(buf *bytes.Buffer, cfg ConfigGenConfig) {
	d := cfg.DevDefaults

	// Dev default constants
	buf.WriteString("// Dev default constants — baked in from shipq.ini at codegen time.\n")
	writeConst(buf, "devDATABASE_URL", d.DatabaseURL)
	writeConst(buf, "devPORT", d.Port)
	writeConst(buf, "devGO_ENV", "development")
	writeConst(buf, "devCOOKIE_SECRET", d.CookieSecret)

	if cfg.FilesEnabled {
		writeConst(buf, "devS3_BUCKET", d.S3Bucket)
		writeConst(buf, "devS3_REGION", d.S3Region)
		writeConst(buf, "devS3_ENDPOINT", d.S3Endpoint)
		writeConst(buf, "devAWS_ACCESS_KEY_ID", d.AWSAccessKeyID)
		writeConst(buf, "devAWS_SECRET_ACCESS_KEY", d.AWSSecretAccessKey)
		writeConst(buf, "devMAX_UPLOAD_SIZE_MB", d.MaxUploadSizeMB)
		writeConst(buf, "devMULTIPART_THRESHOLD_MB", d.MultipartThresholdMB)
	}

	if cfg.WorkersEnabled {
		writeConst(buf, "devREDIS_URL", d.RedisURL)
		writeConst(buf, "devCENTRIFUGO_API_URL", d.CentrifugoAPIURL)
		writeConst(buf, "devCENTRIFUGO_API_KEY", d.CentrifugoAPIKey)
		writeConst(buf, "devCENTRIFUGO_HMAC_SECRET", d.CentrifugoHMACSecret)
		writeConst(buf, "devCENTRIFUGO_WS_URL", d.CentrifugoWSURL)
	}

	if cfg.OAuthGoogle {
		writeConst(buf, "devGOOGLE_CLIENT_ID", "")
		writeConst(buf, "devGOOGLE_CLIENT_SECRET", "")
	}

	if cfg.OAuthGitHub {
		writeConst(buf, "devGITHUB_CLIENT_ID", "")
		writeConst(buf, "devGITHUB_CLIENT_SECRET", "")
	}

	if cfg.OAuthGoogle || cfg.OAuthGitHub {
		writeConst(buf, "devOAUTH_REDIRECT_URL", d.OAuthRedirectURL)
		writeConst(buf, "devOAUTH_REDIRECT_BASE_URL", d.OAuthRedirectBaseURL)
	}

	if cfg.EmailEnabled {
		writeConst(buf, "devSMTP_HOST", d.SMTPHost)
		writeConst(buf, "devSMTP_PORT", d.SMTPPort)
		writeConst(buf, "devSMTP_USERNAME", d.SMTPUsername)
		writeConst(buf, "devSMTP_PASSWORD", d.SMTPPassword)
		writeConst(buf, "devAPP_URL", d.AppURL)
	}

	for _, ev := range cfg.CustomEnvVars {
		writeConst(buf, "dev"+ev.Name, ev.Default)
	}

	buf.WriteString("\n")

	// devDefaultMap
	buf.WriteString("var devDefaultMap = map[string]string{\n")
	writeMapEntry(buf, "DATABASE_URL", "devDATABASE_URL")
	writeMapEntry(buf, "PORT", "devPORT")
	writeMapEntry(buf, "GO_ENV", "devGO_ENV")
	writeMapEntry(buf, "COOKIE_SECRET", "devCOOKIE_SECRET")

	if cfg.FilesEnabled {
		writeMapEntry(buf, "S3_BUCKET", "devS3_BUCKET")
		writeMapEntry(buf, "S3_REGION", "devS3_REGION")
		writeMapEntry(buf, "S3_ENDPOINT", "devS3_ENDPOINT")
		writeMapEntry(buf, "AWS_ACCESS_KEY_ID", "devAWS_ACCESS_KEY_ID")
		writeMapEntry(buf, "AWS_SECRET_ACCESS_KEY", "devAWS_SECRET_ACCESS_KEY")
		writeMapEntry(buf, "MAX_UPLOAD_SIZE_MB", "devMAX_UPLOAD_SIZE_MB")
		writeMapEntry(buf, "MULTIPART_THRESHOLD_MB", "devMULTIPART_THRESHOLD_MB")
	}

	if cfg.WorkersEnabled {
		writeMapEntry(buf, "REDIS_URL", "devREDIS_URL")
		writeMapEntry(buf, "CENTRIFUGO_API_URL", "devCENTRIFUGO_API_URL")
		writeMapEntry(buf, "CENTRIFUGO_API_KEY", "devCENTRIFUGO_API_KEY")
		writeMapEntry(buf, "CENTRIFUGO_HMAC_SECRET", "devCENTRIFUGO_HMAC_SECRET")
		writeMapEntry(buf, "CENTRIFUGO_WS_URL", "devCENTRIFUGO_WS_URL")
	}

	if cfg.OAuthGoogle {
		writeMapEntry(buf, "GOOGLE_CLIENT_ID", "devGOOGLE_CLIENT_ID")
		writeMapEntry(buf, "GOOGLE_CLIENT_SECRET", "devGOOGLE_CLIENT_SECRET")
	}

	if cfg.OAuthGitHub {
		writeMapEntry(buf, "GITHUB_CLIENT_ID", "devGITHUB_CLIENT_ID")
		writeMapEntry(buf, "GITHUB_CLIENT_SECRET", "devGITHUB_CLIENT_SECRET")
	}

	if cfg.OAuthGoogle || cfg.OAuthGitHub {
		writeMapEntry(buf, "OAUTH_REDIRECT_URL", "devOAUTH_REDIRECT_URL")
		writeMapEntry(buf, "OAUTH_REDIRECT_BASE_URL", "devOAUTH_REDIRECT_BASE_URL")
	}

	if cfg.EmailEnabled {
		writeMapEntry(buf, "SMTP_HOST", "devSMTP_HOST")
		writeMapEntry(buf, "SMTP_PORT", "devSMTP_PORT")
		writeMapEntry(buf, "SMTP_USERNAME", "devSMTP_USERNAME")
		writeMapEntry(buf, "SMTP_PASSWORD", "devSMTP_PASSWORD")
		writeMapEntry(buf, "APP_URL", "devAPP_URL")
	}

	for _, ev := range cfg.CustomEnvVars {
		writeMapEntry(buf, ev.Name, "dev"+ev.Name)
	}

	buf.WriteString("}\n\n")

	// loadWithDefaults function
	buf.WriteString(`func loadWithDefaults(s any) {
	val := reflect.ValueOf(s).Elem()
	for i := range val.NumField() {
		name := val.Type().Field(i).Name
		envVar := os.Getenv(name)
		if envVar != "" {
			val.Field(i).SetString(envVar)
			continue
		}
		if def, ok := devDefaultMap[name]; ok && def != "" {
			val.Field(i).SetString(def)
			continue
		}
		fmt.Fprintf(os.Stderr, "WARNING: environment variable %s is not set and has no dev default\n", name)
	}
}

`)
}

// writeConst writes a single const line: const name = "value"
func writeConst(buf *bytes.Buffer, name, value string) {
	buf.WriteString(fmt.Sprintf("const %s = %q\n", name, value))
}

// writeMapEntry writes a single map entry: "KEY": constName,
func writeMapEntry(buf *bytes.Buffer, key, constName string) {
	buf.WriteString(fmt.Sprintf("\t%q: %s,\n", key, constName))
}

// generateSettingsConfig writes the SettingsConfig struct and Make method.
// It uses embedded structs for required vs. optional env vars so that adding
// a new field to either group is a one-line change — no duplication needed.
func generateSettingsConfig(buf *bytes.Buffer, cfg ConfigGenConfig) {
	// Required config: program exits if any of these are missing.
	buf.WriteString(`// RequiredConfig contains environment variables that must be set.
// The program will exit if any of these are missing.
type RequiredConfig struct {
	DATABASE_URL  string
	PORT          string
	GO_ENV        string
	COOKIE_SECRET string
`)
	if cfg.FilesEnabled {
		buf.WriteString("\tS3_BUCKET             string\n")
		buf.WriteString("\tS3_REGION             string\n")
		buf.WriteString("\tAWS_ACCESS_KEY_ID     string\n")
		buf.WriteString("\tAWS_SECRET_ACCESS_KEY string\n")
	}
	if cfg.WorkersEnabled {
		buf.WriteString("\tREDIS_URL             string\n")
		buf.WriteString("\tCENTRIFUGO_API_URL    string\n")
		buf.WriteString("\tCENTRIFUGO_API_KEY    string\n")
		buf.WriteString("\tCENTRIFUGO_HMAC_SECRET string\n")
		buf.WriteString("\tCENTRIFUGO_WS_URL     string\n")
	}
	if cfg.OAuthGoogle {
		buf.WriteString("\tGOOGLE_CLIENT_ID     string\n")
		buf.WriteString("\tGOOGLE_CLIENT_SECRET string\n")
	}
	if cfg.OAuthGitHub {
		buf.WriteString("\tGITHUB_CLIENT_ID     string\n")
		buf.WriteString("\tGITHUB_CLIENT_SECRET string\n")
	}
	if cfg.EmailEnabled {
		buf.WriteString("\tSMTP_HOST     string\n")
		buf.WriteString("\tSMTP_PORT     string\n")
		buf.WriteString("\tSMTP_USERNAME string\n")
		buf.WriteString("\tSMTP_PASSWORD string\n")
		buf.WriteString("\tAPP_URL       string\n")
	}
	for _, ev := range cfg.CustomEnvVars {
		if ev.Required {
			buf.WriteString(fmt.Sprintf("\t%s string\n", ev.Name))
		}
	}
	buf.WriteString("}\n\n")

	// Optional config: program warns but continues if these are missing.
	buf.WriteString(`// OptionalConfig contains environment variables that have sensible defaults.
// A warning is printed for any that are not set.
type OptionalConfig struct {
`)
	if cfg.FilesEnabled {
		buf.WriteString("\tS3_ENDPOINT            string\n")
		buf.WriteString("\tMAX_UPLOAD_SIZE_MB     string\n")
		buf.WriteString("\tMULTIPART_THRESHOLD_MB string\n")
	}
	if cfg.WorkersEnabled {
		// No optional worker config fields currently, but the block is here
		// for future additions (e.g., WORKER_CONCURRENCY).
	}
	if cfg.OAuthGoogle || cfg.OAuthGitHub {
		buf.WriteString("\tOAUTH_REDIRECT_URL      string\n")
		buf.WriteString("\tOAUTH_REDIRECT_BASE_URL string\n")
	}
	for _, ev := range cfg.CustomEnvVars {
		if !ev.Required {
			buf.WriteString(fmt.Sprintf("\t%s string\n", ev.Name))
		}
	}
	buf.WriteString("}\n\n")

	// Composite config with Make()
	buf.WriteString(`// SettingsConfig contains all environment variables used to configure the API.
type SettingsConfig struct {
	RequiredConfig
	OptionalConfig
}

// Make populates the SettingsConfig from the environment.
// Required fields cause a fatal exit if missing; optional fields emit a warning.
func (s *SettingsConfig) Make() {
	loadRequired(&s.RequiredConfig)
	loadOptional(&s.OptionalConfig)
}

`)
}

// generateGlobalVars writes the global Settings and Logger variables.
func generateGlobalVars(buf *bytes.Buffer) {
	buf.WriteString(`// Settings contains the actual values for the environment variables.
var Settings SettingsConfig

// Logger contains the logger for the application.
var Logger *slog.Logger

`)
}

// generateGetLogger writes the getLogger function.
func generateGetLogger(buf *bytes.Buffer) {
	buf.WriteString(`// getLogger returns the appropriate logger based on the environment.
func getLogger(env string) *slog.Logger {
	if env == "development" {
		return logging.DevLogger
	}
	return logging.ProdLogger
}

`)
}

// generateInit writes the init function with dev-mode bifurcation.
func generateInit(buf *bytes.Buffer, cfg ConfigGenConfig) {
	buf.WriteString(`func init() {
	goEnv := os.Getenv("GO_ENV")
	if goEnv == "production" {
		// Strict mode: all required env vars must be set.
		Settings.Make()
	} else {
		// Dev mode: fall back to compile-time defaults from shipq.ini.
		loadWithDefaults(&Settings.RequiredConfig)
		loadWithDefaults(&Settings.OptionalConfig)
		if goEnv == "" {
			Settings.GO_ENV = "development"
		}
		fmt.Fprintln(os.Stderr, "shipq: dev mode — using compile-time defaults from shipq.ini (set GO_ENV=production to require env vars)")
	}
	Logger = getLogger(Settings.GO_ENV)
}
`)
}

