func TestGenerateConfig_CustomEnvVars_RequiredInRequiredConfig(t *testing.T) {
	cfg := ConfigGenConfig{
		ModulePath: "example.com/myapp",
		Dialect:    "sqlite",
		DevDefaults: DevDefaults{
			DatabaseURL:  "sqlite:///tmp/myapp.db",
			Port:         "8080",
			CookieSecret: "dev-secret",
		},
		CustomEnvVars: []CustomEnvVar{
			{Name: "OPENAI_API_KEY", Required: true},
			{Name: "ANTHROPIC_API_KEY", Required: false},
		},
	}

	code, err := GenerateConfig(cfg)
	if err != nil {
		t.Fatalf("GenerateConfig() error = %v", err)
	}
	codeStr := string(code)

	// Required custom env var should be in RequiredConfig
	// Find the RequiredConfig struct
	reqIdx := strings.Index(codeStr, "type RequiredConfig struct")
	optIdx := strings.Index(codeStr, "type OptionalConfig struct")
	if reqIdx < 0 {
		t.Fatal("missing RequiredConfig struct")
	}
	if optIdx < 0 {
		t.Fatal("missing OptionalConfig struct")
	}

	reqSection := codeStr[reqIdx:optIdx]
	// Find the closing brace of OptionalConfig to avoid matching devDefaultMap entries
	settingsIdx := strings.Index(codeStr, "type SettingsConfig struct")
	if settingsIdx < 0 {
		t.Fatal("missing SettingsConfig struct")
	}
	optSection := codeStr[optIdx:settingsIdx]

	// OPENAI_API_KEY is required → should be a field in RequiredConfig
	if !strings.Contains(reqSection, "\tOPENAI_API_KEY string") {
		t.Error("expected OPENAI_API_KEY field in RequiredConfig")
	}
	// OPENAI_API_KEY should NOT be a field in OptionalConfig
	if strings.Contains(optSection, "\tOPENAI_API_KEY string") {
		t.Error("OPENAI_API_KEY should not be in OptionalConfig")
	}
}

