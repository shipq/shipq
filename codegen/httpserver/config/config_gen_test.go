func TestGenerateConfig_FilesEnabled(t *testing.T) {
	cfg := ConfigGenConfig{
		ModulePath:   "example.com/myapp",
		Dialect:      "sqlite",
		FilesEnabled: true,
		DevDefaults: DevDefaults{
			DatabaseURL:          "sqlite:///tmp/myapp.db",
			Port:                 "8080",
			CookieSecret:         "dev-secret",
			S3Bucket:             "shipq-dev",
			S3Region:             "us-east-1",
			S3Endpoint:           "http://localhost:9000",
			AWSAccessKeyID:       "minioadmin",
			AWSSecretAccessKey:   "minioadmin",
			MaxUploadSizeMB:      "100",
			MultipartThresholdMB: "10",
		},
	}

	code, err := GenerateConfig(cfg)
	if err != nil {
		t.Fatalf("GenerateConfig() error = %v", err)
	}

	// Verify it's valid Go
	_, err = parser.ParseFile(token.NewFileSet(), "", code, parser.AllErrors)
	if err != nil {
		t.Errorf("generated code is not valid Go: %v\n%s", err, string(code))
	}

	codeStr := string(code)

	// Required S3 fields should be in RequiredConfig
	if !strings.Contains(codeStr, "type RequiredConfig struct") {
		t.Error("missing RequiredConfig struct")
	}
	for _, field := range []string{"S3_BUCKET", "S3_REGION", "AWS_ACCESS_KEY_ID", "AWS_SECRET_ACCESS_KEY"} {
		if !strings.Contains(codeStr, field) {
			t.Errorf("missing required S3 field %s", field)
		}
	}

	// Optional S3 fields should be in OptionalConfig
	if !strings.Contains(codeStr, "type OptionalConfig struct") {
		t.Error("missing OptionalConfig struct")
	}
	for _, field := range []string{"S3_ENDPOINT", "MAX_UPLOAD_SIZE_MB", "MULTIPART_THRESHOLD_MB"} {
		if !strings.Contains(codeStr, field) {
			t.Errorf("missing optional S3 field %s", field)
		}
	}

	// SettingsConfig should embed both
	if !strings.Contains(codeStr, "RequiredConfig") || !strings.Contains(codeStr, "OptionalConfig") {
		t.Error("SettingsConfig should embed RequiredConfig and OptionalConfig")
	}

	// Make should use loadRequired and loadOptional
	if !strings.Contains(codeStr, "loadRequired(&s.RequiredConfig)") {
		t.Error("Make should call loadRequired")
	}
	if !strings.Contains(codeStr, "loadOptional(&s.OptionalConfig)") {
		t.Error("Make should call loadOptional")
	}
}

