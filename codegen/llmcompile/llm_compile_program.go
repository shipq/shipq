package llmcompile

import (
	"bytes"
	"fmt"
	"go/format"
	"sort"
)

// LLMCompileProgramConfig holds configuration for generating the LLM
// compile program.
type LLMCompileProgramConfig struct {
	ModulePath string   // e.g., "myapp" or "github.com/company/monorepo/services/myservice"
	ToolPkgs   []string // import paths to tool packages with Register(app *llm.App) functions
}

// GenerateLLMCompileProgram generates the temporary program that imports all
// tool packages, calls their Register() functions, and serializes tool metadata
// (name, description, JSON Schema, input/output types, package path) to stdout
// as JSON.
//
// This is the LLM equivalent of channelcompile.GenerateChannelCompileProgram.
func GenerateLLMCompileProgram(cfg LLMCompileProgramConfig) ([]byte, error) {
	var buf bytes.Buffer

	// Sort for deterministic output.
	pkgs := make([]string, len(cfg.ToolPkgs))
	copy(pkgs, cfg.ToolPkgs)
	sort.Strings(pkgs)

	llmPkg := cfg.ModulePath + "/shipq/lib/llm"

	buf.WriteString(`// Code generated by shipq. This file can be run manually for debugging.
//
// To run manually:
//   cd <project_root>
//   go run .shipq/llm_compile/main.go
//
// Output: JSON array of tool definitions to stdout
// Errors: Written to stderr
//
package main

import (
	"encoding/json"
	"fmt"
	"os"

`)
	fmt.Fprintf(&buf, "\t%q\n", llmPkg)

	// Write imports for tool packages.
	if len(pkgs) > 0 {
		buf.WriteString("\n\t// Tool packages - importing to call their Register functions\n")
		for i, pkg := range pkgs {
			fmt.Fprintf(&buf, "\ttoolpkg%d %q\n", i, pkg)
		}
	}

	buf.WriteString(`)

// toolMeta is a JSON-serializable representation of a single tool's metadata.
type toolMeta struct {
	Name        string          ` + "`json:\"name\"`" + `
	Description string          ` + "`json:\"description\"`" + `
	InputSchema json.RawMessage ` + "`json:\"input_schema\"`" + `
	InputType   string          ` + "`json:\"input_type\"`" + `
	OutputType  string          ` + "`json:\"output_type\"`" + `
	Package     string          ` + "`json:\"package\"`" + `
}

func main() {
	app := llm.NewApp()

	// Call all Register functions
`)

	for i := range pkgs {
		fmt.Fprintf(&buf, "\ttoolpkg%d.Register(app)\n", i)
	}

	buf.WriteString(`
	// Serialize tool metadata to stdout as JSON.
	var tools []toolMeta
	for _, t := range app.Tools {
		tools = append(tools, toolMeta{
			Name:        t.Name,
			Description: t.Description,
			InputSchema: t.InputSchema,
			InputType:   t.InputType,
			OutputType:  t.OutputType,
			Package:     t.Package,
		})
	}

	// Use an empty array instead of null when there are no tools.
	if tools == nil {
		tools = []toolMeta{}
	}

	enc := json.NewEncoder(os.Stdout)
	enc.SetIndent("", "  ")
	if err := enc.Encode(tools); err != nil {
		fmt.Fprintf(os.Stderr, "error: %v\n", err)
		os.Exit(1)
	}
}
`)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return unformatted source for debugging.
		return buf.Bytes(), fmt.Errorf("failed to format llm compile program: %w", err)
	}

	return formatted, nil
}
