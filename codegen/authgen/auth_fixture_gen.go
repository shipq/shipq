package authgen

import (
	"bytes"
	"fmt"
	"go/format"
)

// GenerateAccountFixture generates api/accounts/fixture/fixture.go.
// Unlike resource fixtures that use HTTP, this fixture creates an account
// directly via the query runner since accounts are auth-managed tables
// without public CRUD routes. It depends on the organization fixture
// because accounts have a non-nullable FK to organizations via
// default_organization_id.
func GenerateAccountFixture(cfg AuthGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by shipq.\n")
	buf.WriteString("package fixture\n\n")

	buf.WriteString("import (\n")
	buf.WriteString("\t\"context\"\n")
	buf.WriteString("\t\"database/sql\"\n")
	buf.WriteString("\t\"testing\"\n\n")
	fmt.Fprintf(&buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/crypto")
	fmt.Fprintf(&buf, "\t%q\n", cfg.ModulePath+"/shipq/lib/nanoid")
	fmt.Fprintf(&buf, "\t%q\n", cfg.ModulePath+"/shipq/queries")
	fmt.Fprintf(&buf, "\tdbrunner %q\n", cfg.ModulePath+"/shipq/queries/"+cfg.Dialect)
	fmt.Fprintf(&buf, "\torganizationfixture %q\n", cfg.ModulePath+"/api/organizations/fixture")
	buf.WriteString(")\n\n")

	buf.WriteString("// Create creates a test account via direct DB insert and returns the result.\n")
	buf.WriteString("// It also creates a test organization (via organizationfixture) to satisfy the FK.\n")
	buf.WriteString("func Create(t *testing.T, ctx context.Context, tx *sql.Tx) *queries.SignupCreateAccountResult {\n")
	buf.WriteString("\tt.Helper()\n")
	buf.WriteString("\trunner := dbrunner.NewQueryRunner(tx)\n\n")
	buf.WriteString("\torg := organizationfixture.Create(t, ctx, tx)\n\n")
	buf.WriteString("\tpasswordHash, err := crypto.HashPassword(\"password\")\n")
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString("\t\tt.Fatalf(\"fixture: failed to hash password: %v\", err)\n")
	buf.WriteString("\t}\n\n")
	buf.WriteString("\tresult, err := runner.SignupCreateAccount(ctx, queries.SignupCreateAccountParams{\n")
	buf.WriteString("\t\tPublicId:              nanoid.New(),\n")
	buf.WriteString("\t\tFirstName:             \"Test\",\n")
	buf.WriteString("\t\tLastName:              \"User\",\n")
	buf.WriteString("\t\tEmail:                 nanoid.New() + \"@test.com\",\n")
	buf.WriteString("\t\tPasswordHash:          passwordHash,\n")
	buf.WriteString("\t\tDefaultOrganizationId: org.Id,\n")
	buf.WriteString("\t})\n")
	buf.WriteString("\tif err != nil {\n")
	buf.WriteString("\t\tt.Fatalf(\"fixture: failed to create account: %v\", err)\n")
	buf.WriteString("\t}\n")
	buf.WriteString("\treturn result\n")
	buf.WriteString("}\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format account fixture: %w\nunformatted:\n%s", err, buf.String())
	}
	return formatted, nil
}

