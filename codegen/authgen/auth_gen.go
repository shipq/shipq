// writeFindAccountByInternalIDQuery writes the FindAccountByInternalID query
// definition. The query LEFT JOINs account_roles and roles to aggregate the
// account's roles as a JSON array. Both scoped and unscoped variants use the
// same role columns (name, description) since the JSON key collision issue
// prevents including organization columns from a different joined table.
func writeFindAccountByInternalIDQuery(buf *bytes.Buffer, scopeColumn string) {
	_ = scopeColumn // both variants use the same query shape
	buf.WriteString("\tquery.MustDefineOne(\"FindAccountByInternalID\",\n")
	buf.WriteString("\t\tquery.From(schema.Accounts).\n")

	// LEFT JOIN account_roles (filtering soft-deleted)
	buf.WriteString("\t\t\tLeftJoin(schema.AccountRoles).On(query.And(\n")
	buf.WriteString("\t\t\t\tschema.AccountRoles.AccountId().Eq(schema.Accounts.Id()),\n")
	buf.WriteString("\t\t\t\tschema.AccountRoles.DeletedAt().IsNull(),\n")
	buf.WriteString("\t\t\t)).\n")

	// LEFT JOIN roles (filtering soft-deleted)
	buf.WriteString("\t\t\tLeftJoin(schema.Roles).On(query.And(\n")
	buf.WriteString("\t\t\t\tschema.Roles.Id().Eq(schema.AccountRoles.RoleId()),\n")
	buf.WriteString("\t\t\t\tschema.Roles.DeletedAt().IsNull(),\n")
	buf.WriteString("\t\t\t)).\n")

	// SELECT account columns
	buf.WriteString("\t\t\tSelect(\n")
	buf.WriteString("\t\t\t\tschema.Accounts.Id(),\n")
	buf.WriteString("\t\t\t\tschema.Accounts.PublicId(),\n")
	buf.WriteString("\t\t\t\tschema.Accounts.Email(),\n")
	buf.WriteString("\t\t\t\tschema.Accounts.FirstName(),\n")
	buf.WriteString("\t\t\t\tschema.Accounts.LastName(),\n")
	buf.WriteString("\t\t\t\tschema.Accounts.DefaultOrganizationId(),\n")
	buf.WriteString("\t\t\t).\n")

	// SelectJSONAgg for roles (name and description)
	buf.WriteString("\t\t\tSelectJSONAgg(\"roles\", schema.Roles.Name(), schema.Roles.Description()).\n")

	// WHERE + GROUP BY
	buf.WriteString("\t\t\tWhere(query.And(\n")
	buf.WriteString("\t\t\t\tschema.Accounts.Id().Eq(query.Param[int64](\"id\")),\n")
	buf.WriteString("\t\t\t\tschema.Accounts.DeletedAt().IsNull(),\n")
	buf.WriteString("\t\t\t)).\n")
	buf.WriteString("\t\t\tGroupBy(schema.Accounts.Id()).\n")
	buf.WriteString("\t\t\tBuild())\n\n")
}

// formatSource formats Go source code, returning the original if formatting fails.
func formatSource(src []byte) ([]byte, error) {
	formatted, err := format.Source(src)
	if err != nil {
		return src, fmt.Errorf("failed to format source: %w\n%s", err, src)
	}
	return formatted, nil
}
