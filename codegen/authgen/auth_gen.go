// writeCheckRBACQuery writes the CheckRBAC query definition.
// When scopeColumn is set, the query filters role_actions through roles.organization_id.
// When scopeColumn is empty, roles and role_actions are global.
func writeCheckRBACQuery(buf *bytes.Buffer, scopeColumn string) {
	if scopeColumn != "" {
		writeCheckRBACQueryScoped(buf)
	} else {
		writeCheckRBACQueryUnscoped(buf)
	}
}

func writeCheckRBACQueryUnscoped(buf *bytes.Buffer) {
	// Subquery 1: Is this account a GLOBAL_OWNER?
	buf.WriteString("\t// Subquery 1: Is this account a GLOBAL_OWNER?\n")
	buf.WriteString("\tisOwnerSub := query.From(schema.Roles).\n")
	buf.WriteString("\t\tJoin(schema.AccountRoles).On(schema.AccountRoles.RoleId().Eq(schema.Roles.Id())).\n")
	buf.WriteString("\t\tSelect(schema.Roles.Id()).\n")
	buf.WriteString("\t\tWhere(query.And(\n")
	buf.WriteString("\t\t\tschema.AccountRoles.AccountId().Eq(query.Param[int64](\"account_id\")),\n")
	buf.WriteString("\t\t\tschema.Roles.Name().Eq(query.Literal(\"GLOBAL_OWNER\")),\n")
	buf.WriteString("\t\t\tschema.AccountRoles.DeletedAt().IsNull(),\n")
	buf.WriteString("\t\t\tschema.Roles.DeletedAt().IsNull(),\n")
	buf.WriteString("\t\t))\n\n")

	// Subquery 2: Is this route restricted by any role?
	buf.WriteString("\t// Subquery 2: Is this route restricted by any role?\n")
	buf.WriteString("\tisRestrictedSub := query.From(schema.RoleActions).\n")
	buf.WriteString("\t\tJoin(schema.Roles).On(schema.Roles.Id().Eq(schema.RoleActions.RoleId())).\n")
	buf.WriteString("\t\tSelect(schema.RoleActions.Id()).\n")
	buf.WriteString("\t\tWhere(query.And(\n")
	buf.WriteString("\t\t\tschema.RoleActions.RoutePath().Eq(query.Param[string](\"route_path\")),\n")
	buf.WriteString("\t\t\tquery.Or(\n")
	buf.WriteString("\t\t\t\tschema.RoleActions.Method().Eq(query.Param[string](\"method\")),\n")
	buf.WriteString("\t\t\t\tschema.RoleActions.Method().Eq(query.Literal(\"*\")),\n")
	buf.WriteString("\t\t\t),\n")
	buf.WriteString("\t\t\tschema.RoleActions.DeletedAt().IsNull(),\n")
	buf.WriteString("\t\t\tschema.Roles.DeletedAt().IsNull(),\n")
	buf.WriteString("\t\t))\n\n")

	// Subquery 3: Does this account have permission?
	buf.WriteString("\t// Subquery 3: Does this account have permission for this route?\n")
	buf.WriteString("\thasPermSub := query.From(schema.RoleActions).\n")
	buf.WriteString("\t\tJoin(schema.Roles).On(schema.Roles.Id().Eq(schema.RoleActions.RoleId())).\n")
	buf.WriteString("\t\tJoin(schema.AccountRoles).On(schema.AccountRoles.RoleId().Eq(schema.Roles.Id())).\n")
	buf.WriteString("\t\tSelect(schema.RoleActions.Id()).\n")
	buf.WriteString("\t\tWhere(query.And(\n")
	buf.WriteString("\t\t\tschema.AccountRoles.AccountId().Eq(query.Param[int64](\"account_id\")),\n")
	buf.WriteString("\t\t\tschema.RoleActions.RoutePath().Eq(query.Param[string](\"route_path\")),\n")
	buf.WriteString("\t\t\tquery.Or(\n")
	buf.WriteString("\t\t\t\tschema.RoleActions.Method().Eq(query.Param[string](\"method\")),\n")
	buf.WriteString("\t\t\t\tschema.RoleActions.Method().Eq(query.Literal(\"*\")),\n")
	buf.WriteString("\t\t\t),\n")
	buf.WriteString("\t\t\tschema.AccountRoles.DeletedAt().IsNull(),\n")
	buf.WriteString("\t\t\tschema.RoleActions.DeletedAt().IsNull(),\n")
	buf.WriteString("\t\t\tschema.Roles.DeletedAt().IsNull(),\n")
	buf.WriteString("\t\t))\n\n")

	// Main query: three EXISTS booleans
	buf.WriteString("\tquery.MustDefineOne(\"CheckRBAC\",\n")
	buf.WriteString("\t\tquery.From(schema.Roles).\n")
	buf.WriteString("\t\t\tSelectExprAs(query.Exists(isOwnerSub), \"is_global_owner\").\n")
	buf.WriteString("\t\t\tSelectExprAs(query.Exists(isRestrictedSub), \"route_is_restricted\").\n")
	buf.WriteString("\t\t\tSelectExprAs(query.Exists(hasPermSub), \"has_permission\").\n")
	buf.WriteString("\t\t\tLimit(query.Literal(1)).\n")
	buf.WriteString("\t\t\tBuild())\n\n")
}

