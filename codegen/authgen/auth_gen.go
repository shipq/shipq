// writeMeRoleInfoStruct writes the RoleInfo struct.
// Both scoped and unscoped variants use the same fields (name, description)
// since the JSON aggregation only includes role columns.
func writeMeRoleInfoStruct(buf *bytes.Buffer, scopeColumn string) {
	_ = scopeColumn // same struct for both variants
	buf.WriteString("// RoleInfo contains role information for the current user.\n")
	buf.WriteString("type RoleInfo struct {\n")
	buf.WriteString("\tName        string  `json:\"name\"`\n")
	buf.WriteString("\tDescription *string `json:\"description,omitempty\"`\n")
	buf.WriteString("}\n\n")
}

// writeMeHandlerFunc writes the Me handler function body.
// Both scoped and unscoped variants map the same json_agg columns (name, description).
func writeMeHandlerFunc(buf *bytes.Buffer, scopeColumn string) {
	buf.WriteString(`// Me handles GET /me
func Me(ctx context.Context, req *MeRequest) (*MeResponse, error) {
	runner := queries.RunnerFromContext(ctx)

	// Get current session
	session, err := getCurrentSession(ctx, runner)
	if err != nil {
		return nil, httperror.Unauthorized("not logged in")
	}

	// Get account from session (includes aggregated roles)
	account, err := runner.FindAccountByInternalID(ctx, queries.FindAccountByInternalIDParams{
		Id: session.AccountId,
	})
	if err != nil {
		return nil, httperror.Wrap(500, "failed to get account", err)
	}
	if account == nil {
		return nil, httperror.Unauthorized("account not found")
	}

	// Map roles from query result
	roles := make([]RoleInfo, 0, len(account.Roles))
	for _, r := range account.Roles {
		roles = append(roles, RoleInfo{
			Name:        r.Name,
			Description: r.Description,
		})
	}

	resp := &MeResponse{
		ID:        account.PublicId,
		Email:     account.Email,
		FirstName: account.FirstName,
		LastName:  account.LastName,
		Roles:     roles,
	}
`)

	// Include default organization if scoped
	if scopeColumn != "" {
		buf.WriteString(`
	// Include default organization if set
	if account.DefaultOrganizationId != nil {
		org, err := runner.FindOrganizationByInternalID(ctx, queries.FindOrganizationByInternalIDParams{
			Id: *account.DefaultOrganizationId,
		})
		if err == nil && org != nil {
			description := ""
			if org.Description != nil {
				description = *org.Description
			}
			resp.Organization = &OrgInfo{
				ID:          org.PublicId,
				Name:        org.Name,
				Description: description,
			}
		}
	}
`)
	}

	buf.WriteString(`
	return resp, nil
}
`)
}

// GenerateSignupHandler generates api/auth/signup.go
func GenerateSignupHandler(cfg AuthGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString(generatedFileHeader)
	buf.WriteString("package auth\n\n")

	// Imports
	buf.WriteString("import (\n")
	buf.WriteString("\t\"context\"\n")
	buf.WriteString("\t\"fmt\"\n")
	if cfg.EmailEnabled {
		buf.WriteString("\t\"os\"\n")
	}
	buf.WriteString("\t\"time\"\n\n")
	if cfg.EmailEnabled {
		buf.WriteString("\t\"" + cfg.ModulePath + "/config\"\n")
	}
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/lib/crypto\"\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/lib/httperror\"\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/lib/nanoid\"\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/queries\"\n")
	buf.WriteString(")\n\n")

	// Request struct
	buf.WriteString(`// SignupRequest is the request body for signup.
type SignupRequest struct {
	Email     string ` + "`json:\"email\"`" + `
	Password  string ` + "`json:\"password\"`" + `
	FirstName string ` + "`json:\"first_name\"`" + `
	LastName  string ` + "`json:\"last_name\"`" + `
}

`)

	// Response struct
	buf.WriteString(`// SignupResponse is the response body after successful signup.
type SignupResponse struct {
	ID        string ` + "`json:\"id\"`" + `
	Email     string ` + "`json:\"email\"`" + `
	FirstName string ` + "`json:\"first_name\"`" + `
	LastName  string ` + "`json:\"last_name\"`" + `
}

`)

	// Handler function
	buf.WriteString(`// Signup handles POST /signup
func Signup(ctx context.Context, req *SignupRequest) (*SignupResponse, error) {
	runner := queries.RunnerFromContext(ctx)

	// Check if email is already taken
	existing, err := runner.FindAccountByEmail(ctx, queries.FindAccountByEmailParams{
		Email: req.Email,
	})
	if err != nil {
		return nil, httperror.Wrap(500, "failed to check email", err)
	}
	if existing != nil {
		return nil, httperror.Conflict("email already taken")
	}

	// Hash password
	passwordHash, err := crypto.HashPassword(req.Password)
	if err != nil {
		return nil, httperror.Wrap(500, "failed to hash password", err)
	}

	// Create organization with auto-generated name
	orgName := fmt.Sprintf("%s's Organization", req.FirstName)
	org, err := runner.SignupCreateOrganization(ctx, queries.SignupCreateOrganizationParams{
		PublicId:    nanoid.New(),
		Name:        orgName,
		Description: "",
	})
	if err != nil {
		return nil, httperror.Wrap(500, "failed to create organization", err)
	}

	// Create account linked to organization
	account, err := runner.SignupCreateAccount(ctx, queries.SignupCreateAccountParams{
		PublicId:              nanoid.New(),
		FirstName:             req.FirstName,
		LastName:              req.LastName,
		Email:                 req.Email,
		PasswordHash:          passwordHash,
		DefaultOrganizationId: org.Id,
	})
	if err != nil {
		return nil, httperror.Wrap(500, "failed to create account", err)
	}

	// Create organization_users link
	_, err = runner.SignupCreateOrganizationUser(ctx, queries.SignupCreateOrganizationUserParams{
		PublicId:       nanoid.New(),
		OrganizationId: org.Id,
		AccountId:      account.Id,
	})
	if err != nil {
		return nil, httperror.Wrap(500, "failed to link account to organization", err)
	}

	// Create session
	session, err := runner.SignupCreateSession(ctx, queries.SignupCreateSessionParams{
		PublicId:  nanoid.New(),
		AccountId: account.Id,
		ExpiresAt: time.Now().UTC().Add(14 * 24 * time.Hour).Format("2006-01-02 15:04:05"),
	})
	if err != nil {
		return nil, httperror.Wrap(500, "failed to create session", err)
	}

	// Set session cookie
	setSessionCookie(ctx, session.PublicId)
`)

	// When email verification is enabled, send a verification email after signup
	if cfg.EmailEnabled {
		buf.WriteString(`
	// Send verification email
	verifyToken, tokenErr := generateSecureToken(32)
	if tokenErr != nil {
		// Account is created but we couldn't generate a token — log and continue
		fmt.Fprintf(os.Stderr, "WARNING: failed to generate verification token: %v\n", tokenErr)
	} else {
		tokenHash := HashToken(verifyToken)

		_, tokenInsertErr := runner.InsertEmailVerificationToken(ctx, queries.InsertEmailVerificationTokenParams{
			PublicId:   nanoid.New(),
			AccountId:  account.Id,
			TokenHash:  tokenHash,
			ExpiresAt:  time.Now().UTC().Add(24 * time.Hour).Format("2006-01-02 15:04:05"),
		})
		if tokenInsertErr != nil {
			fmt.Fprintf(os.Stderr, "WARNING: failed to store verification token: %v\n", tokenInsertErr)
		} else {
			verifyURL := config.Settings.APP_URL + "/verify-email?token=" + verifyToken
			htmlBody := fmt.Sprintf(` + "`" + `<h2>Verify Your Email</h2>
<p>Welcome! Click the link below to verify your email address. This link expires in 24 hours.</p>
<p><a href="%s">Verify Email</a></p>` + "`" + `, verifyURL)

			sendParams := SendEmailParams{
				From:            "noreply@" + config.Settings.SMTP_HOST,
				To:              req.Email,
				Subject:         "Verify Your Email",
				HTMLBody:        htmlBody,
				SensitiveTokens: []string{verifyToken},
			}
			if sendErr := SendEmail(ctx, runner, sendParams); sendErr != nil {
				fmt.Fprintf(os.Stderr, "WARNING: failed to send verification email: %v\n", sendErr)
			}
		}
	}
`)
	}

	buf.WriteString(`
	return &SignupResponse{
		ID:        account.PublicId,
		Email:     req.Email,
		FirstName: req.FirstName,
		LastName:  req.LastName,
	}, nil
}
`)

	return formatSource(buf.Bytes())
}

// GenerateRegister generates api/auth/register.go
func GenerateRegister(cfg AuthGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString(generatedFileHeader)
	buf.WriteString("package auth\n\n")

	// Imports
	if len(cfg.OAuthProviders) > 0 {
		buf.WriteString("import (\n")
		buf.WriteString("\t\"net/http\"\n\n")
		buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/lib/handler\"\n")
		buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/queries\"\n")
		buf.WriteString(")\n\n")
	} else {
		buf.WriteString("import \"" + cfg.ModulePath + "/shipq/lib/handler\"\n\n")
	}

	// Register function
	buf.WriteString(`// Register registers all auth handlers with the app.
func Register(app *handler.App) {
	app.Post("/login", Login)
	app.Delete("/logout", Logout).Auth()
	app.Get("/me", Me).Auth()
`)

	if cfg.EmailEnabled {
		buf.WriteString("\tapp.Post(\"/forgot-password\", ForgotPassword)\n")
		buf.WriteString("\tapp.Post(\"/reset-password\", ResetPassword)\n")
		buf.WriteString("\tapp.Post(\"/verify-email\", VerifyEmail)\n")
		buf.WriteString("\tapp.Post(\"/resend-verification\", ResendVerification)\n")
	}

	buf.WriteString("}\n")

	// RegisterOAuthRoutes function (when OAuth providers are configured)
	if len(cfg.OAuthProviders) > 0 {
		writeRegisterOAuthRoutes(&buf, cfg.OAuthProviders)
	}

	return formatSource(buf.Bytes())
}

// GenerateSignupFiles generates the signup handler and an updated register.go
// that includes the /signup route. This is used by `shipq signup`, which is run
// separately after `shipq auth`.
func GenerateSignupFiles(cfg AuthGenConfig) (map[string][]byte, error) {
	files := make(map[string][]byte)

	signup, err := GenerateSignupHandler(cfg)
	if err != nil {
		return nil, fmt.Errorf("failed to generate signup.go: %w", err)
	}
	files["signup.go"] = signup

	register, err := GenerateSignupRegister(cfg)
	if err != nil {
		return nil, fmt.Errorf("failed to generate register.go: %w", err)
	}
	files["register.go"] = register

	return files, nil
}

