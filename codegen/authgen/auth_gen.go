// generatedFileHeader is the standard header for generated auth handler files.
const generatedFileHeader = "// Code generated by shipq.\n"

// AuthGenConfig holds configuration for generating auth handlers.
type AuthGenConfig struct {
	ModulePath      string   // e.g., "myapp" - the user's module path
	Dialect         string   // "postgres", "mysql", or "sqlite" — used for test generation
	TestDatabaseURL string   // test database URL derived from shipq.ini (e.g., "postgres://…/myapp_test")
	ScopeColumn     string   // from [db] scope in shipq.ini (e.g., "organization_id"), empty if unscoped
	OAuthProviders  []string // e.g., ["google", "github"] — OAuth providers enabled in shipq.ini
	SignupEnabled   bool     // true when `shipq signup` has been run — controls whether OAuth auto-creates accounts
	EmailEnabled    bool     // true when `shipq email` has been run — controls verified check + email routes
}

// GenerateAuthHandlerFiles generates all auth handler files.
// Returns a map of filenames to their contents.
func GenerateAuthHandlerFiles(cfg AuthGenConfig) (map[string][]byte, error) {
	files := make(map[string][]byte)

	generators := map[string]func(AuthGenConfig) ([]byte, error){
		"login.go":  GenerateLoginHandler,
		"logout.go": GenerateLogoutHandler,
		"me.go":     GenerateMeHandler,
		// signup.go removed -- generated by `shipq signup` instead
		"register.go": GenerateRegister,
		"helpers.go":  GenerateHelpers,
	}

	for filename, generator := range generators {
		content, err := generator(cfg)
		if err != nil {
			return nil, fmt.Errorf("failed to generate %s: %w", filename, err)
		}
		files[filename] = content
	}

	return files, nil
}

// GenerateLoginHandler generates api/auth/login.go
func GenerateLoginHandler(cfg AuthGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString(generatedFileHeader)
	buf.WriteString("package auth\n\n")

	// Imports
	buf.WriteString("import (\n")
	buf.WriteString("\t\"context\"\n")
	buf.WriteString("\t\"net/http\"\n")
	buf.WriteString("\t\"time\"\n\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/lib/crypto\"\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/lib/httperror\"\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/lib/httpserver\"\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/lib/nanoid\"\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/queries\"\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/config\"\n")
	buf.WriteString(")\n\n")

	// Request struct
	buf.WriteString(`// LoginRequest is the request body for login.
type LoginRequest struct {
	Email    string ` + "`json:\"email\"`" + `
	Password string ` + "`json:\"password\"`" + `
}

`)

	// Response struct
	buf.WriteString(`// LoginResponse is the response body after successful login.
type LoginResponse struct {
	ID        string ` + "`json:\"id\"`" + `
	Email     string ` + "`json:\"email\"`" + `
	FirstName string ` + "`json:\"first_name\"`" + `
	LastName  string ` + "`json:\"last_name\"`" + `
}

`)

	// Handler function
	buf.WriteString(`// Login handles POST /login
func Login(ctx context.Context, req *LoginRequest) (*LoginResponse, error) {
	runner := queries.RunnerFromContext(ctx)

	// Look up account by email
	account, err := runner.FindAccountByEmail(ctx, queries.FindAccountByEmailParams{
		Email: req.Email,
	})
	if err != nil {
		return nil, httperror.Wrap(500, "failed to lookup account", err)
	}
	if account == nil {
		return nil, httperror.Unauthorized("invalid email or password")
	}

	// Verify password
`)

	// password_hash is nullable — accounts without a password (e.g., OAuth-only)
	// have NULL password_hash. Guard against that before attempting verification.
	buf.WriteString(`	if account.PasswordHash == nil {
		return nil, httperror.New(401, "this account does not have a password")
	}
`)

	buf.WriteString(`	if !crypto.VerifyPassword(account.PasswordHash, req.Password) {
		return nil, httperror.Unauthorized("invalid email or password")
	}

`)

	// When email verification is enabled, reject unverified accounts
	if cfg.EmailEnabled {
		buf.WriteString(`	// Check email verification
	if !account.Verified {
		return nil, httperror.New(403, "email not verified")
	}

`)
	}

	buf.WriteString(`	// Create session with 2-week expiry
	session, err := runner.SignupCreateSession(ctx, queries.SignupCreateSessionParams{
		PublicId:  nanoid.New(),
		AccountId: account.Id,
		ExpiresAt: time.Now().UTC().Add(14 * 24 * time.Hour).Format("2006-01-02 15:04:05"),
	})
	if err != nil {
		return nil, httperror.Wrap(500, "failed to create session", err)
	}

	// Set session cookie
	setSessionCookie(ctx, session.PublicId)

	return &LoginResponse{
		ID:        account.PublicId,
		Email:     account.Email,
		FirstName: account.FirstName,
		LastName:  account.LastName,
	}, nil
}

// setSessionCookie sets the session cookie on the response.
func setSessionCookie(ctx context.Context, sessionID string) {
	secret := []byte(config.Settings.COOKIE_SECRET)
	signedValue := crypto.SignCookie(sessionID, secret)

	cookie := &http.Cookie{
		Name:     "session",
		Value:    signedValue,
		Path:     "/",
		HttpOnly: true,
		Secure:   config.Settings.GO_ENV != "development",
		SameSite: http.SameSiteLaxMode,
		MaxAge:   14 * 24 * 60 * 60, // 14 days
	}

	httpserver.SetCookie(ctx, cookie)
}
`)

	return formatSource(buf.Bytes())
}

// GenerateLogoutHandler generates api/auth/logout.go
func GenerateLogoutHandler(cfg AuthGenConfig) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString(generatedFileHeader)
	buf.WriteString("package auth\n\n")

	// Imports
	buf.WriteString("import (\n")
	buf.WriteString("\t\"context\"\n")
	buf.WriteString("\t\"net/http\"\n\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/lib/httperror\"\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/lib/httpserver\"\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/shipq/queries\"\n")
	buf.WriteString("\t\"" + cfg.ModulePath + "/config\"\n")
	buf.WriteString(")\n\n")

	// Request struct
	buf.WriteString(`// LogoutRequest is the request for logout (empty).
type LogoutRequest struct{}

`)

	// Response struct
	buf.WriteString(`// LogoutResponse is the response after successful logout.
type LogoutResponse struct {
	Success bool ` + "`json:\"success\"`" + `
}

`)

	// Handler function
	buf.WriteString(`// Logout handles DELETE /logout
func Logout(ctx context.Context, req *LogoutRequest) (*LogoutResponse, error) {
	runner := queries.RunnerFromContext(ctx)

	// Get current session
	session, err := getCurrentSession(ctx, runner)
	if err != nil {
		return nil, httperror.Unauthorized("not logged in")
	}

	// Soft-delete the session
	if _, err := runner.SoftDeleteSessionByPublicID(ctx, queries.SoftDeleteSessionByPublicIDParams{
		PublicId: session.PublicId,
	}); err != nil {
		return nil, httperror.Wrap(500, "failed to logout", err)
	}

	// Clear the session cookie
	clearSessionCookie(ctx)

	return &LogoutResponse{Success: true}, nil
}

// clearSessionCookie clears the session cookie.
func clearSessionCookie(ctx context.Context) {
	cookie := &http.Cookie{
		Name:     "session",
		Value:    "",
		Path:     "/",
		HttpOnly: true,
		Secure:   config.Settings.GO_ENV != "development",
		SameSite: http.SameSiteLaxMode,
		MaxAge:   -1, // Delete the cookie
	}

	httpserver.SetCookie(ctx, cookie)
}
`)

	return formatSource(buf.Bytes())
}

