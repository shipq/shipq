func writeTestRestrictedRouteAllowsUserWithMatchingRole(buf *bytes.Buffer, cfg RBACTestGenConfig) {
	buf.WriteString(`func TestRBAC_RestrictedRouteAllowsUserWithMatchingRole(t *testing.T) {
	ts := newRBACTestServer(t)
	ctx := context.Background()
	runner := dbrunner.NewQueryRunner(ts.Tx())

	cookie, accountPublicID, orgID := rbacCreateTestUser(t, ts, "admin@test.com", "Admin")

`)
	if cfg.ScopeColumn != "" {
		buf.WriteString(`	// Create a role scoped to this org
	adminRole, err := runner.CreateRole(ctx, queries.CreateRoleParams{
		PublicId:        nanoid.New(),
		Name:            "admin",
		OrganizationId:  &orgID,
	})
`)
	} else {
		buf.WriteString(`	// Create a role
	adminRole, err := runner.CreateRole(ctx, queries.CreateRoleParams{
		PublicId: nanoid.New(),
		Name:     "admin",
	})
	_ = orgID // unused in unscoped variant
`)
	}

	buf.WriteString(`	if err != nil {
		t.Fatalf("failed to create admin role: %v", err)
	}

	// Restrict /me GET to admin role
	_, err = runner.CreateRoleAction(ctx, queries.CreateRoleActionParams{
		PublicId:   nanoid.New(),
		RoleId:     adminRole.PublicId,
		RoutePath:  "/me",
		Method:     "GET",
	})
	if err != nil {
		t.Fatalf("failed to create role_action: %v", err)
	}

	// Assign admin role to this user
	_, err = runner.CreateAccountRole(ctx, queries.CreateAccountRoleParams{
		PublicId:  nanoid.New(),
		AccountId: accountPublicID,
		RoleId:    adminRole.PublicId,
	})
	if err != nil {
		t.Fatalf("failed to assign admin role: %v", err)
	}

	// User with matching role should have access
	req, _ := http.NewRequest("GET", ts.Server.URL+"/me", nil)
	req.AddCookie(&http.Cookie{Name: "session", Value: cookie})
	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		t.Fatalf("request failed: %v", err)
	}
	defer resp.Body.Close()
	if resp.StatusCode == http.StatusForbidden {
		t.Error("user with matching role should have access, but got 403")
	}
	if resp.StatusCode != http.StatusOK {
		t.Errorf("expected status 200, got %d", resp.StatusCode)
	}
}

`)
}

