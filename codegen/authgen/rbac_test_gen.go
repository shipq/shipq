func writeTestOrgScopedRolesDoNotCrossOrgs(buf *bytes.Buffer, cfg RBACTestGenConfig) {
	buf.WriteString(`func TestRBAC_OrgScopedRolesDoNotCrossOrgs(t *testing.T) {
	ts := newRBACTestServer(t)
	ctx := context.Background()
	runner := dbrunner.NewQueryRunner(ts.Tx())

	// Create two users in different orgs
	_, _, orgAID := rbacCreateTestUser(t, ts, "orgA@test.com", "OrgA")
	cookieB, _, orgBID := rbacCreateTestUser(t, ts, "orgB@test.com", "OrgB")

	// Create an "admin" role in Org A that restricts GET /me
	adminRoleA, err := runner.CreateRole(ctx, queries.CreateRoleParams{
		PublicId:        nanoid.New(),
		Name:            "admin",
		OrganizationId:  &orgAID,
	})
	if err != nil {
		t.Fatalf("failed to create admin role in org A: %v", err)
	}

	_, err = runner.CreateRoleAction(ctx, queries.CreateRoleActionParams{
		PublicId:   nanoid.New(),
		RoleId:     adminRoleA.PublicId,
		RoutePath:  "/me",
		Method:     "GET",
	})
	if err != nil {
		t.Fatalf("failed to create role_action for org A: %v", err)
	}

	// Create an "admin" role in Org B with NO role_actions (doesn't restrict /me)
	_, err = runner.CreateRole(ctx, queries.CreateRoleParams{
		PublicId:        nanoid.New(),
		Name:            "admin",
		OrganizationId:  &orgBID,
	})
	if err != nil {
		t.Fatalf("failed to create admin role in org B: %v", err)
	}

	// User B in Org B should still have access to /me, because Org B's roles
	// don't restrict it -- even though Org A's "admin" role does.
	req, _ := http.NewRequest("GET", ts.Server.URL+"/me", nil)
	req.AddCookie(&http.Cookie{Name: "session", Value: cookieB})
	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		t.Fatalf("request failed: %v", err)
	}
	defer resp.Body.Close()
	if resp.StatusCode == http.StatusForbidden {
		t.Error("Org B user should not be affected by Org A's role_actions, but got 403")
	}
	if resp.StatusCode != http.StatusOK {
		t.Errorf("expected status 200, got %d", resp.StatusCode)
	}
}

`)
}
