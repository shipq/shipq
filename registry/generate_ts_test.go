// makeTestHandlers returns a minimal set of handlers sufficient to exercise
// the TypeScript generation paths.
func makeTestHandlers() []codegen.SerializedHandlerInfo {
	return []codegen.SerializedHandlerInfo{
		{
			Method:      "POST",
			Path:        "/posts",
			FuncName:    "CreatePost",
			PackagePath: "myapp/api/posts",
			RequireAuth: true,
			Request: &codegen.SerializedStructInfo{
				Name: "CreatePostRequest",
				Fields: []codegen.SerializedFieldInfo{
					{Name: "Title", Type: "string", JSONName: "title", Required: true},
				},
			},
			Response: &codegen.SerializedStructInfo{
				Name: "CreatePostResponse",
				Fields: []codegen.SerializedFieldInfo{
					{Name: "ID", Type: "string", JSONName: "id", Required: true},
				},
			},
		},
		{
			Method:      "GET",
			Path:        "/posts/:id",
			FuncName:    "GetPost",
			PackagePath: "myapp/api/posts",
			RequireAuth: false,
			PathParams:  []codegen.SerializedPathParam{{Name: "id", Position: 0}},
			Response: &codegen.SerializedStructInfo{
				Name: "GetPostResponse",
				Fields: []codegen.SerializedFieldInfo{
					{Name: "ID", Type: "string", JSONName: "id", Required: true},
					{Name: "Title", Type: "string", JSONName: "title", Required: true},
				},
			},
		},
	}
}

// assertFileExists is a test helper that verifies a file exists and returns its contents.
func assertFileExists(t *testing.T, path string) string {
	t.Helper()
	content, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("expected file %s to exist: %v", path, err)
	}
	return string(content)
}

// assertFileNotExist is a test helper that verifies a file does not exist.
func assertFileNotExist(t *testing.T, path string) {
	t.Helper()
	if _, err := os.Stat(path); err == nil {
		t.Errorf("expected file %s to NOT exist, but it does", path)
	}
}

func TestGenerateTS_BaseClientWritten(t *testing.T) {
	tmpDir := t.TempDir()
	tsDir := "ts-out"

	err := generateTypeScriptHTTPClient(CompileConfig{
		ShipqRoot:    tmpDir,
		Handlers:     makeTestHandlers(),
		TSHTTPOutput: tsDir,
	})
	if err != nil {
		t.Fatalf("generateTypeScriptHTTPClient failed: %v", err)
	}

	content := assertFileExists(t, filepath.Join(tmpDir, tsDir, "shipq-api.ts"))

	if !strings.Contains(content, "Code generated by shipq") {
		t.Error("base client should contain generated header")
	}
	if !strings.Contains(content, "createPost") {
		t.Error("base client should contain createPost function")
	}
	if !strings.Contains(content, "getPost") {
		t.Error("base client should contain getPost function")
	}
}

