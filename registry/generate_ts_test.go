func TestGenerateTS_OnlyReactWhenFrameworkIsReact(t *testing.T) {
	tmpDir := t.TempDir()
	tsDir := "ts-out"

	handlers := makeTestHandlers()

	// Write the base client so the directory exists
	if err := generateTypeScriptHTTPClient(CompileConfig{
		ShipqRoot:    tmpDir,
		Handlers:     handlers,
		TSHTTPOutput: tsDir,
	}); err != nil {
		t.Fatalf("generateTypeScriptHTTPClient failed: %v", err)
	}

	// Only react
	frameworks := ParseFrameworks("react")
	if HasFramework(frameworks, "react") {
		if err := generateTypeScriptReactHooks(CompileConfig{
			ShipqRoot:    tmpDir,
			Handlers:     handlers,
			TSHTTPOutput: tsDir,
		}); err != nil {
			t.Fatalf("generateTypeScriptReactHooks failed: %v", err)
		}
	}
	if HasFramework(frameworks, "svelte") {
		if err := generateTypeScriptSvelteHooks(CompileConfig{
			ShipqRoot:    tmpDir,
			Handlers:     handlers,
			TSHTTPOutput: tsDir,
		}); err != nil {
			t.Fatalf("generateTypeScriptSvelteHooks failed: %v", err)
		}
	}

	// React should exist
	assertFileExists(t, filepath.Join(tmpDir, tsDir, "shipq-api.ts"))
	assertFileExists(t, filepath.Join(tmpDir, tsDir, "react", "shipq-api.ts"))

	// Svelte should NOT exist
	assertFileNotExist(t, filepath.Join(tmpDir, tsDir, "svelte", "shipq-api.ts"))
}

