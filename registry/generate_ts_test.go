func TestGenerateTS_EmptyTSHTTPOutput_NothingGenerated(t *testing.T) {
	tmpDir := t.TempDir()

	// When TSHTTPOutput is empty, no TS files should be generated.
	// This mirrors the CompileRegistry guard: if cfg.TSHTTPOutput == "" { skip }
	tsDir := ""

	// Verify that writing to an empty tsDir would default to "." but the
	// guard in CompileRegistry should prevent this from being called at all.
	// We test that the individual generators handle empty tsDir gracefully:
	// they default to "." and write there.
	//
	// The real guard is in CompileRegistry, so here we just verify the
	// guard logic: TSHTTPOutput == "" means skip.
	cfg := CompileConfig{
		ShipqRoot:    tmpDir,
		Handlers:     makeTestHandlers(),
		TSHTTPOutput: tsDir,
	}

	if cfg.TSHTTPOutput != "" {
		t.Error("expected TSHTTPOutput to be empty for this test")
	}

	// No shipq-api.ts should be written because the caller skips generation
	assertFileNotExist(t, filepath.Join(tmpDir, "shipq-api.ts"))
	assertFileNotExist(t, filepath.Join(tmpDir, "react", "shipq-api.ts"))
	assertFileNotExist(t, filepath.Join(tmpDir, "svelte", "shipq-api.ts"))
}

