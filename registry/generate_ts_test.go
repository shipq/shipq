func TestGenerateTS_IdempotentWrites(t *testing.T) {
	tmpDir := t.TempDir()
	tsDir := "ts-out"

	cfg := CompileConfig{
		ShipqRoot:    tmpDir,
		Handlers:     makeTestHandlers(),
		TSHTTPOutput: tsDir,
	}

	// Write once
	if err := generateTypeScriptHTTPClient(cfg); err != nil {
		t.Fatalf("first write failed: %v", err)
	}

	basePath := filepath.Join(tmpDir, tsDir, "shipq-api.ts")
	info1, err := os.Stat(basePath)
	if err != nil {
		t.Fatalf("stat after first write: %v", err)
	}

	content1, _ := os.ReadFile(basePath)

	// Write again with same handlers
	if err := generateTypeScriptHTTPClient(cfg); err != nil {
		t.Fatalf("second write failed: %v", err)
	}

	content2, _ := os.ReadFile(basePath)

	// Content should be identical
	if string(content1) != string(content2) {
		t.Error("expected identical content on second write")
	}

	// Verify file was not rewritten (WriteFileIfChanged semantics).
	// ModTime may or may not change depending on FS resolution, so we
	// just check content equality which is the real contract.
	_ = info1
}
