// EncodeFilesCursor encodes a cursor to a base64 string.
func EncodeFilesCursor(cursor *FilesCursor) string {
	if cursor == nil {
		return ""
	}
	data, _ := json.Marshal(cursor)
	return base64.StdEncoding.EncodeToString(data)
}

// DecodeFilesCursor decodes a base64 string to a cursor.
func DecodeFilesCursor(encoded string) *FilesCursor {
	if encoded == "" {
		return nil
	}
	data, err := base64.StdEncoding.DecodeString(encoded)
	if err != nil {
		return nil
	}
	var cursor FilesCursor
	if err := json.Unmarshal(data, &cursor); err != nil {
		return nil
	}
	return &cursor
}

