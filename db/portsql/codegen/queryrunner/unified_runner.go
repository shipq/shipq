func isSQLiteSpecialResultGoType(goType string) bool {
	return goType == "time.Time" || goType == "*time.Time" || goType == "json.RawMessage"
}

func writeSQLiteScanHelpers(buf *bytes.Buffer) {
	buf.WriteString(`// parseSQLiteTime parses SQLite datetime/timestamp text values.
func parseSQLiteTime(s string) (time.Time, error) {
	formats := []string{
		"2006-01-02 15:04:05",
		"2006-01-02 15:04:05.000",
		"2006-01-02T15:04:05",
		"2006-01-02T15:04:05Z",
		time.RFC3339,
		time.RFC3339Nano,
	}
	for _, format := range formats {
		if t, err := time.Parse(format, s); err == nil {
			return t, nil
		}
	}
	return time.Time{}, fmt.Errorf("invalid sqlite datetime value %q", s)
}

func parseSQLiteNullTime(ns sql.NullString) (*time.Time, error) {
	if !ns.Valid || ns.String == "" {
		return nil, nil
	}
	t, err := parseSQLiteTime(ns.String)
	if err != nil {
		return nil, err
	}
	return &t, nil
}

`)
}
