func collectRunnerImports(cfg UnifiedRunnerConfig, queries []userQueryInfo) map[string]bool {
	imports := make(map[string]bool)

	// Always needed
	imports["context"] = true
	imports["database/sql"] = true

	// Types package import
	imports[cfg.ModulePath+"/shipq/queries"] = true

	// SQLite needs extra imports for scan helpers (parseSQLiteTime, etc.)
	// Non-SQLite dialects reference time.Time and json.RawMessage only via the
	// shared queries package types, so the runner itself doesn't import them.
	// Note: json.RawMessage columns in SQLite are scanned via []byte(raw.String),
	// which does NOT require encoding/json. The json_agg block below handles the
	// cases that actually call json.Unmarshal.
	if cfg.Dialect == dburl.DialectSQLite {
		imports["fmt"] = true
		imports["time"] = true
	}

	// All dialects need encoding/json when any query has json_agg fields
	// (for json.Unmarshal of the aggregated JSON string into typed slices).
	for _, qi := range queries {
		for _, r := range qi.Results {
			if len(r.JSONAggCols) > 0 {
				imports["encoding/json"] = true
			}
		}
	}

	// Paginated queries need fmt for fmt.Sprint when building cursors
	for _, qi := range queries {
		if qi.ReturnType == query.ReturnPaginated {
			imports["fmt"] = true
			break
		}
	}

	return imports
}

func collectTypesImports(queries []userQueryInfo, cfg UnifiedRunnerConfig) map[string]bool {
	imports := make(map[string]bool)

	// Check user queries
	for _, qi := range queries {
		// Runner interface needs database/sql for Exec-type queries
		if qi.ReturnType == query.ReturnExec {
			imports["database/sql"] = true
		}
		for _, p := range qi.Params {
			if needsTimeImport(p.GoType) {
				imports["time"] = true
			}
			if needsJSONImport(p.GoType) {
				imports["encoding/json"] = true
			}
		}
		for _, r := range qi.Results {
			if needsTimeImport(r.GoType) {
				imports["time"] = true
			}
			if needsJSONImport(r.GoType) {
				imports["encoding/json"] = true
			}
		}
	}

	return imports
}

func needsTimeImport(goType string) bool {
	return strings.Contains(goType, "time.Time")
}

func needsJSONImport(goType string) bool {
	return strings.Contains(goType, "json.RawMessage")
}

